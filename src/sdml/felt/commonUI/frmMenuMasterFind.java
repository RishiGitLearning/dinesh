    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String menuId = txtMenuId.getText();
        String menuCaption = txtMenuCaption.getText();       
        String moduleId = txtModuleId.getText();
        
        if(!menuId.equals("")) {
            stringFindQuery=" MENU_ID ="+ menuId;
        }
        
        if(!menuCaption.equals("")) {
            stringFindQuery=" MENU_CAPTION LIKE'%"+menuCaption+"%' ";
        }
        
        if(!moduleId.equals("")) {
            stringFindQuery=" MODULE_ID ="+ moduleId;
        }
        
        if(!menuId.equals("") && !menuCaption.equals("")) {
            stringFindQuery=" MENU_ID ="+ menuId +" AND MENU_CAPTION LIKE'%"+menuCaption+"%' ";
        }       
        
        if(!moduleId.equals("") && !menuCaption.equals("")) {
            stringFindQuery=" MODULE_ID ="+ moduleId +" AND MENU_CAPTION LIKE'%"+menuCaption+"%' ";
        }
        
        if(!menuId.equals("") && !moduleId.equals("")) {
            stringFindQuery=" MENU_ID ="+ menuId +" AND MODULE_ID ="+ moduleId;
        }
        
        if(!menuId.equals("") && !menuCaption.equals("") && !moduleId.equals("")) {
            stringFindQuery=" MENU_ID ="+ menuId +" AND MENU_CAPTION LIKE'%"+menuCaption+"%' AND MODULE_ID ="+ moduleId;
        }
        
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
        
