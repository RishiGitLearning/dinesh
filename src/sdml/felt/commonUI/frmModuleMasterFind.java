    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String moduleId = txtModuleId.getText();
        String moduleDesc = txtModuleDesc.getText();       
                
        if(!moduleDesc.equals("")) {
            stringFindQuery=" MODULE_DESC LIKE'%"+moduleDesc+"%' ";
        }
        
        if(!moduleId.equals("")) {
            stringFindQuery=" MODULE_ID ="+ moduleId;
        }
        
        if(!moduleId.equals("") && !moduleDesc.equals("")) {
            stringFindQuery=" MODULE_ID ="+ moduleId +" AND MODULE_DESC LIKE'%"+moduleDesc+"%' ";
        }       
        
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
        
