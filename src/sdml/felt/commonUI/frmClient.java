    private void GenerateCombo() {
        HashMap List = new HashMap();

        cmbUserModel = new SDMLComboModel();
        cmbUser.setModel(cmbUserModel);
        cmbUser.removeAllItems();

        //Add Default users
        ComboData cmbData = new ComboData();
        cmbData.Text = "Server";
        cmbData.strCode = "##SERVER##";
        cmbUserModel.addElement(cmbData);

        cmbData = new ComboData();
        cmbData.Text = "Everybody";
        cmbData.strCode = "##EVERYBODY##";
        cmbUserModel.addElement(cmbData);

        List = MessageServer.getUserList();

        for (int i = 1; i <= List.size(); i++) {
            String theUser = (String) List.get(Integer.toString(i));

            cmbData = new ComboData();
            cmbData.Text = theUser;
            cmbData.strCode = theUser;
            cmbUserModel.addElement(cmbData);
        }

    }

    //Recurses through the hierarchy of classes
    //until it finds Frame
    private Frame findParentFrame(JApplet pApplet) {
        Container c = (Container) pApplet;
        while (c != null) {
            if (c instanceof Frame) {
                return (Frame) c;
            }

            c = c.getParent();
        }
        return (Frame) null;
    }

