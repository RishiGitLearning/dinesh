    private void CalcExpen() {

        double mfare = 0, malwnc = 0, madvance = 0;

        try {
            if (Table.getRowCount() > 0) {
                for (int m = 0; m < Table.getRowCount(); m++) {
                    try {
                        mfare = mfare + Double.parseDouble(Table.getValueAt(m, 7).toString());
                    } catch (Exception a) {

                    }
                    try {
                        malwnc = malwnc + Double.parseDouble(Table.getValueAt(m, 14).toString());
                    } catch (Exception a) {

                    }
                }
            }
            try {
                madvance = Double.parseDouble(advance.getText());
            } catch (Exception a) {

            }
            double mbal = 0, mexpense = 0;
            if (TableExpense.getRowCount() > 0) {
                for (int m = 0; m < TableExpense.getRowCount(); m++) {
                    try {
                        mexpense = mexpense + Double.parseDouble(TableExpense.getValueAt(m, 3).toString());
                    } catch (Exception e) {

                    }
                }
            }
            expense.setText(String.valueOf(mexpense));
            mfare = EITLERPGLOBAL.round(mfare, 0);
            fare.setText(String.valueOf(mfare));
            allowance.setText(String.valueOf(malwnc));
            total.setText(String.valueOf(mfare + malwnc + mexpense));
            balance.setText(String.valueOf((mfare + malwnc + mexpense) - madvance));
            mbal = ((mfare + malwnc) - madvance);
            if (mbal > 0) {
                cheque.setVisible(false);
                cashpay.setVisible(true);
                epayment.setVisible(true);
//                if (gujod.isSelected()) {
//                    epayment.setVisible(false);
//                    cashpay.setSelected(true);
//                }
                if (mbal > 9900) {
                    cashpay.setVisible(false);
                }
            }
            if (mbal < 0) {
                cheque.setVisible(true);
                cashpay.setVisible(true);
                epayment.setVisible(false);
            }
            if (mbal == 0) {
                cheque.setVisible(false);
                cashpay.setVisible(false);
                epayment.setVisible(false);
            }
        } catch (Exception e) {
        }

    }

