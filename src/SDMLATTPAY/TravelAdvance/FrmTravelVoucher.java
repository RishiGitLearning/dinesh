    private void Add() {

        String mDocNo = "";
        LOV aList = new LOV();

        aList.SQL = "SELECT DISTINCT TSD_DOC_NO,RIGHT(TSD_DOC_NO,10) AS DOC_NO,DATE_FORMAT(TSD_DOC_DATE,'%d/%m/%Y') AS DOC_DATE,TSD_PAY_EMP_NO AS EMP_NO,"
                + "TSD_NAME AS NAME "
                + "FROM SDMLATTPAY.TRAVEL_SANCTION_DETAIL D "
                + "LEFT JOIN (SELECT TSD_DOC_NO AS HDOCNO,UPDATED_BY FROM SDMLATTPAY.TRAVEL_SANCTION_DETAIL_H WHERE REVISION_NO=1) AS H "
                + "ON  TSD_DOC_NO=HDOCNO "
                + "WHERE COALESCE(APPROVED,0)=1 AND COALESCE(CANCELED,0)=0   "
                + "AND UPDATED_BY=" + EITLERPGLOBAL.gNewUserID + " "
                + "AND RIGHT(TSD_DOC_NO,10) NOT IN (SELECT RIGHT(TSD_DOC_NO,10) FROM SDMLATTPAY.TRAVEL_VOUCHER_DETAIL WHERE COALESCE(CANCELED,0)=0) "
                + "ORDER BY TSD_DOC_DATE DESC,TSD_DOC_NO DESC";
        aList.ReturnCol = 1;
        aList.ShowReturnCol = true;
        aList.DefaultSearchOn = 1;

        if (aList.ShowLOV()) {
            mDocNo = aList.ReturnVal;

            if (IsAmendmentUnderProcess(mDocNo)) {
                JOptionPane.showMessageDialog(null, "One Travelling Voucher is already under process. Cannot make another Voucher");
                EditMode = 0;
                return;
            }

            EditMode = EITLERPGLOBAL.ADD;
            SetFields(true);
            DisableToolbar();
            clearFields();
            SetupApproval();

            //Display newly generated document no.
            //int MaxNo = data.getIntValueFromDB("SELECT COALESCE(MAX(RIGHT(DOC_NO,1)),0) FROM PRODUCTION.FELT_WARPING_BEAM_ORDER_HEADER_AMEND WHERE BEAM_NO='" + BeamNo + "'") + 1;
            DisplayDataTD(mDocNo);

        } else {
            JOptionPane.showMessageDialog(null, "You must select Travelling Entry DOC No.");
            EditMode = 0;
            Cancel();
        }

        // OpgFinal.setSelected(true);
    }

