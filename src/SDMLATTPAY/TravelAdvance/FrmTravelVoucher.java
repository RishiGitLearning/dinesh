    private void Save() {

        if (TableParty.getRowCount() <= 0) {
            JOptionPane.showMessageDialog(this, "Please Enter Data...", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (SDMLATTPAY.ApprovalFlow.IsCreator(ModuleId, TravellingVoucher.getAttribute("TSD_DOC_NO").getString() + "")) {
            CalcExpen();
        }
        if (EditMode == EITLERPGLOBAL.ADD) {
            CalcExpen();
        }
        String Err = "";
        for (int k = 0; k <= TableParty.getRowCount() - 1; k++) {
            if (TableParty.getValueAt(k, 2).toString().equalsIgnoreCase("")) {
                Err = Err + "\n Select Visit Party at Row " + (k + 1);
            }
            if (TableParty.getValueAt(k, 1).toString().equalsIgnoreCase("")) {
                Err = Err + "\n Enter Visit Date at Row " + (k + 1);
            }
        }
        for (int k = 0; k <= Table.getRowCount() - 1; k++) {
            if (Table.getValueAt(k, 5).toString().equalsIgnoreCase("")) {
                Err = Err + "\n Enter Kms at Row " + (k + 1);
            }
            if (Table.getValueAt(k, 7).toString().equalsIgnoreCase("")) {
                Err = Err + "\n Enter Actual Fare at Row " + (k + 1);
            }
        }
        for (int k = 0; k <= DocTable.getRowCount() - 1; k++) {
            if (DocTable.getValueAt(k, 2).toString().equalsIgnoreCase("")) {
                Err = Err + "\n Select Type in Document  at Row " + (k + 1);
            }
            if (DocTable.getValueAt(k, 7).toString().equalsIgnoreCase("") && DocTable.getValueAt(k, 2).toString().equalsIgnoreCase("REPORT")) {
                Err = Err + "\n Select Party in Document  at Row " + (k + 1);
            }
        }
        if (Err.length() > 0) {
            JOptionPane.showMessageDialog(this, Err, "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        SetData();

        if (cmbHierarchy.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Select the hierarchy.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if ((!OpgApprove.isSelected()) && (!OpgReject.isSelected()) && (!OpgFinal.isSelected()) && (!OpgHold.isSelected())) {
            JOptionPane.showMessageDialog(this, "Select the Approval Action.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (OpgReject.isSelected() && txtToRemarks.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter the remarks for rejection", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if ((OpgApprove.isSelected() || OpgReject.isSelected()) && cmbSendTo.getItemCount() <= 0) {
            JOptionPane.showMessageDialog(this, "Select the user, to whom rejected document to be send", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //TravellingVoucher.LoadData();
        if (EditMode == EITLERPGLOBAL.ADD) {
            if (TravellingVoucher.Insert()) {
                int RevNo = EITLERPGLOBAL.GetQueryResult("SELECT MAX(REVISION_NO) FROM SDMLATTPAY.TRAVEL_VOUCHER_H "
                        + "WHERE DOCUMENT_DOC_NO='" + lbldoc.getText() + "'");
                RevNo++;
                data.Execute("INSERT INTO DOC_MGMT.TRAVEL_VOUCHER_H "
                        + "(REVISION_NO, DOC_ID, DOC_NAME, DOCUMENT, DOCUMENT_DOC_NO, DOCUMENT_SR_NO, DOC_REMARK, DOC_DATETIME,DOC_TYPE,DOC_PARTY_CODE,DOC_PARTY_NAME) "
                        + "SELECT " + RevNo + ",DOC_ID, DOC_NAME, DOCUMENT, DOCUMENT_DOC_NO, DOCUMENT_SR_NO, DOC_REMARK,NOW(),DOC_TYPE,DOC_PARTY_CODE,DOC_PARTY_NAME "
                        + "FROM DOC_MGMT.TRAVEL_VOUCHER D WHERE DOCUMENT_DOC_NO='" + lbldoc.getText() + "'");
                if (OpgFinal.isSelected()) {
                    VoucherGenerate();
                }
                DisplayData();
            } else {
                JOptionPane.showMessageDialog(this, "Error occured while saving. Error is " + TravellingVoucher.LastError, " SAVING ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        if (EditMode == EITLERPGLOBAL.EDIT) {
            if (TravellingVoucher.Update()) {
                int RevNo = EITLERPGLOBAL.GetQueryResult("SELECT MAX(REVISION_NO) FROM SDMLATTPAY.TRAVEL_VOUCHER_H "
                        + "WHERE DOCUMENT_DOC_NO='" + lbldoc.getText() + "'");
                RevNo++;
                data.Execute("INSERT INTO DOC_MGMT.TRAVEL_VOUCHER_H "
                        + "(REVISION_NO, DOC_ID, DOC_NAME, DOCUMENT, DOCUMENT_DOC_NO, DOCUMENT_SR_NO, DOC_REMARK, DOC_DATETIME,DOC_TYPE,DOC_PARTY_CODE,DOC_PARTY_NAME) "
                        + "SELECT " + RevNo + ",DOC_ID, DOC_NAME, DOCUMENT, DOCUMENT_DOC_NO, DOCUMENT_SR_NO, DOC_REMARK,NOW(),DOC_TYPE,DOC_PARTY_CODE,DOC_PARTY_NAME "
                        + "FROM DOC_MGMT.TRAVEL_VOUCHER D WHERE DOCUMENT_DOC_NO='" + lbldoc.getText() + "'");
                if (OpgFinal.isSelected()) {
                    VoucherGenerate();
                }
                DisplayData();
            } else {
                JOptionPane.showMessageDialog(this, "Error occured while saving editing. Error is " + TravellingVoucher.LastError, "SAVING ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        EditMode = 0;
        SetFields(false);
        EnableToolbar();
        SetMenuForRights();
        try {
            if (PENDING_DOCUMENT) {
                frmPA.RefreshView();
                PENDING_DOCUMENT = false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

