    private void cmdAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAddActionPerformed
        Cancelled = false;

        String state = txtstate.getText();
        String city = txtcity.getText();

        if (!city.equals("") && !state.equals("")) {
            stringFindQuery = city;
            data.Execute("INSERT INTO SDMLATTPAY.CITY (CITY_NAME,CITY_STATE,CITY_CLASS) VALUES "
                    + "('"+city+"','"+state+"','C')");
            getParent().getParent().getParent().getParent().show(false);
        }else{
            JOptionPane.showMessageDialog(this, "Enter City and State to Add...");
        }        
    }//GEN-LAST:event_cmdAddActionPerformed

