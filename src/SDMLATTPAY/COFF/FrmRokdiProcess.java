    private void Save() {

        if (txtfromdt.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Enter From Date.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txttodt.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Enter To Date.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtda.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Enter DA", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (Table.getRowCount() <= 0) {
            JOptionPane.showMessageDialog(this, "Please Process Before Saving.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        SetData();

        if (cmbHierarchy.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Select the hierarchy.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if ((!OpgApprove.isSelected()) && (!OpgReject.isSelected()) && (!OpgFinal.isSelected()) && (!OpgHold.isSelected())) {
            JOptionPane.showMessageDialog(this, "Select the Approval Action.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (OpgReject.isSelected() && txtToRemarks.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter the remarks for rejection", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if ((OpgApprove.isSelected() || OpgReject.isSelected()) && cmbSendTo.getItemCount() <= 0) {
            JOptionPane.showMessageDialog(this, "Select the user, to whom rejected document to be send", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //RokdiProcess.LoadData();
        if (EditMode == EITLERPGLOBAL.ADD) {
            if (RokdiProcess.Insert()) {

                if (OpgFinal.isSelected()) {
                    try {

                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
                MoveLast();
                DisplayData();
            } else {
                JOptionPane.showMessageDialog(this, "Error occured while saving. Error is " + RokdiProcess.LastError, " SAVING ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        if (EditMode == EITLERPGLOBAL.EDIT) {
            if (RokdiProcess.Update()) {
                if (OpgFinal.isSelected()) {
                    try {

                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
                MoveLast();
                DisplayData();
            } else {
                JOptionPane.showMessageDialog(this, "Error occured while saving editing. Error is " + RokdiProcess.LastError, "SAVING ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        EditMode = 0;
        SetFields(false);
        EnableToolbar();
        SetMenuForRights();
        try {
            if (PENDING_DOCUMENT) {
                frmPA.RefreshView();
                PENDING_DOCUMENT = false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

