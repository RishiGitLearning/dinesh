        boolean chk = false;
        String c;
        c = data.getStringValueFromDB("SELECT COALESCE(GROUP_CONCAT(DISTINCT COFF_DOC_NO),'') FROM SDMLATTPAY.ATT_COFF_ROKDI_ENTRY WHERE COFF_DOC_NO LIKE 'R%' AND COFF_DATE>='" + EITLERPGLOBAL.formatDateDB(txtfromdt.getText()) + "' AND COFF_DATE<='" + EITLERPGLOBAL.formatDateDB(txttodt.getText()) + "' AND APPROVED=0 AND CANCELED=0");
