        if (c.equalsIgnoreCase("")) {
            chk = true;
        } else {
            JOptionPane.showMessageDialog(this, "Rokdi Document No. " + c + " is Pending.. \n First Approve then Process", "Error", JOptionPane.ERROR_MESSAGE);
            chk = false;
            return false;
        }
        c = data.getStringValueFromDB("SELECT DISTINCT COALESCE(DOC_NO,'') FROM SDMLATTPAY.ROKDI_PAYMENT_DETAIL WHERE DOC_NO='" + txtfromdt.getText() + " - " + txttodt.getText() + "'");
        if (c.equalsIgnoreCase("")) {
            chk = true;
        } else {
            JOptionPane.showMessageDialog(this, "Rokdi is already Processed...", "Error", JOptionPane.ERROR_MESSAGE);
            chk = false;
            return false;
        }
        c = data.getStringValueFromDB("SELECT DISTINCT COALESCE(DOC_NO,'') FROM SDMLATTPAY.ROKDI_PAYMENT_DETAIL WHERE APPROVED=0 AND COALESCE(CANCELED,0)=0");
