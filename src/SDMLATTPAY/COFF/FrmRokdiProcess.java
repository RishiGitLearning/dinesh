                    mtotal = EITLERPGLOBAL.round(Double.parseDouble(rowData[p - 1].toString()) + Double.parseDouble(rowData[p - 2].toString()), 2);
                    rowData[p] = mtotal;
                    p++;
                    rowData[p] = mrhrs;
                    p++;
