                    p++;
                    rowData[p] = hm1.get(t.getString("EMPLOYEE_CODE"));
                    p++;
                    rowData[p] = mbasic;
                    p++;
                    rowData[p] = EITLERPGLOBAL.round((mbasic / 26) * (mhrs / 8), 2);
                    p++;
                    rowData[p] = madhoc;
                    p++;
                    rowData[p] = EITLERPGLOBAL.round((madhoc / 26) * (mhrs / 8), 2);
                    p++;
                    rowData[p] = mda;
                    p++;
                    rowData[p] = EITLERPGLOBAL.round((mda / 26) * (mhrs / 8), 2);
                    p++;
                    rowData[p] = EITLERPGLOBAL.round(EITLERPGLOBAL.round((mbasic / 26) * (mhrs / 8), 2) + EITLERPGLOBAL.round((madhoc / 26) * (mhrs / 8), 2) + EITLERPGLOBAL.round((mda / 26) * (mhrs / 8), 2), 2);
