    private void CalcDiff() {
        String mf, mt, md, sql;
        for (int m = 0; m < Table.getRowCount(); m++) {
            mf = Table.getValueAt(m, 6).toString() + ":00";
            mt = Table.getValueAt(m, 7).toString() + ":00";
            md = data.getStringValueFromDB("SELECT CASE WHEN TIMEDIFF('" + mt + "','" + mf + "')>='00:30:00' THEN TIMEDIFF('" + mt + "','" + mf + "') ELSE '00:00:00' END FROM DUAL");
            if (md.equalsIgnoreCase("00:00:00")) {
                //Table.setValueAt("", m, 9);
            } else {
                Table.setValueAt(md.substring(0, 5), m, 8);
            }
            sql = "SELECT COUNT(*) FROM SDMLATTPAY.ATT_COFF_ROKDI_ENTRY "
                    + "WHERE COFF_EMPID='" + Table.getValueAt(m, 1).toString() + "' "
                    + "AND COFF_DATE='" + EITLERPGLOBAL.formatDateDB(txtrdate.getText()) + "' "
                    + "AND COFF_HRS='" + Table.getValueAt(m, 8).toString() + "' "
                    + "AND COFF_OT_SHIFT='" + Table.getValueAt(m, 5).toString() + "' "
                    + "AND COFF_DOC_NO!='" + lbldoc.getText() + "'";

            if (data.getIntValueFromDB(sql) > 0) {
                JOptionPane.showMessageDialog(null, "Already Coff...");
                DataModel.removeRow(m);
                RearrangeSrNo();
            }
        }
    }
