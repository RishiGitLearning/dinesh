    private boolean DateValidate() {
        if (txtFromDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(FrmShiftScheduleChange.this, "Please Enter From Date");
            txtFromDate.setText("");
            return false;
        }
        if (data.getIntValueFromDB("SELECT DATEDIFF('" + EITLERPGLOBAL.formatDateDB(txtFromDate.getText()) + "',CURDATE()) FROM DUAL") > 0) {
            JOptionPane.showMessageDialog(FrmShiftScheduleChange.this, "Please Enter Less than or Equals Current Date in From Date");
            txtFromDate.setText("");
            return false;
        }

        if (txtToDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(FrmShiftScheduleChange.this, "Please Enter To Date");
            txtToDate.setText("");
            return false;
        }
        if (data.getIntValueFromDB("SELECT DATEDIFF('" + EITLERPGLOBAL.formatDateDB(txtToDate.getText()) + "','" + EITLERPGLOBAL.formatDateDB(txtFromDate.getText()) + "') FROM DUAL") < 0) {
            JOptionPane.showMessageDialog(FrmShiftScheduleChange.this, "Please Enter Greater Date than From Date in To Date");
            txtToDate.setText("");
            return false;
        }

        return true;
    }

