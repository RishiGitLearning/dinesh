    private void TableKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TableKeyPressed
        // TODO add your handling code here:
        if (EditMode == EITLERPGLOBAL.ADD || EditMode == EITLERPGLOBAL.EDIT) {
            if (evt.getKeyCode() == 112) //F1 Key pressed
            {
                if (Table.getSelectedColumn() == 1) {
                    LOV aList = new LOV();
                    aList.SQL = "SELECT PAY_EMP_NO AS EMP_NO,EMP_NAME,COALESCE(NAME,'') AS CATEGORY "
                            + "FROM SDMLATTPAY.ATTPAY_EMPMST M "
                            + "LEFT JOIN SDMLATTPAY.ATT_DESIGNATION_MASTER D ON DSGID=EMP_DESIGNATION "
                            + "WHERE COALESCE(EMP_LEFT_DATE,'0000-00-00 00:00:00')='0000-00-00 00:00:00' AND EMP_DEPARTMENT IN(28,58,53) "
                            + "AND LEFT(PAY_EMP_NO,5) IN ('BRD00','BRD30') "
                            + "ORDER BY DESIGNATION_PRIORITY,NAME,PAY_EMP_NO ";
                    aList.ReturnCol = 1;
                    aList.ShowReturnCol = true;
                    aList.DefaultSearchOn = 1;

                    if (aList.ShowLOV()) {
                        Table.setValueAt(aList.ReturnVal, Table.getSelectedRow(), 1);
                        Table.setValueAt(data.getStringValueFromDB("SELECT EMP_NAME FROM SDMLATTPAY.ATTPAY_EMPMST WHERE PAY_EMP_NO='" + aList.ReturnVal + "'"), Table.getSelectedRow(), 2);
                        Table.setValueAt(data.getStringValueFromDB("SELECT COALESCE(NAME,'') FROM SDMLATTPAY.ATTPAY_EMPMST  M "
                                + "LEFT JOIN SDMLATTPAY.ATT_DESIGNATION_MASTER D ON DSGID=EMP_DESIGNATION "
                                + "WHERE PAY_EMP_NO='" + aList.ReturnVal + "'"), Table.getSelectedRow(), 3);
                    }
                }
            }
            final TableColumnModel columnModel = Table.getColumnModel();
            for (int column = 0; column < Table.getColumnCount(); column++) {
                int width = 60; // Min width
                for (int row = 0; row < Table.getRowCount(); row++) {
                    TableCellRenderer renderer = Table.getCellRenderer(row, column);
                    Component comp = Table.prepareRenderer(renderer, row, column);
                    width = Math.max(comp.getPreferredSize().width + 1, width);
                }
                if (width > 300) {
                    width = 300;
                }
                columnModel.getColumn(column).setPreferredWidth(width);
            }
        }
    }//GEN-LAST:event_TableKeyPressed

