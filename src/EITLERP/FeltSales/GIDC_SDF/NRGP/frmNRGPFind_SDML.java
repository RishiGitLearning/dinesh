    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String gpDate = txtGatepassDate.getText();       
        String gpNo = txtGatepassNo.getText();
        String itemCode = txtPieceNo.getText();        
        
        if(!gpDate.equals("")) {
            stringFindQuery=stringFindQuery+" WHERE GATEPASS_TYPE='BS' AND GATEPASS_DATE='"+EITLERPGLOBAL.formatDateDB(gpDate)+"' ";
        }
        
        if(!gpNo.equals("")) {
            stringFindQuery=stringFindQuery+" WHERE GATEPASS_TYPE='BS' AND GATEPASS_NO ='"+ gpNo +"'";
        }
        
        if(!itemCode.equals("")) {
            stringFindQuery=stringFindQuery+" WHERE GATEPASS_TYPE='BS' AND ITEM_CODE ='"+ itemCode +"'";
        }
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
