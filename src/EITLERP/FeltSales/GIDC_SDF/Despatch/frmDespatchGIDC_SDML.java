    //Display data on the Screen
    private void DisplayData() {
        //========= Authority Delegation Check =====================//
        if (EITLERPGLOBAL.gAuthorityUserID != EITLERPGLOBAL.gUserID) {
            if (clsAuthority.IsAuthorityGiven(EITLERPGLOBAL.gCompanyID, EITLERPGLOBAL.gUserID, EITLERPGLOBAL.gAuthorityUserID, 792)) {
                EITLERPGLOBAL.gNewUserID = EITLERPGLOBAL.gAuthorityUserID;
            } else {
                EITLERPGLOBAL.gNewUserID = EITLERPGLOBAL.gUserID;
            }
        }
        //==========================================================//

        //=========== Title Bar Color Indication ===============//
        try {
            if (EditMode == 0) {
                if (ObjFeltGIDCSDF.getAttribute("APPROVED").getInt() == 1) {
                    lblTitle.setBackground(Color.BLUE);
                } else {
                    lblTitle.setBackground(Color.GRAY);
                }

                if (ObjFeltGIDCSDF.getAttribute("CANCELED").getInt() == 1) {
                    lblTitle.setBackground(Color.RED);
                }
            }
            //============================================//

            String docDate = EITLERPGLOBAL.formatDate(ObjFeltGIDCSDF.getAttribute("DOC_DATE").getString());
            String docNo = ObjFeltGIDCSDF.getAttribute("DOC_NO").getString();

            lblTitle.setText(" GIDC Despatch to SDML [SDF] - " + docNo);
            lblRevNo.setText(Integer.toString((int) ObjFeltGIDCSDF.getAttribute("REVISION_NO").getVal()));
            EITLERPGLOBAL.setComboIndex(cmbHierarchy, ObjFeltGIDCSDF.getAttribute("HIERARCHY_ID").getInt());
            DoNotEvaluate = true;

            //Now Generate Table
            FormatGrid();

            for (int i = 1; i <= ObjFeltGIDCSDF.hmFeltGIDCSDFDetails.size(); i++) {
                clsDespatchGIDC_SDMLDetails ObjFeltGIDCSDFDetails = (clsDespatchGIDC_SDMLDetails) ObjFeltGIDCSDF.hmFeltGIDCSDFDetails.get(Integer.toString(i));

                Object[] rowData = new Object[50];

                txtDocDate.setText(docDate);
                txtDocNo.setText(docNo);

                rowData[0] = ObjFeltGIDCSDFDetails.getAttribute("SR_NO").getString();
                rowData[1] = ObjFeltGIDCSDFDetails.getAttribute("PIECE_NO").getString();
                rowData[2] = ObjFeltGIDCSDFDetails.getAttribute("PARTY_CODE").getString();
                rowData[3] = ObjFeltGIDCSDFDetails.getAttribute("PARTY_NAME").getString();
                rowData[4] = ObjFeltGIDCSDFDetails.getAttribute("STYLE_CODE").getString();
                rowData[5] = ObjFeltGIDCSDFDetails.getAttribute("LENGTH").getString();
                rowData[6] = ObjFeltGIDCSDFDetails.getAttribute("WIDTH").getString();
                rowData[7] = ObjFeltGIDCSDFDetails.getAttribute("WEIGHT").getString();
                rowData[8] = ObjFeltGIDCSDFDetails.getAttribute("GSM").getString();
                rowData[9] = ObjFeltGIDCSDFDetails.getAttribute("SQMTR").getString();
                rowData[10] = ObjFeltGIDCSDFDetails.getAttribute("ACTUAL_LENGTH").getString();
                rowData[11] = ObjFeltGIDCSDFDetails.getAttribute("ACTUAL_WIDTH").getString();
                rowData[12] = ObjFeltGIDCSDFDetails.getAttribute("ACTUAL_WEIGHT").getString();
                rowData[13] = ObjFeltGIDCSDFDetails.getAttribute("WASTE_LENGTH").getString();
                rowData[14] = ObjFeltGIDCSDFDetails.getAttribute("WASTE_WEFT").getString();
                rowData[15] = ObjFeltGIDCSDFDetails.getAttribute("REMARKS").getString();

                ResultSet rs = data.getResult("SELECT * FROM PRODUCTION.GIDC_FELT_PIECE_REGISTER WHERE GIDC_PIECE_NO='" + ObjFeltGIDCSDFDetails.getAttribute("PIECE_NO").getString() + "'");
                rs.first();

                rowData[16] = rs.getString("GIDC_RMCODE_LENGTH_WEIGHT");
                rowData[17] = rs.getString("GIDC_RMCODE_WEFT_WEIGHT");
                rowData[18] = rs.getString("GIDC_RMCODE_LENGTH");
                rowData[19] = rs.getString("GIDC_RMCODE_WEFT");

                rowData[20] = ObjFeltGIDCSDFDetails.getAttribute("RATE").getString();
                rowData[21] = ObjFeltGIDCSDFDetails.getAttribute("NET_AMOUNT").getString();
                DataModel.addRow(rowData);
            }

            //======== Generating Grid for Document Approval Flow ========//
            FormatGridApprovalStatus();
            HashMap hmList = new HashMap();

            hmList = clsFeltProductionApprovalFlow.getDocumentFlow(792, ObjFeltGIDCSDF.getAttribute("DOC_NO").getString());
            for (int i = 1; i <= hmList.size(); i++) {

                clsDocFlow ObjFlow = (clsDocFlow) hmList.get(Integer.toString(i));
                Object[] rowData = new Object[7];

                rowData[0] = Integer.toString(i);
                rowData[1] = clsUser.getUserName(EITLERPGLOBAL.gCompanyID, (int) ObjFlow.getAttribute("USER_ID").getVal());
                rowData[2] = clsDepartment.getDeptName(EITLERPGLOBAL.gCompanyID, clsUser.getDeptID(EITLERPGLOBAL.gCompanyID, (int) ObjFlow.getAttribute("USER_ID").getVal()));
                rowData[3] = ObjFlow.getAttribute("STATUS").getString();
                rowData[4] = EITLERPGLOBAL.formatDate(ObjFlow.getAttribute("RECEIVED_DATE").getString());
                rowData[5] = EITLERPGLOBAL.formatDate(ObjFlow.getAttribute("ACTION_DATE").getString());
                rowData[6] = ObjFlow.getAttribute("REMARKS").getString();

                DataModelApprovalStatus.addRow(rowData);
            }
            //============================================================//

            // Generating Grid for Showing Production Details Update History
            FormatGridUpdateHistory();
            HashMap hmApprovalHistory = clsDespatchGIDC_SDML.getHistoryList(ObjFeltGIDCSDF.getAttribute("DOC_DATE").getString(), ObjFeltGIDCSDF.getAttribute("DOC_NO").getString());
            for (int i = 1; i <= hmApprovalHistory.size(); i++) {
                clsDespatchGIDC_SDML ObjFeltGIDCSDF = (clsDespatchGIDC_SDML) hmApprovalHistory.get(Integer.toString(i));
                Object[] rowData = new Object[6];

                rowData[0] = Integer.toString((int) ObjFeltGIDCSDF.getAttribute("REVISION_NO").getVal());
                rowData[1] = clsUser.getUserName(2, (int) ObjFeltGIDCSDF.getAttribute("UPDATED_BY").getVal());
                rowData[2] = ObjFeltGIDCSDF.getAttribute("ENTRY_DATE").getString();

                String ApprovalStatus = "";

                if ((ObjFeltGIDCSDF.getAttribute("APPROVAL_STATUS").getString()).equals("H")) {
                    ApprovalStatus = "Hold";
                }

                if ((ObjFeltGIDCSDF.getAttribute("APPROVAL_STATUS").getString()).equals("A")) {
                    ApprovalStatus = "Approved";
                }

                if ((ObjFeltGIDCSDF.getAttribute("APPROVAL_STATUS").getString()).equals("F")) {
                    ApprovalStatus = "Final Approved";
                }

                if ((ObjFeltGIDCSDF.getAttribute("APPROVAL_STATUS").getString()).equals("W")) {
                    ApprovalStatus = "Waiting";
                }

                if ((ObjFeltGIDCSDF.getAttribute("APPROVAL_STATUS").getString()).equals("R")) {
                    ApprovalStatus = "Rejected";
                }

                if ((ObjFeltGIDCSDF.getAttribute("APPROVAL_STATUS").getString()).equals("P")) {
                    ApprovalStatus = "Pending";
                }

                if ((ObjFeltGIDCSDF.getAttribute("APPROVAL_STATUS").getString()).equals("C")) {
                    ApprovalStatus = "Skiped";
                }

                rowData[3] = ApprovalStatus;
                rowData[4] = ObjFeltGIDCSDF.getAttribute("APPROVER_REMARKS").getString();
                rowData[5] = ObjFeltGIDCSDF.getAttribute("FROM_IP").getString();

                DataModelUpdateHistory.addRow(rowData);
            }
            SetFields(false);
        } catch (Exception e) {
            e.printStackTrace();
        }

        DoNotEvaluate = false;
    }

