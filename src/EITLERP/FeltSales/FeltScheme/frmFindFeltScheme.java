    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String DocNo = txtDocNo.getText();
        String DocDate = txtDocDate.getText();   
        String PartyCode = txtPartyCode.getText();
        String PieceNo = txtPieceNo.getText();
        
        if(!DocNo.equals("")) {
            stringFindQuery=" DOC_NO ='"+ DocNo +"'";
        }
        
        if(!DocDate.equals("")) {
            stringFindQuery=" DOC_DATE='"+EITLERPGLOBAL.formatDateDB(DocDate)+"' ";
        }
        
        if(!PartyCode.equals("")) {
            stringFindQuery=" PARTY_CODE ='"+ PartyCode +"'";
        }
        
       
        
        if(!DocNo.equals("") && !DocDate.equals("")) {
            stringFindQuery=" DOC_NO ='"+ DocNo +"' AND DOC_DATE='"+EITLERPGLOBAL.formatDateDB(DocDate)+"' ";
        }
        
        
        if (!PieceNo.equals("")) {
            stringFindQuery = " PIECE_NO ='" + PieceNo + "'";
        }

        
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed

