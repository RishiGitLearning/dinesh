    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String docDate = jTextFieldDocDate.getText();       
        String docNo = jTextFieldDocNo.getText();
        String partyCode = jTextFieldPartyCode.getText();        
        
        if(!docDate.equals("")) {
            stringFindQuery=stringFindQuery+" PDC_DOC_DATE='"+EITLERPGLOBAL.formatDateDB(docDate)+"' ";
        }
        
        if(!docNo.equals("")) {
            stringFindQuery=stringFindQuery+" PDC_DOC_NO ='"+ docNo +"'";
        }
        
        if(!partyCode.equals("")) {
            stringFindQuery=stringFindQuery+" PDC_PARTY_CODE ='"+ partyCode +"'";
        }
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
