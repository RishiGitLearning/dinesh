    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String DocNo = txtDocNo.getText();
        String ProcessDate = txtProcessDate.getText();
        String PartyCode = txtPartyCode.getText();
        String BaleNo = txtBaleNo.getText();
        String FromDate = txtFromDate.getText();
        String ToDate = txtToDate.getText();
        
        if(!DocNo.equals("")) {
            stringFindQuery=" DOC_NO ='"+ DocNo +"'";
        }
        
        if(!ProcessDate.equals("")) {
            stringFindQuery=" PROCESSING_DATE='"+EITLERPGLOBAL.formatDateDB(ProcessDate)+"' ";
        }
        
        if(!PartyCode.equals("")) {
            stringFindQuery=" PARTY_CODE ='"+ PartyCode +"'";
        }
        
        if(!BaleNo.equals("")) {
            stringFindQuery=" BALE_NO ='"+ BaleNo +"'";
        }
        
        if(!DocNo.equals("") && !ProcessDate.equals("")) {
            stringFindQuery=" DOC_NO ='"+ DocNo +"' AND PROCESSING_DATE='"+EITLERPGLOBAL.formatDateDB(ProcessDate)+"' ";
        }       
        
        if(!FromDate.equals("") && !ToDate.equals("")) {
            stringFindQuery=" DOC_DATE>='"+EITLERPGLOBAL.formatDateDB(FromDate)+"' AND DOC_DATE<='"+EITLERPGLOBAL.formatDateDB(ToDate)+"' ";
        }
        
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
        
