    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String documentDate = jTextFieldDocumentDate.getText();       
        String pieceNo = jTextFieldPieceNo.getText();
        String consignmentNo = jTextFieldConsignmentNo.getText();        
        
        if(!documentDate.equals("")) {
            stringFindQuery=stringFindQuery+" AND CON_DOC_DATE='"+EITLERPGLOBAL.formatDateDB(documentDate)+"' ";
        }
        
        if(!pieceNo.equals("")) {
            stringFindQuery=stringFindQuery+" AND CON_PIECE_NO ='"+ pieceNo +"'";
        }
        
        if(!consignmentNo.equals("")) {
            stringFindQuery=stringFindQuery+" AND CON_NO ='"+ consignmentNo +"'";
        }
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
