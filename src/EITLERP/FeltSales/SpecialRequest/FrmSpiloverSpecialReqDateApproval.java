    private void Add() {
        //  EditMode=EITLERPGLOBAL.ADD;

        if (!EITLERPGLOBAL.YearIsOpen) {
            JOptionPane.showMessageDialog(null, "The year is closed. You cannot enter/edit any transaction");
            return;
        }
        clearFields();

        EditMode = EITLERPGLOBAL.ADD;
        DisableToolbar();
        SetFields(true);
        

        SetupApproval();
        lblTitle.setBackground(new Color(0, 102, 153));
        lblTitle.setForeground(Color.WHITE);

        SelectFirstFree aList = new SelectFirstFree();
        aList.ModuleID = ModuleId;
        aList.FirstFreeNo = 371;

        OCDDate.setText(df.format(new Date()));
        FFNo = aList.FirstFreeNo;
        OCDNo.setText(clsFirstFree.getNextFreeNo(EITLERPGLOBAL.gCompanyID, ModuleId, FFNo, false));
        lblTitle.setText("Felt Spilover Special Request Date Approval - " + OCDNo.getText());
        //OpgHold.setSelected(true);
        FormatGrid();
        try{
            
            ResultSet rsData = data.getResult("SELECT * FROM PRODUCTION.SPILLOVER_RESCHEDULING_REQ_MONTH_SPCL_REQ_DATE where UPDATE_STATUS='PENDING' "
                    + " AND PIECE_NO NOT IN (SELECT D.PIECE_NO FROM PRODUCTION.SPILLOVER_REQ_MONTH_SPCL_REQ_DATE_APPROVAL_HEADER H,PRODUCTION.SPILLOVER_REQ_MONTH_SPCL_REQ_DATE_APPROVAL_DETAIL D WHERE H.APPROVED=0 AND H.CANCELED=0 AND H.DOC_NO=D.DOC_NO)");
            
            rsData.first();
            while(!rsData.isAfterLast())
            {
                Object[] rowData = new Object[1];
                DataModel.addRow(rowData);
                int NewRow = Table.getRowCount() - 1;
                DataModel.setValueByVariable("SrNo", (NewRow + 1) + "", NewRow);

                DataModel.setValueByVariable("PIECE_NO", rsData.getString("PIECE_NO"), NewRow);
                DataModel.setValueByVariable("SPL_DATE_APPROVAL", false, NewRow);
                DataModel.setValueByVariable("UPN", rsData.getString("UPN"), NewRow);
                DataModel.setValueByVariable("PARTY_CODE", rsData.getString("PARTY_CODE"), NewRow);
                DataModel.setValueByVariable("PARTY_NAME", rsData.getString("PARTY_NAME"), NewRow);
                DataModel.setValueByVariable("REQ_MONTH", rsData.getString("REQ_MONTH"), NewRow);
                DataModel.setValueByVariable("OC_MONTH", rsData.getString("OC_MONTH"), NewRow);
                DataModel.setValueByVariable("SPECIAL_REQ_DATE", rsData.getString("SPECIAL_REQ_DATE"), NewRow);
                
                rsData.next();
            }
        }catch(Exception e)
        {
            e.printStackTrace();
        }
    }

