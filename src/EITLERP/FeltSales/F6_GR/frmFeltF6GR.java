    private void TableKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TableKeyPressed
        // TODO add your handling code here:
        if (EditMode == EITLERPGLOBAL.ADD || EditMode == EITLERPGLOBAL.EDIT) {
            if (evt.getKeyCode() == 112) //F1 Key pressed
            {
                if (Table.getSelectedColumn() == 1) {
                    LOV aList = new LOV();

                    String strSQL = "SELECT I.PIECE_NO,I.PARTY_CODE,I.PARTY_NAME,I.INVOICE_NO,I.INVOICE_DATE,I.INVOICE_AMT "
                            + "FROM PRODUCTION.FELT_SALES_RETURNS_HEADER H, PRODUCTION.FELT_SALES_RETURNS_DETAIL D, "
                            + "PRODUCTION.FELT_SAL_INVOICE_HEADER I "
                            + "WHERE H.DOC_NO=D.DOC_NO AND H.APPROVED=1 AND H.CANCELED=0 "
                            + "AND D.PARTY_CODE=I.PARTY_CODE AND D.NEW_PIECE_NO=I.PIECE_NO AND I.APPROVED=1 AND I.CANCELLED=0 "
                            + "AND D.RETURN_CATEGORY='F6' AND I.PARTY_CHARGE_CODE IN ('09','07') "
                            + "AND I.PIECE_NO NOT IN (SELECT INVOICE_PIECE_NO FROM PRODUCTION.FELT_F6GR_ELIGIBLE_INV_LIST WHERE CANCELED=0) "
                            + "AND I.INVOICE_DATE>='" + EITLERPGLOBAL.formatDateDB(txtFromDate.getText().trim()) + "' "
                            + "AND I.INVOICE_DATE<='" + EITLERPGLOBAL.formatDateDB(txtToDate.getText().trim()) + "' "
                            + "ORDER BY I.INVOICE_DATE,I.INVOICE_NO ";
                    aList.SQL = strSQL;
                    aList.ReturnCol = 1;
                    aList.ShowReturnCol = true;
                    //aList.DefaultSearchOn=;
                    aList.DefaultSearchOn = 1;

                    if (aList.ShowLOV()) {
                        if (Table.getCellEditor() != null) {
                            Table.getCellEditor().stopCellEditing();
                        }
                        Table.setValueAt(aList.ReturnVal, Table.getSelectedRow(), 1);
                        String partycode = clsFeltF6GR.getpartycode(aList.ReturnVal);
                        Table.setValueAt(partycode, Table.getSelectedRow(), 2);
                        String partyname = clsFeltF6GR.getpartyname(aList.ReturnVal);
                        Table.setValueAt(partyname, Table.getSelectedRow(), 3);
                        String invoiceno = clsFeltF6GR.getinvoiceno(aList.ReturnVal);
                        Table.setValueAt(invoiceno, Table.getSelectedRow(), 4);
                        String invoicedate = EITLERPGLOBAL.formatDate(clsFeltF6GR.getinvoicedate(aList.ReturnVal));
                        Table.setValueAt(invoicedate, Table.getSelectedRow(), 5);
                        String invamt = clsFeltF6GR.getinvoiceamt(aList.ReturnVal);
                        Table.setValueAt(invamt, Table.getSelectedRow(), 6);
                    }

                }
            }
        }
    }//GEN-LAST:event_TableKeyPressed

