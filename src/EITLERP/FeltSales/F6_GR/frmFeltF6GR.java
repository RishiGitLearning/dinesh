    private void Save() {
        String DocDate = "", DocNo = "", FromDate = "", ToDate = "";
        int i = 0, j = 0;
        String invNo = "", invDate = "", partyName = "", partyCode = "", pieceNo = "", invAmt = "";

        DocDate = txtDocDate.getText().trim();
        DocNo = txtDocNo.getText().trim();
        FromDate = txtFromDate.getText().trim();
        ToDate = txtToDate.getText().trim();

        //Form level validations
        if (DocNo.equals("") || DocNo.trim().equals("")) {
            JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter Valid DocNo.", "Wrong Document No", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (DocDate.equals("") || !EITLERPGLOBAL.isDate(DocDate)) {
            JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter Valid DocDate.", "Wrong Document Date", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (FromDate.equals("") || !EITLERPGLOBAL.isDate(FromDate)) {
            JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter Valid FromDate.", "Wrong From Date", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (ToDate.equals("") || !EITLERPGLOBAL.isDate(ToDate)) {
            JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter Valid ToDate.", "Wrong To Date", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // check Production Date is Within Financial Year?
        java.sql.Date FinFromDate = java.sql.Date.valueOf(EITLERPGLOBAL.FinFromDateDB);
        java.sql.Date FinToDate = java.sql.Date.valueOf(EITLERPGLOBAL.FinToDateDB);
        java.sql.Date Doc_Date = java.sql.Date.valueOf(EITLERPGLOBAL.formatDateDB(DocDate));
        if ((Doc_Date.after(FinFromDate) || Doc_Date.compareTo(FinFromDate) == 0) && (Doc_Date.before(FinToDate) || Doc_Date.compareTo(FinToDate) == 0)) {
            //Within the year
        } else {
            JOptionPane.showMessageDialog(frmFeltF6GR.this, "Doc Date is Not Within Financial Year.", "FINANCIAL YEAR ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (Doc_Date.compareTo(java.sql.Date.valueOf(EITLERPGLOBAL.getCurrentDateDB())) > 0) {
            JOptionPane.showMessageDialog(frmFeltF6GR.this, "Doc Date Must be Before or the Same Date as Today.", "Wrong Production Date", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //Check the no. of items in table
        if (Table.getRowCount() <= 0) {
            JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter F6 GR Invoice Details Before Saving.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // check duplicate piece no in table
            for (int k = 0; k <= Table.getRowCount() - 1; k++) {
                for (int l = k; l <= Table.getRowCount() - 1; l++) {
                    if (l != k && ((String) Table.getValueAt(k, 1)).trim().equals(((String) Table.getValueAt(l, 1)).trim())) {
                        JOptionPane.showMessageDialog(this, "Same Invoice No at Row " + (k + 1) + " and " + (l + 1), "ERROR", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }
            }

            ObjFeltOrderUpd.hmFeltOrderUpdDetails.clear();

            //Check the entered details in Table.
            for (i = 0; i <= Table.getRowCount() - 1; i++) {

                // Piece no Validation before Saving  
                j++;
                pieceNo = ((String) Table.getValueAt(i, 1)).trim().toUpperCase();
                if (pieceNo.equals("") || pieceNo.equals(null)) {
                    JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter Piece No.", "ERROR", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                j++;
                partyCode = ((String) Table.getValueAt(i, 2)).trim();
                if (partyCode.equals("") || partyCode.equals(null)) {
                    JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter Party Code.", "ERROR", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                j++;
                partyName = ((String) Table.getValueAt(i, 3)).trim();
                if (partyName.equals("") || partyName.equals(null)) {
                    JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter Party Name", "ERROR", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                j++;
                invNo = ((String) Table.getValueAt(i, 4)).trim().toUpperCase();
                if (invNo.equals("") || invNo.equals(null)) {
                    JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter Invoice No.", "ERROR", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                j++;
                invDate = ((String) Table.getValueAt(i, 5)).trim();
                if (invDate.equals("") || invDate.equals(null)) {
                    JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter Invoice Date.", "ERROR", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                j++;
                invAmt = ((String) Table.getValueAt(i, 6)).trim();
                if (invAmt.equals("") || invAmt.equals(null)) {
                    JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter Invoice Amount.", "ERROR", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                // check piece no is already weaved?
                clsFeltF6GRDetails ObjFeltOrderUpdDetails = new clsFeltF6GRDetails();

                ObjFeltOrderUpdDetails.setAttribute("INVOICE_NO", invNo);
                ObjFeltOrderUpdDetails.setAttribute("INVOICE_DATE", invDate);
                ObjFeltOrderUpdDetails.setAttribute("PARTY_CODE", partyCode);
                ObjFeltOrderUpdDetails.setAttribute("PARTY_NAME", partyName);
                ObjFeltOrderUpdDetails.setAttribute("INVOICE_PIECE_NO", pieceNo);
                ObjFeltOrderUpdDetails.setAttribute("INVOICE_AMOUNT", invAmt);

                ObjFeltOrderUpd.hmFeltOrderUpdDetails.put(Integer.toString(ObjFeltOrderUpd.hmFeltOrderUpdDetails.size() + 1), ObjFeltOrderUpdDetails);
            }
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter Correct Details at Row " + (i + 1) + " and Column " + (j + 1) + ". Error is " + nfe.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            j = 0;
            nfe.printStackTrace();
            return;
        }

        if (cmbHierarchy.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(frmFeltF6GR.this, "Select the hierarchy.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if ((!OpgApprove.isSelected()) && (!OpgReject.isSelected()) && (!OpgFinal.isSelected()) && (!OpgHold.isSelected())) {
            JOptionPane.showMessageDialog(frmFeltF6GR.this, "Select the Approval Action.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (OpgReject.isSelected() && txtToRemarks.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(frmFeltF6GR.this, "Enter the remarks for rejection", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if ((OpgApprove.isSelected() || OpgReject.isSelected()) && cmbSendTo.getItemCount() <= 0) {
            JOptionPane.showMessageDialog(frmFeltF6GR.this, "Select the user, to whom rejected document to be send", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //set data for insert/update
        ObjFeltOrderUpd.setAttribute("FELT_AMEND_ID", DocNo);
        ObjFeltOrderUpd.setAttribute("DOC_DATE", DocDate);
        ObjFeltOrderUpd.setAttribute("FROM_DATE", FromDate);
        ObjFeltOrderUpd.setAttribute("TO_DATE", ToDate);

        SetData();

        if (EditMode == EITLERPGLOBAL.ADD) {
            if (ObjFeltOrderUpd.Insert()) {
                DisplayData();
            } else {
                JOptionPane.showMessageDialog(frmFeltF6GR.this, "Error occured while saving. Error is " + ObjFeltOrderUpd.LastError, "SAVING ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        if (EditMode == EITLERPGLOBAL.EDIT) {
            if (ObjFeltOrderUpd.Update()) {
                DisplayData();
            } else {
                JOptionPane.showMessageDialog(frmFeltF6GR.this, "Error occured while saving editing. Error is " + ObjFeltOrderUpd.LastError, "SAVING ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        EditMode = 0;
        SetFields(false);
        EnableToolbar();
        SetMenuForRights();
        try {
            if (PENDING_DOCUMENT) {
                frmPA.RefreshView();
                PENDING_DOCUMENT = false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

