        ObjFeltOrderUpd.setAttribute("HIERARCHY_ID", EITLERPGLOBAL.getComboCode(cmbHierarchy));
        ObjFeltOrderUpd.setAttribute("FROM", EITLERPGLOBAL.gNewUserID);
