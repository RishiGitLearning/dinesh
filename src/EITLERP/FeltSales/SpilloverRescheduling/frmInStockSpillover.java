    private void Display_Req_Month_Spcl_Req_Date()
    {
        try{
               FormatGrid_ReqMonthSpclReqDate();
                
               ResultSet rs = data.getResult("SELECT * FROM PRODUCTION.SPILLOVER_RESCHEDULING_REQ_MONTH_SPCL_REQ_DATE where DOC_NO='"+DFNo.getText()+"'");
               if(data.IsRecordExist("SELECT * FROM PRODUCTION.SPILLOVER_RESCHEDULING_REQ_MONTH_SPCL_REQ_DATE where DOC_NO='"+DFNo.getText()+"'"))
               {
                    rs.first();
                    int i=1;
                    while(!rs.isAfterLast())
                    {

                        Object[] rowData = new Object[100];

                        rowData[0] = i;
                        rowData[1] = rs.getString("PIECE_NO");
                        rowData[2] = rs.getString("UPN");
                        rowData[3] = rs.getString("PARTY_CODE");
                        rowData[4] = rs.getString("PARTY_NAME");
                        rowData[5] = rs.getString("REQ_MONTH");
                        rowData[6] = rs.getString("OC_MONTH");
                        rowData[7] = rs.getString("UPDATED_REQ_MONTH");
                        rowData[8] = rs.getString("SPECIAL_REQ_DATE");
                        rowData[9] = rs.getString("UPDATE_STATUS");

                        i++;
                        DataModelReqMonthSpclReqDate.addRow(rowData);
                        rs.next();
                    }
               }
               
       }catch(Exception e)
       {
           e.printStackTrace();
       }
    }
    
