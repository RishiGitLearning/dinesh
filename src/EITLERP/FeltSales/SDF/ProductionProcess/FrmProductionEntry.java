    private void SetData() {

        //======= Set Line part ============
        try {
            
            sdfProductionEntry.hmPieceDetails.clear();
            int sr_no=0;
            for (int i = 0; i <= tbl_TO_SPIRALED.getRowCount() - 1; i++) {
                sr_no = sr_no+1;
                clsPieceDetails piece = new clsPieceDetails();
                //objsdfProductionEntryDetails.setAttribute("S_ORDER_DETAIL_CODE","");
                piece.setAttribute("PP_NO", PPNo.getText());
                piece.setAttribute("SR_NO", sr_no);
                piece.setAttribute("PIECE_NO", tbl_TO_SPIRALED.getValueAt(i, 0));
                piece.setAttribute("PIECE_STAGE", "SPIRALED");
                
                
                sdfProductionEntry.setAttribute("PRODUCTION_DOCUMENT_NO", PPNo.getText());
                sdfProductionEntry.setAttribute("PRODUCTION_DATE", PPDate.getText());
 
                
                sdfProductionEntry.setAttribute("SPIRALED_REMARK", txtRemark_SPIRALED.getText());
                
                DOC_NO = PPNo.getText();
                sdfProductionEntry.setAttribute("DOC_NO", PPNo.getText());
                sdfProductionEntry.setAttribute("DOC_DATE", PPDate.getText());
                sdfProductionEntry.setAttribute("MODULE_ID", ModuleId);
                sdfProductionEntry.setAttribute("USER_ID", EITLERPGLOBAL.gNewUserID);

                //----- Update Approval Specific Fields -----------//
                sdfProductionEntry.setAttribute("HIERARCHY_ID", EITLERPGLOBAL.getComboCode(cmbHierarchy));
                sdfProductionEntry.setAttribute("FROM", EITLERPGLOBAL.gNewUserID);
                sdfProductionEntry.setAttribute("TO", EITLERPGLOBAL.getComboCode(cmbSendTo));
                sdfProductionEntry.setAttribute("FROM_REMARKS", txtToRemarks.getText());
                

                if (OpgApprove.isSelected()) {
                    sdfProductionEntry.setAttribute("APPROVAL_STATUS", "A");
                }

                if (OpgFinal.isSelected()) {
                    sdfProductionEntry.setAttribute("APPROVAL_STATUS", "F");
                }

                if (OpgReject.isSelected()) {
                    sdfProductionEntry.setAttribute("APPROVAL_STATUS", "R");
                    sdfProductionEntry.setAttribute("SEND_DOC_TO", EITLERPGLOBAL.getComboCode(cmbSendTo));
                }

                if (OpgHold.isSelected()) {
                    sdfProductionEntry.setAttribute("APPROVAL_STATUS", "H");
                }

                if (EditMode == EITLERPGLOBAL.ADD) {
                    sdfProductionEntry.setAttribute("CREATED_BY", EITLERPGLOBAL.gNewUserID);
                    sdfProductionEntry.setAttribute("CREATED_DATE", EITLERPGLOBAL.getCurrentDateTimeDB());
                } else {
                    sdfProductionEntry.setAttribute("MODIFIED_BY", EITLERPGLOBAL.gNewUserID);
                    sdfProductionEntry.setAttribute("MODIFIED_DATE", EITLERPGLOBAL.getCurrentDateDB());
                    sdfProductionEntry.setAttribute("UPDATED_BY", EITLERPGLOBAL.gNewUserID);
                    sdfProductionEntry.setAttribute("UPDATED_DATE", EITLERPGLOBAL.getCurrentDateDB());
                }
                
                
                sdfProductionEntry.hmPieceDetails.put(Integer.toString(sdfProductionEntry.hmPieceDetails.size() + 1), piece);
            }
            
        } catch (Exception e) {
            System.out.println("Eroor on setData : " + e.getMessage());
            e.printStackTrace();
        };
    }


