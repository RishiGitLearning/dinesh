    private void Edit() {

        String productionDocumentNo = (String) sdfProductionEntry.getAttribute("PRODUCTION_DOCUMENT_NO").getObj();
        if (sdfProductionEntry.IsEditable(productionDocumentNo, EITLERPGLOBAL.gNewUserID)) {

            EditMode = EITLERPGLOBAL.EDIT;
            DisableToolbar();
            SetupApproval();
            GenerateHierarchyCombo();
            GenerateSendToCombo();
            DisplayData();

            //ReasonResetReadonly();
            //cmbOrderReason.setEnabled(false);
            if (clsFeltProductionApprovalFlow.IsCreator(ModuleId, productionDocumentNo)) {
                SetFields(true);
            } else {
                EnableApproval();
            }

        } else {
            JOptionPane.showMessageDialog(this, "You cannot edit this record. It is either approved/rejected or waiting approval for other user", "EDITING ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

