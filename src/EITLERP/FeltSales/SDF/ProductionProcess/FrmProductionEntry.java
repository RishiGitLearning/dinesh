    private void Add() {
        //  EditMode=EITLERPGLOBAL.ADD;
        String Pending_Doc = data.getStringValueFromDB("SELECT PROD_DOC_NO FROM PRODUCTION.FELT_SDF_PROD_DATA WHERE APPROVED=0 AND CANCELED=0");
        if(!Pending_Doc.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Document : "+Pending_Doc+" is pending, Please Approve first.");
            return;
        }
        if (!EITLERPGLOBAL.YearIsOpen) {
            JOptionPane.showMessageDialog(null, "The year is closed. You cannot enter/edit any transaction");
            return;
        }
        
        clearFields();

        EditMode = EITLERPGLOBAL.ADD;
        DisableToolbar();
        SetFields(true);
        SetupApproval();
        lblTitle.setBackground(new Color(0, 102, 153));
        lblTitle.setForeground(Color.WHITE);

        SelectFirstFree aList = new SelectFirstFree();
        aList.ModuleID = ModuleId;
        aList.FirstFreeNo = 280;

        FFNo = aList.FirstFreeNo;
        PPNo.setText(clsFirstFree.getNextFreeNo(EITLERPGLOBAL.gCompanyID, ModuleId, FFNo, false));
        PPDate.setText(EITLERPGLOBAL.getCurrentDate());
        lblTitle.setText("SDF Production Process Entry - " + PPNo.getText());
        AddPieces();
    }

    private void Find() {
        Loader ObjLoader = new Loader(this, "EITLERP.FeltSales.SDF.ProductionProcess.frmFindProductionEntry", true);
        frmFindProductionEntry ObjFindsdfProductionEntry = (frmFindProductionEntry) ObjLoader.getObj();

        if (ObjFindsdfProductionEntry.Cancelled == false) {
            if (!sdfProductionEntry.Filter(ObjFindsdfProductionEntry.stringFindQuery)) {
                JOptionPane.showMessageDialog(this, "No records found.", "SDF Production Process Entry - ", JOptionPane.YES_OPTION);
            }
            MoveLast();
        }
    }

