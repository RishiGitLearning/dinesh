    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String LRNo = txtLRNo.getText();       
        String PieceNo = txtPieceNo.getText();
        String InvoiceNo = txtInvoiceNo.getText();
        
        if(!LRNo.equals("")) {
            stringFindQuery=stringFindQuery+" AND LR_NO='"+ LRNo +"' ";
        }
        
        if(!PieceNo.equals("")) {
            stringFindQuery=stringFindQuery+" AND PIECE_NO ='"+ PieceNo +"' ";
        }
        
        if(!InvoiceNo.equals("")) {
            stringFindQuery=stringFindQuery+" AND INVOICE_NO ='"+ InvoiceNo +"' ";
        }
        
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
