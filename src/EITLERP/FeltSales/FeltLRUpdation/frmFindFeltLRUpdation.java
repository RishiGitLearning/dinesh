    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String invoiceNo = txtInvoiceNo.getText();
        String invoiceDate = txtInvoiceDate.getText();       
        
        if(!invoiceNo.equals("")) {
            stringFindQuery=" INVOICE_NO ='"+ invoiceNo +"'";
        }
        
        if(!invoiceDate.equals("")) {
            stringFindQuery=" INVOICE_DATE='"+EITLERPGLOBAL.formatDateDB(invoiceDate)+"' ";
        }
        
        if(!invoiceNo.equals("") && !invoiceDate.equals("")) {
            stringFindQuery=" INVOICE_NO ='"+ invoiceNo +"' AND INVOICE_DATE='"+EITLERPGLOBAL.formatDateDB(invoiceDate)+"' ";
        }       
        
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
        
