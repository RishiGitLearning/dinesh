    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String packingNo = txtPackingNo.getText();
        String packingDate = txtPackingDate.getText();   
        String BaleNo = txtBaleNo.getText();
        String BaleDate = txtBaleDate.getText();
        String PieceNo = txtPieceNo.getText();
        
        if(!packingNo.equals("")) {
            stringFindQuery=" PKG_DP_NO ='"+ packingNo +"'";
        }
        
        if(!packingDate.equals("")) {
            stringFindQuery=" PKG_DP_DATE='"+EITLERPGLOBAL.formatDateDB(packingDate)+"' ";
        }
        
        if(!BaleNo.equals("")) {
            stringFindQuery=" PKG_BALE_NO ='"+ BaleNo +"'";
        }
        
        if(!BaleDate.equals("")) {
            stringFindQuery=" PKG_BALE_DATE ='"+EITLERPGLOBAL.formatDateDB(BaleDate)+"'";
        }
        
        if(!packingNo.equals("") && !packingDate.equals("")) {
            stringFindQuery=" PKG_DP_NO ='"+ packingNo +"' AND PKG_DP_DATE='"+EITLERPGLOBAL.formatDateDB(packingDate)+"' ";
        }
        
        if(!BaleNo.equals("") && !BaleDate.equals("")) {
            stringFindQuery=" PKG_BALE_NO ='"+ BaleNo +"' AND PKG_BALE_DATE='"+EITLERPGLOBAL.formatDateDB(BaleDate)+"' ";
        }
        
        if (!PieceNo.equals("")) {
            stringFindQuery = " PKG_PIECE_NO ='" + PieceNo + "'";
        }

        
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
        
