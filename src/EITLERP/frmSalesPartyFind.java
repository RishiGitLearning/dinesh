    private void txtPartyCodeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtPartyCodeFocusLost
        // TODO add your handling code here:
        if (! txtPartyCode.getText().trim().equals("")) {
            txtName.setText(clsSales_Party.getPartyName(EITLERPGLOBAL.gCompanyID, txtPartyCode.getText().trim()));
        }
    }//GEN-LAST:event_txtPartyCodeFocusLost

    private void txtPartyCodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPartyCodeKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==112) //F1 Key pressed
        {
            LOV aList=new LOV();
            
            aList.SQL="SELECT PARTY_CODE,PARTY_NAME FROM D_SAL_PARTY_MASTER WHERE COMPANY_ID="+EITLERPGLOBAL.gCompanyID+" AND APPROVED=1 ORDER BY PARTY_NAME";
            aList.ReturnCol=1;
            aList.ShowReturnCol=true;
            aList.DefaultSearchOn=2;
            
            if(aList.ShowLOV()) {
                txtPartyCode.setText(aList.ReturnVal);
                txtName.setText(clsSales_Party.getPartyName(EITLERPGLOBAL.gCompanyID, aList.ReturnVal));
            }
        }
    }//GEN-LAST:event_txtPartyCodeKeyPressed

    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        // TODO add your handling code here:
         Cancelled=false;
      
       strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID); 
       
       if(!txtPartyCode.getText().equals(""))
       {
         strQuery=strQuery+" AND PARTY_CODE='"+txtPartyCode.getText()+"' ";
       }
       
       if(!txtAgentPrefixCode.getText().equals(""))
       {
         strQuery=strQuery+" AND AGENT_PREFIX LIKE '"+txtAgentPrefixCode.getText()+"%' ";
       }

//       if(!txtName.getText().equals(""))
//       {
//         strQuery=strQuery+" AND PARTY_NAME LIKE '%"+txtName.getText()+"%' ";
//       }
      
       if(!txtAreaID.getText().trim().equals(""))
       {
        strQuery=strQuery+" AND AREA_ID LIKE '%"+txtAreaID.getText()+"%' ";
       }
       
       if(!txtCity.getText().trim().equals(""))
       {
        strQuery=strQuery+" AND CITY_NAME LIKE '%"+txtCity.getText()+"%' ";
       }
       
       if(!txtState.getText().trim().equals(""))
       {
        strQuery=strQuery+" AND DISTRICT LIKE '%"+txtState.getText()+"%' ";
       }
               
       getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed

