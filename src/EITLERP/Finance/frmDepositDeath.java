    private void cmdSelectreceiptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSelectreceiptActionPerformed
        // TODO add your handling code here:
        SelectPartyReceipt.PCode = txtPartyCode.getText().trim();
        SelectPartyReceipt ObjReceipt=new SelectPartyReceipt();
        String PartyCode = txtPartyCode.getText().trim();
        FormatGrid();
        if(ObjReceipt.ShowList()) {
            for(int i=1;i<=ObjReceipt.colSelItems.size();i++) {
                clsDepositDeathItems ObjItem = (clsDepositDeathItems)ObjReceipt.colSelItems.get(Integer.toString(i));
                Object[] rowData=new Object[7];
                
                rowData[0]=Integer.toString(ObjItem.getAttribute("SR_NO").getInt());
                rowData[1]=ObjItem.getAttribute("RECEIPT_NO").getString();
                rowData[2]=EITLERPGLOBAL.formatDate(ObjItem.getAttribute("RECEIPT_DATE").getString());
                rowData[3]=EITLERPGLOBAL.formatDate(ObjItem.getAttribute("EFFECTIVE_DATE").getString());
                rowData[4]=EITLERPGLOBAL.formatDate(ObjItem.getAttribute("MATURITY_DATE").getString());
                rowData[5]=ObjItem.getAttribute("MAIN_ACCOUNT_CODE").getString();
                rowData[6]=Double.toString(ObjItem.getAttribute("AMOUNT").getDouble());
                DataModelL.addRow(rowData);
            }
        }
    }//GEN-LAST:event_cmdSelectreceiptActionPerformed
    
    private void txtTransferPartyCodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTransferPartyCodeKeyPressed
        // TODO add your handling code here:
        if(EditMode==EITLERPGLOBAL.ADD || EditMode==EITLERPGLOBAL.EDIT) {
            if(evt.getKeyCode()==112) {//F1 Key pressed
                LOV aList=new LOV();
                
                aList.SQL="SELECT DISTINCT PARTY_CODE,PARTY_NAME "+
                " FROM D_FIN_PARTY_MASTER WHERE APPROVED=1 AND CANCELLED=0 AND MAIN_ACCOUNT_CODE IN ('115012','115153','115218','115225','115029','115036') "+
                " ORDER BY PARTY_CODE ";
                aList.ReturnCol=1;
                aList.ShowReturnCol=true;
                aList.DefaultSearchOn=2;
                aList.UseSpecifiedConn=true;
                aList.dbURL=FinanceGlobal.FinURL;
                
                if(aList.ShowLOV()) {
                    txtTransferPartyCode.setText(aList.ReturnVal);
                    txtTransferPartyName.setText(data.getStringValueFromDB("SELECT PARTY_NAME FROM D_FIN_PARTY_MASTER WHERE PARTY_CODE='"+txtTransferPartyCode.getText().trim()+"' ",FinanceGlobal.FinURL));
                    txtAddress1.setText(data.getStringValueFromDB("SELECT ADDRESS FROM D_FIN_PARTY_MASTER WHERE PARTY_CODE='"+txtTransferPartyCode.getText().trim()+"' ",FinanceGlobal.FinURL));
                    txtAddress2.setText("");
                    txtAddress3.setText("");
                    txtPanNo.setText(data.getStringValueFromDB("SELECT PAN_NO FROM D_FIN_PARTY_MASTER WHERE PARTY_CODE='"+txtTransferPartyCode.getText().trim()+"' ",FinanceGlobal.FinURL));
                    txtPanDate.setText(data.getStringValueFromDB("SELECT PAN_DATE FROM D_FIN_PARTY_MASTER WHERE PARTY_CODE='"+txtTransferPartyCode.getText().trim()+"' ",FinanceGlobal.FinURL));
                } else {
                    txtTransferPartyCode.setText("");
                    txtTransferPartyName.setText("");
                    txtAddress1.setText("");
                    txtAddress2.setText("");
                    txtAddress3.setText("");
                    txtPanNo.setText("");
                    txtPanDate.setText("");
                }
            }
        }
    }//GEN-LAST:event_txtTransferPartyCodeKeyPressed
    
