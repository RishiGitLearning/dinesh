    private boolean Validate() {
        if(txtPartyCode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Enter Party Code.");
            return false;
        }
        if(txtPartyCode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Enter To Party Code.");
            return false;
        } else if(!data.IsRecordExist("SELECT * FROM D_FIN_PARTY_MASTER WHERE PARTY_CODE='"+txtPartyCode.getText().trim()+"' AND MAIN_ACCOUNT_CODE IN ('115012','115218','115153','115225','115029','115036') AND APPROVED=1 AND CANCELLED=0 ",FinanceGlobal.FinURL)) {
            JOptionPane.showMessageDialog(null,"To Party Code with To main code does not exists in finance party master.");
            return false;
        }
        
        if(txtPartyCode.getText().trim().equals(txtTransferPartyCode.getText().trim())) {
            JOptionPane.showMessageDialog(null,"Party Code and To party code are same.");
            return false;
        }
        
        if(txtTransferPartyCode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Enter To Party Code.");
            return false;
        } else if(!data.IsRecordExist("SELECT * FROM D_FIN_PARTY_MASTER WHERE PARTY_CODE='"+txtTransferPartyCode.getText().trim()+"' AND MAIN_ACCOUNT_CODE IN ('115012','115218','115153','115225','115029','115036') AND APPROVED=1 AND CANCELLED=0 ",FinanceGlobal.FinURL)) {
            JOptionPane.showMessageDialog(null,"To Party Code with To main code does not exists in finance party master.");
            return false;
        }
        
        if(txtTransferDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Enter Transfer Date.");
            return false;
        } else if(!EITLERPGLOBAL.isDate(txtTransferDate.getText().trim())) {
            JOptionPane.showMessageDialog(null,"Enter Transfer Date.");
            return false;
        }
        
        if(TableL.getRowCount() <= 0) {
            JOptionPane.showMessageDialog(null,"No receipt(s) for party.");
            return false;
        }
        String PartyCode = txtPartyCode.getText().trim();
        String TransferPartyCode = txtTransferPartyCode.getText().trim();
        String TransferDate = EITLERPGLOBAL.formatDateDB(txtTransferDate.getText().trim());
        String ReceiptNo = "",MaturityDate ="",LastWarrantDate="",MainAccountCode="";
        int interestCalc = 0;
        int prematurity = 0;
        int refund = 0;
        for(int i=0;i<TableL.getRowCount();i++) {
            ReceiptNo = DataModelL.getValueByVariable("RECEIPT_NO",i).toString();
            MaturityDate = EITLERPGLOBAL.formatDateDB(DataModelL.getValueByVariable("MATURITY_DATE",i).toString());
            MainAccountCode = DataModelL.getValueByVariable("MAIN_ACCOUNT_CODE",i).toString();
            
            if(data.IsRecordExist("SELECT A.DOC_NO FROM D_FD_DEPOSIT_DEATH_HEADER A, D_FD_DEPOSIT_DEATH_DETAIL " +
            "WHERE A.DOC_NO=B.DOC_NO AND A.DOC_NO<>'"+txtDocNo.getText().trim()+"' AND A.APPROVED=0 AND A.CANCELLED=0 AND B.RECEIPT_NO='"+ReceiptNo+"' ",FinanceGlobal.FinURL)) {
                JOptionPane.showMessageDialog(null,"Entry for receipt no ("+ReceiptNo+") is already under approval.");
                return false;
            }
            
            if(java.sql.Date.valueOf(TransferDate).after(java.sql.Date.valueOf(MaturityDate)) || java.sql.Date.valueOf(TransferDate).compareTo(java.sql.Date.valueOf(MaturityDate)) == 0) {
                JOptionPane.showMessageDialog(null,"Please Check.\n 1. Transfer date should not equal or after maturity date. ");
                return false;
            }
            
            LastWarrantDate = data.getStringValueFromDB("SELECT B.WARRANT_DATE FROM D_FD_INT_CALC_HEADER A, D_FD_INT_CALC_DETAIL B " +
            "WHERE A.DOC_NO=B.DOC_NO AND B.RECEIPT_NO='"+ReceiptNo+"' AND B.PARTY_CODE='"+PartyCode+"' AND A.APPROVED=1 AND A.CANCELLED=0 ORDER BY B.WARRANT_DATE DESC " ,FinanceGlobal.FinURL);
            if(java.sql.Date.valueOf(TransferDate).before(java.sql.Date.valueOf(LastWarrantDate)) || java.sql.Date.valueOf(TransferDate).compareTo(java.sql.Date.valueOf(LastWarrantDate)) == 0) {
                JOptionPane.showMessageDialog(null,"Please Check.\n 1. Transfer date should not equal or before maturity date. ");
                return false;
            }
            
            interestCalc = data.getIntValueFromDB("SELECT COUNT(A.DOC_NO) FROM D_FD_INT_CALC_HEADER A, D_FD_INT_CALC_DETAIL B " +
            "WHERE A.DOC_NO=B.DOC_NO AND B.RECEIPT_NO='"+ReceiptNo+"' AND B.PARTY_CODE='"+PartyCode+"' AND A.APPROVED=0 AND A.CANCELLED=0 ",FinanceGlobal.FinURL);
            if(interestCalc>0) {
                JOptionPane.showMessageDialog(null,ReceiptNo + " already in progress in interest calculation module. " +
                "\n1. Either cancel interest calculation ducument then complete deposit transfer \n or \n" +
                "2. Cancel generating current document and final approve interest calculation document then create current document.");
                return false;
            }
            prematurity = data.getIntValueFromDB("SELECT COUNT(DOC_NO) FROM D_FD_DEPOSIT_PM WHERE RECEIPT_NO='"+ReceiptNo+"' " +
            "AND PARTY_CODE='"+PartyCode+"' AND APPROVED=0 AND CANCELLED=0 ",FinanceGlobal.FinURL);
            if(prematurity>0) {
                JOptionPane.showMessageDialog(null,ReceiptNo + " already in progress in deposit prematurity module. " +
                "\n1. Either cancel deposit prematurity ducument then complete deposit transfer \n or \n" +
                "2. Cancel generating current document and final approve deposit prematurity document.");
                return false;
            }
            refund = data.getIntValueFromDB("SELECT COUNT(DOC_NO) FROM D_FD_DEPOSIT_REFUND WHERE RECEIPT_NO='"+ReceiptNo+"' " +
            "AND SUB_ACCOUNT_CODE='"+PartyCode+"' AND APPROVED=0 AND CANCELLED=0 ",FinanceGlobal.FinURL);
            if(refund>0) {
                JOptionPane.showMessageDialog(null,ReceiptNo + " already in progress in refund module. " +
                "\n1. Either cancel deposit refund ducument then complete deposit transfer \n or \n" +
                "2. Cancel generating current document and final approve deposit refund document.");
                return false;
            }
            
            if(!data.IsRecordExist("SELECT * FROM D_FIN_PARTY_MASTER WHERE MAIN_ACCOUNT_CODE='"+MainAccountCode+"' AND PARTY_CODE='"+TransferPartyCode+"' ",FinanceGlobal.FinURL)) {
                JOptionPane.showMessageDialog(null,"Transfer party code ("+TransferPartyCode+") with main code ("+MainAccountCode+") does not exists in finance party master.");
                return false;
            }
            if(MainAccountCode.equals("115153")) {
                if(!data.IsRecordExist("SELECT * FROM D_FIN_PARTY_MASTER WHERE MAIN_ACCOUNT_CODE='115160' AND PARTY_CODE='"+TransferPartyCode+"' ",FinanceGlobal.FinURL)) {
                    JOptionPane.showMessageDialog(null,"Transfer party code ("+TransferPartyCode+") with main code (115160) does not exists in finance party master.");
                    return false;
                }
            }
        }
        
        if(txtRemarks.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please insert Remarks.");
            return false;
        }
        
        if(OpgReject.isSelected()) {
            if(txtToRemarks.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null,"Please Enter Remarks for rejection.");
                return false;
            }
        }
        
        //        if(!OpgReject.isSelected()) {
        //            if(!txtDocDate.getText().trim().substring(3,5).equals("03")) {
        //                int Result = clsVoucher.ValidateVoucherEntry(EditMode,EITLERPGLOBAL.formatDateDB(txtDocDate.getText().trim()));
        //                if(Result==EITLERPGLOBAL.ADD) {
        //                    JOptionPane.showMessageDialog(this,"Month is Closed. \nYou can not create voucher(s).");
        //                    return false;
        //                } else if(Result==EITLERPGLOBAL.EDIT) {
        //                    JOptionPane.showMessageDialog(this,"Month is Closed. \nYou can not Approve / Final Approve voucher(s).\n Either you have to cancel voucher or change voucher date.");
        //                    return false;
        //                }
        //            }
        //        }
        return true;
    }
