    private void UpdateTotal() {
        DoNotEvaluate=true;
        //double EnterAmount  = Double.parseDouble(DataModelAdj.getValueByVariable("ADJ_AMOUNT",TableAdj.getSelectedRow()));
        double InvoiceRemainingAmount = 0;
        double DebitAmount = Double.parseDouble(lblDebitAmount.getText());
        double RemainingAmount = Double.parseDouble(DataModelSJ.getValueByVariable("REMAINING_AMOUNT", TableSJ.getSelectedRow()));
        double UtilizedAmount = Double.parseDouble(DataModelSJ.getValueByVariable("UTILIZED_AMOUNT", TableSJ.getSelectedRow()));
        RemainingAmount = RemainingAmount - UtilizedAmount;
        double CreditAmount = Math.abs(RemainingAmount - DebitAmount);
        double TableEnterAmount = 0;
        for(int i=0;i<TableAdj.getRowCount();i++) {
            TableEnterAmount += Double.parseDouble(DataModelAdj.getValueByVariable("ADJ_AMOUNT", i));
        }
        InvoiceRemainingAmount = DebitAmount - CreditAmount - TableEnterAmount;
        if(InvoiceRemainingAmount < 0) {
            JOptionPane.showMessageDialog(null,"Adjustment amount exceeding debit amount.","Wrong Input",JOptionPane.ERROR_MESSAGE);
            DataModelAdj.setValueByVariable("ADJ_AMOUNT","0", TableAdj.getRowCount());
        } else {
            lblRemainingAmount.setText(Double.toString(InvoiceRemainingAmount));
        }
    }
