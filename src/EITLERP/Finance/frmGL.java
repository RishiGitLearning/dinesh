    private void txtGroupCodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtGroupCodeKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==112) {
            
            LOV aList=new LOV();
            
            aList.SQL="SELECT GROUP_CODE,ACCOUNT_NAME FROM D_FIN_GROUP_CODE_MASTER ORDER BY CONVERT(GROUP_CODE,SIGNED) ";
            aList.ReturnCol=1;
            aList.ShowReturnCol=true;
            aList.DefaultSearchOn=2;
            aList.UseSpecifiedConn=true;
            aList.dbURL=FinanceGlobal.FinURL;
            
            if(aList.ShowLOV()) {
                txtGroupCode.setText(aList.ReturnVal);
                txtGroupName.setText(data.getStringValueFromDB("SELECT ACCOUNT_NAME FROM D_FIN_GROUP_CODE_MASTER WHERE GROUP_CODE='"+aList.ReturnVal+"'",FinanceGlobal.FinURL));
            }
            
        }
    }//GEN-LAST:event_txtGroupCodeKeyPressed
    
