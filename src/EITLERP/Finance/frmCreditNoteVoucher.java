            DoNotEvaluate = false;

            UpdateTotals();

            //======== Generating Grid for Document Approval Flow ========//
            FormatGridA();
            HashMap List = new HashMap();
            String DocNo = objVoucher.getAttribute("VOUCHER_NO").getString();
            List = ApprovalFlow.getDocumentFlow(EITLERPGLOBAL.gCompanyID, clsVoucher.CreditNoteVoucherModuleID, DocNo);
