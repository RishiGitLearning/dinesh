    private void GenerateCombo()
    {
      try
      {
        cmbBookTypeModel=new EITLComboModel();
        cmbBookType.removeAllItems();
        cmbBookType.setModel(cmbBookTypeModel);
        
        ComboData objData=new ComboData();
        objData.Code=0;
        objData.Text="00";
        
        cmbBookTypeModel.addElement(objData);
        
        objData=new ComboData();
        objData.Code=1;
        objData.Text="01";
        
        cmbBookTypeModel.addElement(objData);
        
        objData=new ComboData();
        objData.Code=2;
        objData.Text="02";
        
        cmbBookTypeModel.addElement(objData);
        
        objData=new ComboData();
        objData.Code=3;
        objData.Text="03";
        
        cmbBookTypeModel.addElement(objData);
          
      } catch(Exception e) {
      }
    }
    
    private boolean Validate() {
        if(txtBookCode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Insert Book Code");
            return false;
        }
        
        if(EITLERPGLOBAL.getComboCode(cmbBookType)==0) {
            JOptionPane.showMessageDialog(null,"Please Select Book Type");
            return false;
        }
        
        if(txtBookName.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Insert Book Name");
            return false;
        }
        return true;
    }
