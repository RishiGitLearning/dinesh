    public void FindEx(int pCompanyID,int pDocNo) {
        objBook.Filter(" WHERE COMPANY_ID="+Integer.toString(pCompanyID)+" AND BOOK_CODE='"+pDocNo+"'",EITLERPGLOBAL.gCompanyID);
        objBook.MoveFirst();
        DisplayData();
    }

