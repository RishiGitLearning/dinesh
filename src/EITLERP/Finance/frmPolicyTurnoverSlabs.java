    //Didplay data on the Screen
    private void DisplayData() {
        
        //=========== Color Indication ===============//
        try {
            if(EditMode==0) {
                
                if(objTurnoverSlabs.getAttribute("CANCELLED").getInt()==1) {
                    lblTitle.setBackground(Color.RED);
                }
                
                
            }
        }
        catch(Exception c) {
            
        }
        //============================================//
        
        
        ClearFields();
        
        txtPolicyID.setText(objTurnoverSlabs.getAttribute("POLICY_ID").getString());
        txtPolicyName.setText(clsPolicyMaster.getPolicyName(EITLERPGLOBAL.gCompanyID, txtPolicyID.getText().trim()));
        txtFromValue.setText(Double.toString(objTurnoverSlabs.getAttribute("FROM_VALUE").getDouble()));
        txtToValue.setText(Double.toString(objTurnoverSlabs.getAttribute("TO_VALUE").getDouble()));
        txtPercentage.setText(Double.toString(objTurnoverSlabs.getAttribute("PERCENTAGE").getDouble()));
        
    }
    
    //Sets data to the Class Object
    private void SetData() {
        
        objTurnoverSlabs.setAttribute("POLICY_ID",txtPolicyID.getText());
        objTurnoverSlabs.setAttribute("FROM_VALUE",UtilFunctions.CDbl(txtFromValue.getText()));
        objTurnoverSlabs.setAttribute("TO_VALUE",UtilFunctions.CDbl(txtToValue.getText()));
        objTurnoverSlabs.setAttribute("PERCENTAGE",UtilFunctions.CDbl(txtPercentage.getText()));
        
    }
    
    
    private void SetMenuForRights() {
        
        // --- Add Rights --
        if(clsUser.IsFunctionGranted(EITLERPGLOBAL.gCompanyID, EITLERPGLOBAL.gNewUserID, 0,11411)) {            
            cmdNew.setEnabled(true);
        }
        else {
            cmdNew.setEnabled(false);
        }
        
        // --- Edit Rights --
        //if(clsUser.IsFunctionGranted(EITLERPGLOBAL.gCompanyID, EITLERPGLOBAL.gNewUserID, 0,11412)) {
            cmdEdit.setEnabled(true);
        //}
        //else {
        //    cmdEdit.setEnabled(false);
        //}
        
        // --- Delete Rights --
        if(clsUser.IsFunctionGranted(EITLERPGLOBAL.gCompanyID, EITLERPGLOBAL.gNewUserID, 0,11413)) {
            
            cmdDelete.setEnabled(true);
        }
        else {
            
            cmdDelete.setEnabled(false);
        }
        
        // --- Print Rights --
        if(clsUser.IsFunctionGranted(EITLERPGLOBAL.gCompanyID, EITLERPGLOBAL.gNewUserID, 0,11414)) {
            cmdPreview.setEnabled(true);
            cmdPrint.setEnabled(true);
        }
        else {
            cmdPreview.setEnabled(false);
            cmdPrint.setEnabled(false);
        }
        
    }
    
    private void Add() {
        EditMode=EITLERPGLOBAL.ADD;
        SetFields(true);
        DisableToolbar();
        ClearFields();
        
    }
    
    private void Edit() {

        EITLERPGLOBAL.ChangeCursorToWait(this);
        EditMode=EITLERPGLOBAL.EDIT;
        
        //---New Change ---//
        DisplayData();
        //----------------//
        
        SetFields(true);
        DisableToolbar();
        txtPolicyID.setEnabled(false);
        txtFromValue.requestFocus();
        EITLERPGLOBAL.ChangeCursorToDefault(this);
        
    }
    
    private void Delete() {
        if(objTurnoverSlabs.Delete(EITLERPGLOBAL.gNewUserID)) {
            MoveLast();
        }
    }
    
    private boolean Validate() {
        //** Validations **//
        
        if(txtPolicyID.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Please Specify Policy ID.");
            txtPolicyID.requestFocus();
            return false;
        }

        if(txtFromValue.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Please Enter From Value.");
            txtFromValue.requestFocus();
            return false;
        }
        
        if(txtToValue.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Please Enter To Value.");
            txtToValue.requestFocus();
            return false;
        }
        
        if (txtPercentage.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Enter Percentage.");    
            txtPercentage.requestFocus();
            return false;
        }
        //*****************//
        
        return true;
    }
    
    private void Save() {
        
        EITLERPGLOBAL.ChangeCursorToWait(this);
        
        //** Validations **//
        if(!Validate()){
            return;
        }
        
        SetData();
        
        if(EditMode==EITLERPGLOBAL.ADD) {
            if(objTurnoverSlabs.Insert()) {
                MoveLast();
                DisplayData();
            }
            else {
                JOptionPane.showMessageDialog(null,"Error occured while saving. Error is "+objTurnoverSlabs.LastError);
                EITLERPGLOBAL.ChangeCursorToDefault(this);
                return;
            }
        }
        
        if(EditMode==EITLERPGLOBAL.EDIT) {
            if(objTurnoverSlabs.Update()) {
                //Nothing to do
                DisplayData();
            }
            else {
                JOptionPane.showMessageDialog(null,"Error occured while saving. Error is "+objTurnoverSlabs.LastError);
                EITLERPGLOBAL.ChangeCursorToDefault(this);
                return;
            }
        }
        
        EditMode=0;
        SetFields(false);
        EnableToolbar();
        SetMenuForRights();
        EITLERPGLOBAL.ChangeCursorToDefault(this);
        
        
    }
    
    private void Cancel() {
        EITLERPGLOBAL.ChangeCursorToWait(this);
        DisplayData();
        EditMode=0;
        SetFields(false);
        EnableToolbar();
        SetMenuForRights();
        EITLERPGLOBAL.ChangeCursorToDefault(this);
    }
    
