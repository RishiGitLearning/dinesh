        txtDocDate.setText(objVoucher.getAttribute("VOUCHER_DATE").getString());
        chkCancelled.setSelected(objVoucher.getAttribute("CANCELLED").getBool());
        txtBookCode.setText(objVoucher.getAttribute("BOOK_CODE").getString());
