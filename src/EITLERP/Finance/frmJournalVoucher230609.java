        txtLegacyNo.setText(objVoucher.getAttribute("LEGACY_NO").getString());
        txtLegacyDate.setText(objVoucher.getAttribute("LEGACY_DATE").getString());
        txtLinkNo.setText(objVoucher.getAttribute("LINK_NO").getString());
        
