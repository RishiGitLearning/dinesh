    private void GenerateGrid() {
        try {
            int SrNo=0;
            HashMap List=new HashMap();
            
            FormatGrid();
            
            ResultSet rsTmp=data.getResult("SELECT * FROM D_FIN_OBC WHERE CANCELLED=0 AND PARTY_CODE='"+PartyCode+"' ",FinanceGlobal.FinURL);
            rsTmp.first();
            
            if(rsTmp.getRow()>0) {
                while(!rsTmp.isAfterLast()) {
                    
                    SrNo++;
                    Object[] rowData=new Object[1];
                    DataModel.addRow(rowData);
                    int NewIndex=Table.getRowCount()-1;
                    
                    DataModel.setValueByVariable("SELECT",new Boolean(false),NewIndex);
                    DataModel.setValueByVariable("SR_NO",Integer.toString(SrNo),NewIndex);
                    DataModel.setValueByVariable("DOC_NO",UtilFunctions.getString(rsTmp,"DOC_NO",""),NewIndex);
                    DataModel.setValueByVariable("DOC_DATE",EITLERPGLOBAL.formatDate(UtilFunctions.getString(rsTmp,"DOC_DATE","0000-00-00")),NewIndex);
                    DataModel.setValueByVariable("AMOUNT",Double.toString(UtilFunctions.getDouble(rsTmp,"AMOUNT",0)),NewIndex);
                    DataModel.setValueByVariable("BANK_REFERENCE_NO",UtilFunctions.getString(rsTmp,"BANK_REFERENCE_NO",""),NewIndex);
                    DataModel.setValueByVariable("DRAFT_NO",UtilFunctions.getString(rsTmp,"DRAFT_NO",""),NewIndex);
                    DataModel.setValueByVariable("COMPANY_ID",Integer.toString(UtilFunctions.getInt(rsTmp,"COMPANY_ID",0)),NewIndex);
                    DataModel.setValueByVariable("COMPANY_NAME",clsCompany.getCompanyName(UtilFunctions.getInt(rsTmp,"COMPANY_ID",0)),NewIndex);
                    
                    rsTmp.next();
                }
            }
            
        }
        catch(Exception e) {
            
        }
    }
    
    public boolean ShowDialog() {
        try {
            
            setSize(682,352);
            txtPartyCode.setText(PartyCode);
            
            Frame f=findParentFrame(this);
            
            aDialog=new JDialog(f,"Select OBC Entry",true);
            
            aDialog.getContentPane().add("Center",this);
            Dimension appletSize = this.getSize();
            aDialog.setSize(appletSize);
            aDialog.setResizable(false);
            
            //Place it to center of the screen
            Dimension screenSize=Toolkit.getDefaultToolkit().getScreenSize();
            aDialog.setLocation((int)(screenSize.width-appletSize.getWidth())/2,(int)(screenSize.height-appletSize.getHeight())/2);
            
            aDialog.setDefaultCloseOperation(javax.swing.JDialog.DISPOSE_ON_CLOSE);
            aDialog.show();
        }
        catch(Exception e) {
        }
        return !Cancelled;
    }
    
    
    private Frame findParentFrame(JApplet pApplet) {
        Container c = (Container) pApplet;
        while(c != null) {
            if (c instanceof Frame)
                return (Frame)c;
            
            c = c.getParent();
        }
        return (Frame)null;
    }
    
