    private void Add() {
        EditMode=EITLERPGLOBAL.ADD;
        SetFields(true);
        DisableToolbar();
        ClearFields();
        
    }
    
    private void Edit() {

        EITLERPGLOBAL.ChangeCursorToWait(this);
        EditMode=EITLERPGLOBAL.EDIT;
        
        //---New Change ---//
        DisplayData();
        //----------------//
        
        SetFields(true);
        DisableToolbar();
        txtPolicyID.setEnabled(false);
        txtSeasonID.requestFocus();
        EITLERPGLOBAL.ChangeCursorToDefault(this);
        
    }
    
    private void Delete() {
        if(objSeasonSlabs.Delete(EITLERPGLOBAL.gNewUserID)) {
            MoveLast();
        }
    }
    
    private boolean Validate() {
        //** Validations **//
        
        if(txtPolicyID.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Please Specify Policy ID.");
            txtPolicyID.requestFocus();
            return false;
        }

        if(txtSeasonID.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Please Specify Season ID.");
            txtSeasonID.requestFocus();
            return false;
        }
        
        if(txtQualityID.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Please Specify Quality ID.");
            txtQualityID.requestFocus();
            return false;
        }
        
        if(txtFromDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Enter From Date.");
            txtFromDate.requestFocus();
            return false;
        } else if(!(EITLERPGLOBAL.isDate(txtFromDate.getText().trim()))) {
            JOptionPane.showMessageDialog(null,"Please Enter The From Date in DD/MM/YYYY Format.");
            txtFromDate.requestFocus();
            return false;
        }
        
        if(txtToDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Enter To Date.");
            txtToDate.requestFocus();
            return false;
        } else if(!(EITLERPGLOBAL.isDate(txtToDate.getText().trim()))) {
            JOptionPane.showMessageDialog(null,"Please Enter The To Date in DD/MM/YYYY Format.");
            txtToDate.requestFocus();
            return false;
        }
        
        if (txtPercentage.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Enter Percentage.");    
            txtPercentage.requestFocus();
            return false;
        }
        //*****************//
        
        return true;
    }
    
    private void Save() {
        
        EITLERPGLOBAL.ChangeCursorToWait(this);
        
        //** Validations **//
        if(!Validate()){
            return;
        }
        
        SetData();
        
        if(EditMode==EITLERPGLOBAL.ADD) {
            if(objSeasonSlabs.Insert()) {
                MoveLast();
                DisplayData();
            }
            else {
                JOptionPane.showMessageDialog(null,"Error occured while saving. Error is "+objSeasonSlabs.LastError);
                EITLERPGLOBAL.ChangeCursorToDefault(this);
                return;
            }
        }
        
        if(EditMode==EITLERPGLOBAL.EDIT) {
            if(objSeasonSlabs.Update()) {
                //Nothing to do
                DisplayData();
            }
            else {
                JOptionPane.showMessageDialog(null,"Error occured while saving. Error is "+objSeasonSlabs.LastError);
                EITLERPGLOBAL.ChangeCursorToDefault(this);
                return;
            }
        }
        
        EditMode=0;
        SetFields(false);
        EnableToolbar();
        SetMenuForRights();
        EITLERPGLOBAL.ChangeCursorToDefault(this);
        
        
    }
    
    private void Cancel() {
        EITLERPGLOBAL.ChangeCursorToWait(this);
        DisplayData();
        EditMode=0;
        SetFields(false);
        EnableToolbar();
        SetMenuForRights();
        EITLERPGLOBAL.ChangeCursorToDefault(this);
    }
    
    private void Find() {
        
        Loader ObjLoader=new Loader(this,"EITLERP.Finance.frmPolicySeasonSlabsFind",true);
        frmPolicySeasonSlabsFind ObjReturn= (frmPolicySeasonSlabsFind) ObjLoader.getObj();
         
        if(ObjReturn.Cancelled==false) {
            if(!objSeasonSlabs.Filter(ObjReturn.strQuery,EITLERPGLOBAL.gCompanyID)) {
                JOptionPane.showMessageDialog(null,"No records found.");
            }
            MoveFirst();
        }
        
    }
    
    private void MoveFirst() {
        EITLERPGLOBAL.ChangeCursorToWait(this);
        objSeasonSlabs.MoveFirst();
        DisplayData();
        EITLERPGLOBAL.ChangeCursorToDefault(this);
    }
    
