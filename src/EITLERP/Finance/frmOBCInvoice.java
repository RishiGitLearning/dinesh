    private void Save() {

        EITLERPGLOBAL.ChangeCursorToWait(this);
        if (!Validate()) {
            EITLERPGLOBAL.ChangeCursorToDefault(this);
            return;
        }

        SetData();

        if (EditMode == EITLERPGLOBAL.ADD) {
            if (objOBC.Insert()) {
                MoveLast();
                DisplayData();
            } else {
                JOptionPane.showMessageDialog(null, "Error occured while saving. Error is " + objOBC.LastError);
                EITLERPGLOBAL.ChangeCursorToDefault(this);
                return;
            }
        }

        if (EditMode == EITLERPGLOBAL.EDIT) {
            if (objOBC.Update()) {
                //Nothing to do
                DisplayData();
            } else {
                JOptionPane.showMessageDialog(null, "Error occured while saving. Error is " + objOBC.LastError);
                EITLERPGLOBAL.ChangeCursorToDefault(this);
                return;
            }
        }

        EditMode = 0;
        SetFields(false);
        EnableToolbar();
        SetMenuForRights();
        EITLERPGLOBAL.ChangeCursorToDefault(this);

        try {
            frmPA.RefreshView();
        } catch (Exception e) {
        }

    }

