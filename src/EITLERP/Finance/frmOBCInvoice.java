    private boolean Validate() {
        if (txtMainCode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Valid Main Account Code.");
            return false;
        }

        if (txtPartyCode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Valid Party Code.");
            return false;
        }

        if ((!txtMainCode.getText().trim().equals("")) && (!txtPartyCode.getText().trim().equals(""))) {
            String MainCode = txtMainCode.getText().trim();
            String SubCode = txtPartyCode.getText().trim();

            if (!clsAccount.IsValidAccount(MainCode, SubCode)) {
                JOptionPane.showMessageDialog(null, "Main Account Code/Sub Account Code is not valid. Please verify");
                return false;
            }
        }

        if (txtBankRefNo.getText().trim().length() < 9 || txtBankRefNo.getText().trim().length() > 9) {
            JOptionPane.showMessageDialog(this, "Bank Refrence No. must be 2 Charecter + 7 Digitis");
            return false;
        }

        if (txtBankRefDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid Bank Refrence Date.");
            return false;
        }
        
        if (data.IsRecordExist("SELECT * FROM D_FIN_OBC_INVOICE_HEADER WHERE BANK_REFERENCE_NO='" + txtBankRefNo.getText().trim() + "' AND BANK_REFERENCE_DATE='" + EITLERPGLOBAL.formatDateDB(txtBankRefDate.getText().trim() + " AND DOC_NO<>'" + txtDocNo.getText().trim() + "'"), FinanceGlobal.FinURL)) {
            JOptionPane.showMessageDialog(this, "Bank Reference No. and Bank Reference date already inserted in other document.");
            return false;
        }

        /* //commented on 2nd dec 2013 by vivek for applying multiple entry
         if(Table.getRowCount()==0 || Table.getRowCount()>1) {
         JOptionPane.showMessageDialog(this,"Enter only one record in table.");
         return false;
         }
         */
        //new code start   
        if (Table.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Enter atlest one record in table.");
            return false;
        }
        // new code end

        return true;
    }
