    private void txtBaseMainCodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBaseMainCodeKeyPressed
        // TODO add your handling code here:
        
        if(evt.getKeyCode()==112) {
            
            LOV aList=new LOV();
            
            aList.SQL="SELECT MAIN_ACCOUNT_CODE,ACCOUNT_NAME FROM D_FIN_GL WHERE APPROVED=1 ORDER BY ACCOUNT_NAME";
            aList.ReturnCol=1;
            aList.ShowReturnCol=true;
            aList.DefaultSearchOn=2;
            aList.UseSpecifiedConn=true;
            aList.dbURL=FinanceGlobal.FinURL;
            
            if(aList.ShowLOV()) {
                txtBaseMainCode.setText(aList.ReturnVal);
                txtAccountName.setText(clsAccount.getAccountName(txtBaseMainCode.getText(),""));
            }
        }
        
    }//GEN-LAST:event_txtBaseMainCodeKeyPressed

