    private void GenerateReport() {
        try {
            TReportWriter.SimpleDataProvider.TRow objRow;
            TReportWriter.SimpleDataProvider.TTable objReportData=new TReportWriter.SimpleDataProvider.TTable();
            
            objReportData.AddColumn("SR_NO");
            objReportData.AddColumn("VOUCHER_NO");
            objReportData.AddColumn("VOUCHER_NAME");
            objReportData.AddColumn("REMARKS");
            objReportData.AddColumn("MAIN_ACCOUNT_CODE");
            objReportData.AddColumn("AMOUNT");
            
            
            TReportWriter.SimpleDataProvider.TRow objOpeningRow=objReportData.newRow();
            
            objOpeningRow.setValue("SR_NO","");
            objOpeningRow.setValue("VOUCHER_NO","");
            objOpeningRow.setValue("VOUCHER_NAME","");
            objOpeningRow.setValue("REMARKS","");
            objOpeningRow.setValue("MAIN_ACCOUNT_CODE","");
            objOpeningRow.setValue("AMOUNT","");
            
            
            String strSQL = "";
            
            strSQL="SELECT VOUHEAD.VOUCHER_NO,VOUHEAD.VOUCHER_DATE,TRIM(VOUHEAD.REMARKS) AS REMARKS, "+
            "VOUDTLEX.MAIN_ACCOUNT_CODE, "+
            "CASE WHEN VOUDTLEX.EFFECT = 'D' THEN SUM(VOUDTLEX.AMOUNT) ELSE 0 END AS AMOUNT "+
            "FROM D_FIN_VOUCHER_HEADER VOUHEAD, D_FIN_VOUCHER_DETAIL_EX  VOUDTLEX "+
            "WHERE VOUHEAD.APPROVED=1 AND VOUHEAD.CANCELLED=0  "+
            "AND VOUHEAD.BOOK_CODE IN (30 ,70) AND VOUHEAD.VOUCHER_TYPE=4 "+
            "AND VOUHEAD.COMPANY_ID=VOUDTLEX.COMPANY_ID AND VOUHEAD.VOUCHER_NO=VOUDTLEX.VOUCHER_NO "+
            "AND VOUDTLEX.AMOUNT >= 20000 AND VOUDTLEX.MAIN_ACCOUNT_CODE LIKE '4%' "+
            "AND VOUHEAD.VOUCHER_DATE >='"+ EITLERPGLOBAL.formatDateDB(txtFromDate.getText()) +"' AND VOUHEAD.VOUCHER_DATE <= '"+ EITLERPGLOBAL.formatDateDB(txtToDate.getText()) +"' "+
            "GROUP BY VOUHEAD.VOUCHER_NO "+
            "ORDER BY VOUHEAD.VOUCHER_DATE,VOUHEAD.VOUCHER_NO ";
            
            //"AND (A.RECEIPT_DATE>= '"+ EITLERPGLOBAL.formatDateDB(txtFromDate.getText()) +"' "+
            //"AND A.RECEIPT_DATE<= '"+ EITLERPGLOBAL.formatDateDB(txtToDate.getText()) +"' ) ";
            //System.out.println("sql="+strSQL);
            
            ResultSet rsTmp=data.getResult(strSQL,FinanceGlobal.FinURL);
            rsTmp.first();
            
            int Counter = 0;
            
            if(rsTmp.getRow()>0) {
                while(!rsTmp.isAfterLast()) {
                    Counter ++;
                    objRow=objReportData.newRow();
                    
                    objRow.setValue("SR_NO",Integer.toString(Counter));
                    objRow.setValue("VOUCHER_NO",UtilFunctions.getString(rsTmp,"VOUCHER_NO",""));
                    objRow.setValue("VOUCHER_DATE",EITLERPGLOBAL.formatDate(UtilFunctions.getString(rsTmp,"VOUCHER_DATE","0000-00-00")));
                    objRow.setValue("REMARKS",UtilFunctions.getString(rsTmp,"REMARKS",""));
                    objRow.setValue("MAIN_ACCOUNT_CODE",UtilFunctions.getString(rsTmp,"MAIN_ACCOUNT_CODE",""));
                    objRow.setValue("AMOUNT",UtilFunctions.getString(rsTmp,"AMOUNT",""));
                                        
                    objReportData.AddRow(objRow);
                    
                    rsTmp.next();
                }
            }
            
            int Comp_ID = EITLERPGLOBAL.gCompanyID;
            HashMap Parameters=new HashMap();
            //Parameters.put("COMPANY_ID",Integer.toString(Comp_ID));
            Parameters.put("FROM_DATE",txtFromDate.getText().trim());
            Parameters.put("TO_DATE",txtToDate.getText().trim());
            Parameters.put("SYS_DATE",EITLERPGLOBAL.getCurrentDate());
            
            
            objEngine.PreviewReport("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/finance/rptCashVoucherDetail.rpt",Parameters,objReportData);
            
            
        }
        catch(Exception e) {
            e.printStackTrace();
        }
        
    }
    
    private boolean Validate() {
        //Form level validations
        
        if(txtFromDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter from date");
            return false;
        } else if(!EITLERPGLOBAL.isDate(txtFromDate.getText())) {
            JOptionPane.showMessageDialog(null,"Invalid From Date in DD/MM/YYYY format.");
            return false;
        }
        
        if(txtToDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter To date");
            return false;
        } else if(!EITLERPGLOBAL.isDate(txtToDate.getText())) {
            JOptionPane.showMessageDialog(null,"Invalid To Date in DD/MM/YYYY format.");
            return false;
        }
        
        return true;
    }
    
