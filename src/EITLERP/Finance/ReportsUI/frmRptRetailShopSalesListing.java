    private void GenerateReport() {
        try {
            TReportWriter.SimpleDataProvider.TRow objRow;
            TReportWriter.SimpleDataProvider.TTable objReportData=new TReportWriter.SimpleDataProvider.TTable();
            
            objReportData.AddColumn("SR_NO");
            objReportData.AddColumn("VOUCHER_NO");
            objReportData.AddColumn("VOUCHER_DATE");
            objReportData.AddColumn("MAIN_ACCOUNT_CODE");
            objReportData.AddColumn("ACCOUNT_NAME");
            objReportData.AddColumn("REMARKS");
            objReportData.AddColumn("AMOUNT");
            objReportData.AddColumn("VAT_AMOUNT");
            
            
            TReportWriter.SimpleDataProvider.TRow objOpeningRow=objReportData.newRow();
            objOpeningRow.setValue("SR_NO","");
            objOpeningRow.setValue("VOUCHER_NO","");
            objOpeningRow.setValue("VOUCHER_DATE","0000-00-00");
            objOpeningRow.setValue("MAIN_ACCOUNT_CODE","");
            objOpeningRow.setValue("ACCOUNT_NAME","");
            objOpeningRow.setValue("REMARKS","");
            objOpeningRow.setValue("AMOUNT","");
            objOpeningRow.setValue("VAT_AMOUNT","");
            
            
            
            String strSQL = "";
            
            strSQL = "SELECT DISTINCT VOUHEAD.COMPANY_ID,VOUHEAD.VOUCHER_NO,VOUHEAD.VOUCHER_DATE, VOUDTL.MAIN_ACCOUNT_CODE, " +
            "GL.ACCOUNT_NAME,TRIM(VOUHEAD.REMARKS) AS REMARKS, SUM(VOUDTL.AMOUNT) AMOUNT, " +
            "CASE WHEN VOUDTL.MAIN_ACCOUNT_CODE='127561' THEN VOUDTL.AMOUNT ELSE 0 END AS VAT_AMOUNT " +
            "FROM D_FIN_VOUCHER_HEADER VOUHEAD, D_FIN_VOUCHER_DETAIL VOUDTL, D_FIN_GL GL " +
            "WHERE VOUHEAD.APPROVED=1 AND VOUHEAD.CANCELLED=0 " +
            "AND VOUDTL.MAIN_ACCOUNT_CODE IN ('301749','301763') AND VOUDTL.EFFECT = 'C' AND VOUHEAD.COMPANY_ID=VOUDTL.COMPANY_ID " +
            "AND VOUHEAD.VOUCHER_NO=VOUDTL.VOUCHER_NO AND VOUDTL.MAIN_ACCOUNT_CODE = GL.MAIN_ACCOUNT_CODE " +
            "AND VOUHEAD.VOUCHER_DATE >= '" + EITLERPGLOBAL.formatDateDB(txtFromDate.getText()) + "' "+
            "AND VOUHEAD.VOUCHER_DATE <= '" + EITLERPGLOBAL.formatDateDB(txtToDate.getText()) + "' "+
            "GROUP BY VOUHEAD.VOUCHER_DATE,VOUHEAD.VOUCHER_NO,VOUDTL.MAIN_ACCOUNT_CODE " +
            "ORDER BY VOUDTL.MAIN_ACCOUNT_CODE,VOUHEAD.COMPANY_ID,VOUHEAD.VOUCHER_DATE,VOUHEAD.VOUCHER_NO";
            
            ResultSet rsTmp=data.getResult(strSQL,FinanceGlobal.FinURL);
            rsTmp.first();
            
            int Counter = 0;
            
            if(rsTmp.getRow()>0) {
                while(!rsTmp.isAfterLast()) {
                    Counter ++;
                    String VoucherNo = UtilFunctions.getString(rsTmp,"VOUCHER_NO","");
                    String MainCode = UtilFunctions.getString(rsTmp,"MAIN_ACCOUNT_CODE","");
                    double VAT_Amount = 0;
                    if(MainCode.equals("301763")) {
                        //VAT_Amount = data.getDoubleValueFromDB("SELECT SUM(AMOUNT) FROM D_FIN_VOUCHER_DETAIL WHERE VOUCHER_NO='"+VoucherNo+"' AND MAIN_ACCOUNT_CODE LIKE '127561%'",FinanceGlobal.FinURL); 
                        VAT_Amount = data.getDoubleValueFromDB("SELECT SUM(B.AMOUNT) FROM D_FIN_VOUCHER_HEADER A,  D_FIN_VOUCHER_DETAIL B " +
                        "WHERE A.VOUCHER_NO='"+VoucherNo+"' AND A.VOUCHER_NO=B.VOUCHER_NO AND B.MAIN_ACCOUNT_CODE LIKE '127561%' " +
                        "AND A.APPROVED=1 AND A.CANCELLED=0",FinanceGlobal.FinURL);
                    } else {
                        VAT_Amount = 0;
                    }
                   
                    objRow=objReportData.newRow();                    
                    objRow.setValue("SR_NO",Integer.toString(Counter));
                    objRow.setValue("VOUCHER_NO",UtilFunctions.getString(rsTmp,"VOUCHER_NO",""));
                    objRow.setValue("VOUCHER_DATE",EITLERPGLOBAL.formatDate(UtilFunctions.getString(rsTmp,"VOUCHER_DATE","0000-00-00")));
                    objRow.setValue("MAIN_ACCOUNT_CODE",UtilFunctions.getString(rsTmp,"MAIN_ACCOUNT_CODE","").trim());
                    objRow.setValue("ACCOUNT_NAME",UtilFunctions.getString(rsTmp,"ACCOUNT_NAME",""));
                    objRow.setValue("REMARKS",UtilFunctions.getString(rsTmp,"REMARKS",""));
                    objRow.setValue("AMOUNT",UtilFunctions.getString(rsTmp,"AMOUNT",""));
                    objRow.setValue("VAT_AMOUNT",Double.toString(VAT_Amount));
                    objReportData.AddRow(objRow);
                    
                    rsTmp.next();
                }
            }
            
            int Comp_ID = EITLERPGLOBAL.gCompanyID;
            HashMap Parameters=new HashMap();
            //Parameters.put("COMPANY_ID",Integer.toString(Comp_ID));
            Parameters.put("FROM_DATE",txtFromDate.getText().trim());
            Parameters.put("TO_DATE",txtToDate.getText().trim());
            Parameters.put("SYS_DATE",EITLERPGLOBAL.getCurrentDate());
            
            objEngine.PreviewReport("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/finance/rptRetailShopSalesListing.rpt",Parameters,objReportData);
        }
        catch(Exception e) {
            e.printStackTrace();
        }
        
    }
    
    private boolean Validate() {
        //Form level validations
        
        if(txtFromDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Enter from Date");
            return false;
        } else if(!EITLERPGLOBAL.isDate(txtFromDate.getText())) {
            JOptionPane.showMessageDialog(null,"Invalid From Date in DD/MM/YYYY format.");
            return false;
        }
        
        if(txtToDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Enter To Date");
            return false;
        } else if(!EITLERPGLOBAL.isDate(txtToDate.getText())) {
            JOptionPane.showMessageDialog(null,"Invalid To Date in DD/MM/YYYY format.");
            return false;
        }
        
        return true;
    }
