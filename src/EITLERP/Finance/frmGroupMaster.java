    private boolean Validate() {
        
        String AccountCode = txtMainAccountCode.getText().substring(3);
        int AcCode = txtMainAccountCode.getText().length();
        
        if(txtMainAccountCode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Insert Main Account Code");
            return false;
        }
        
        if(!txtMainAccountCode.getText().trim().equals("")) {
            if(AcCode!=6)
            {
            JOptionPane.showMessageDialog(null,"Please Enter 6 digit Main Account Code");
            return false;
            }
        }
        if(!txtMainAccountCode.getText().trim().equals("")) {
            if(!AccountCode.equals("000"))
            {
            JOptionPane.showMessageDialog(null,"Invalid Main Account Code, last 3 digit must be zero.");
            return false;
            }
        }
        
        if(txtAccountName.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please Insert Account Name");
            return false;
        }
        return true;
    }
