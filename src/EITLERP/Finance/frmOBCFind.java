    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        // TODO add your handling code here:
       Cancelled=false;
       String subQuery="";
       
       strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID)+" ";
       
       if(!txtMainCode.getText().equals(""))
       {
         strQuery=strQuery+" AND MAIN_CODE='"+txtMainCode.getText()+"' ";
       }              

           
       if (!txtDocNo.getText().equals("")) {           
           strQuery=strQuery+" AND DOC_NO='"+txtDocNo.getText()+"' ";
       }
       if (!txtDocFrom.getText().equals("")) {           
           strQuery=strQuery+" AND DOC_DATE>='"+EITLERPGLOBAL.formatDateDB(txtDocFrom.getText())+"' ";
       }
       if (!txtDocTo.getText().equals("")) {           
           strQuery=strQuery+" AND DOC_DATE<='"+EITLERPGLOBAL.formatDateDB(txtDocTo.getText())+"' ";
       }
       if (!txtBankRefNo.getText().equals("")) {           
           strQuery=strQuery+" AND BANK_REFERENCE_NO='"+txtBankRefNo.getText()+"' ";
       }
       if (!txtBankFrom.getText().equals("")) {           
           strQuery=strQuery+" AND BANK_REFERENCE_DATE>='"+EITLERPGLOBAL.formatDateDB(txtBankFrom.getText())+"' ";
       }
        if (!txtBankTo.getText().equals("")) {           
           strQuery=strQuery+" AND BANK_REFERENCE_DATE<='"+EITLERPGLOBAL.formatDateDB(txtBankTo.getText())+"' ";
       }
        if (!txtPartyCode.getText().equals("")) {           
           strQuery=strQuery+" AND PARTY_CODE ='"+txtPartyCode.getText()+"' ";
       }
       strQuery=strQuery;
       //========== End Sub Query ============//
       
       getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
