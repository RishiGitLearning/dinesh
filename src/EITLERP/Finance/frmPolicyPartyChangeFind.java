    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        // TODO add your handling code here:
        Cancelled=false;
        
        strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID)+" ";
        
        
        if(!txtDocNo.getText().equals("")) {
            strQuery=strQuery+" AND DOC_NO='"+txtOldPartyID.getText()+"' ";
        }
        if(!txtDocDate.getText().equals("")) {
            strQuery=strQuery+" AND DOC_DATE='"+txtOldPartyID.getText()+"' ";
        }
        if(!txtOldPartyID.getText().equals("")) {
            strQuery=strQuery+" AND OLD_PARTY_ID='"+txtOldPartyID.getText()+"' ";
        }
        
        if(!txtNewPartyID.getText().equals("")) {
            strQuery=strQuery+" AND NEW_PARTY_ID ='"+txtNewPartyID.getText()+"' ";
        }
        
        strQuery=strQuery;
        //========== End Sub Query ============//
        
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
