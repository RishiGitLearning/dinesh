    private void txtNewPartyIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNewPartyIDKeyPressed
        // TODO add your handling code here:
        try {
            
            if(evt.getKeyCode()==112) {
                LOV aList=new LOV();
                
                aList.SQL="SELECT PARTY_CODE,PARTY_NAME "+
                " FROM D_FIN_PARTY_MASTER WHERE APPROVED=1 AND CANCELLED=0 ORDER BY PARTY_CODE ";
                aList.ReturnCol=1;
                aList.ShowReturnCol=true;
                aList.DefaultSearchOn=2;
                aList.UseSpecifiedConn=true;
                aList.dbURL=FinanceGlobal.FinURL;
                
                if(aList.ShowLOV()) {
                    txtNewPartyID.setText(aList.ReturnVal);
                    txtNewPartyName.setText(clsPolicyMaster.getPartyName(EITLERPGLOBAL.gCompanyID, txtNewPartyID.getText()));
                }
                
            }
            
        }
        catch(Exception e) {
            
        }
    }//GEN-LAST:event_txtNewPartyIDKeyPressed
    
