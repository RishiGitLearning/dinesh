    private void txtReceiptNoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtReceiptNoKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==112) {
            
            LOV aList=new LOV();
            
            aList.SQL="SELECT RECEIPT_NO FROM D_FD_DEPOSIT_MASTER WHERE APPROVED=1 AND DEPOSIT_STATUS=0 AND MATURITY_DATE > '"+EITLERPGLOBAL.getCurrentDateDB()+"' AND COMPANY_ID="+ EITLERPGLOBAL.gCompanyID +" ORDER BY RECEIPT_NO";
            aList.ReturnCol=1;
            aList.ShowReturnCol=true;
            aList.DefaultSearchOn=2;
            aList.UseSpecifiedConn=true;
            aList.dbURL=FinanceGlobal.FinURL;
            
            if(aList.ShowLOV()) {
                txtReceiptNo.setText(aList.ReturnVal);
            }
        }
    }//GEN-LAST:event_txtReceiptNoKeyPressed
    
