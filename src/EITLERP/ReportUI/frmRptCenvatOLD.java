    private void writeLine(BufferedWriter aFile,String strLine) {
        //Writes line maintaining page length
        try {
            if(lineCounter>=64) {
                
                aFile.write(EITLERPGLOBAL.Replicate("-",80));
                aFile.newLine();
                aFile.write("Page : "+pageCounter);
                aFile.newLine();
                
                aFile.write("\f");
                
                pageCounter++;
                
                
                PrintHeader(aFile);
            }
            
            aFile.write(LeftMargin+strLine);
            aFile.newLine();
            lineCounter++;
        }
        catch(Exception e) {
            
        }
    }
    
    private void newPage(BufferedWriter aFile) {
        //Writes line maintaining page length
        try {
            
            while(lineCounter<=70) {
                aFile.write(" ");
                aFile.newLine();
                
                lineCounter++;
            }
            
            PrintHeader(aFile);
            
        }
        catch(Exception e) {
            
        }
    }
    
    private Frame findParentFrame(JApplet pApplet) {
        Container c = (Container) pApplet;
        while(c != null) {
            if (c instanceof Frame)
                return (Frame)c;
            
            c = c.getParent();
        }
        return (Frame)null;
    }
    
    
