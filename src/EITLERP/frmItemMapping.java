    private void FormatGrid() {
        
        EITLTableCellRenderer cellRender=new EITLTableCellRenderer();
        cellRender.setHorizontalAlignment(JLabel.RIGHT);
        
        DataModel=new EITLTableModel();
        Table.removeAll();
        Table.setModel(DataModel);
        Table.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        
        DataModel.addColumn("Sr.");
        DataModel.addColumn("First Item ID");
        DataModel.addColumn("Item Name");
        DataModel.addColumn("Second Item ID");
        DataModel.addColumn("Item NAme");
        
        DataModel.SetNumeric(1,true);
        
        DataModel.TableReadOnly(true);
        //Table.getColumnModel().getColumn(4).setCellRenderer(cellRender);
        
        RowFormat=new EITLTableCellRenderer();
        for(int j=0;j<Table.getColumnCount();j++) {
            Table.getColumnModel().getColumn(j).setCellRenderer(RowFormat);
        }
    }
    
    
    private void GenerateGrid() {
        HashMap List=new HashMap();
        
        FormatGrid();
        
        List=clsItemMapping.getList();
        
        for(int i=1;i<=List.size();i++) {
            clsItemMapping ObjItem=(clsItemMapping)List.get(Integer.toString(i));
            Object[] rowData=new Object[5];
            
            rowData[0]=Integer.toString((int)ObjItem.getAttribute("SR_NO").getVal());
            rowData[1]=(String)ObjItem.getAttribute("FIRST_ITEM_ID").getObj();
            rowData[2]=clsItem.getItemName(EITLERPGLOBAL.gCompanyID,(String)ObjItem.getAttribute("FIRST_ITEM_ID").getObj());
            rowData[3]=(String)ObjItem.getAttribute("SECOND_ITEM_ID").getObj();
            String Sql="";
            if(EITLERPGLOBAL.gCompanyID == 2) {
                Sql="SELECT ITEM_DESCRIPTION FROM DINESHMILLSA.D_INV_ITEM_MASTER WHERE ITEM_ID ='" + ObjItem.getAttribute("SECOND_ITEM_ID").getString() + "'" ;
            }
            else if(EITLERPGLOBAL.gCompanyID == 3) {
                Sql="SELECT ITEM_DESCRIPTION FROM DINESHMILLS.D_INV_ITEM_MASTER WHERE ITEM_ID ='" + ObjItem.getAttribute("SECOND_ITEM_ID").getString() + "'" ;
            }
            rowData[4]=data.getStringValueFromDB(Sql);
            
            DataModel.addRow(rowData);
        }
        
    }
    
     
    
    private void searchWithin(char recentKey) {
        try {
            RowFormat.removeBackColors();
            CellAlign.removeBackColors();
            
            Table.repaint();
            
            String searchString=txtSearch.getText();
            
            if(!searchString.trim().equals("")) {
                
                
                if(recentKey!=' ') {
                    searchString=searchString+recentKey;
                }
                
                searchString=searchString.toLowerCase();
                
                int currentCol=Table.getSelectedColumn();
                
                for(int i=0;i<=Table.getRowCount()-1;i++) {
                    
                    if(Table.getValueAt(i,currentCol).toString().trim().toLowerCase().indexOf(searchString)!=-1) {
                        
                        Table.changeSelection(i, currentCol, false,false);
                        
                        RowFormat.setBackColor(i, currentCol, Color.YELLOW);
                        
                        /*if(currentCol==DataModel.getColFromVariable("AMOUNT")) {
                            CellAlign.setBackColor(i, currentCol, Color.YELLOW);
                        }*/
                    }
                }
                Table.repaint();
            }
        }
        catch(Exception e) {
            
        }
    }
    
