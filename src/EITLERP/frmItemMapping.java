    private void cmdAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAddActionPerformed
        // TODO add your handling code here:
        if(txtFirstItemCode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter item code");
            return;
        }
        
        if(!clsItem.IsValidItemID(EITLERPGLOBAL.gCompanyID, txtFirstItemCode.getText())) {
            JOptionPane.showMessageDialog(null,"Item code you entered is not valid. Please verify");
            return;
        }
        
        if(txtSecondItemCode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter second item code");
            return;
        }
        
        clsItemMapping ObjItem=new clsItemMapping();
        ObjItem.setAttribute("COMPANY_ID",EITLERPGLOBAL.gCompanyID);
        ObjItem.setAttribute("FIRST_ITEM_ID",txtFirstItemCode.getText().trim());
        
        if(EITLERPGLOBAL.gCompanyID==2) {
            ObjItem.setAttribute("SECOND_COMPANY_ID",3);
        }
        
        if(EITLERPGLOBAL.gCompanyID==3) {
            ObjItem.setAttribute("SECOND_COMPANY_ID",2);
        }
        
        ObjItem.setAttribute("SECOND_ITEM_ID",txtSecondItemCode.getText().trim());
        
        
        String SQL="SELECT * FROM D_COM_ITEM_MAPPING WHERE FIRST_ITEM_ID = '" + txtFirstItemCode.getText() + "'";
        if(data.IsRecordExist(SQL)) {
            JOptionPane.showMessageDialog(null,"Fisrt Item code you entered is Already Exsits");
            return;
        }
        
        SQL="SELECT * FROM D_COM_ITEM_MAPPING WHERE SECOND_ITEM_ID = '" + txtSecondItemCode.getText() + "'";
        if(data.IsRecordExist(SQL)) {
            JOptionPane.showMessageDialog(null,"Second Unit Item code you entered is Already Exsits");
            return;
        }
        
        if(clsItemMapping.Insert(ObjItem)) {
            GenerateGrid();
        }
        else {
            JOptionPane.showMessageDialog(null,"Error occured while saving");
        }
        
    }//GEN-LAST:event_cmdAddActionPerformed
    
