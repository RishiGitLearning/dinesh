    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String DebitMemoDate = jTextFieldDebitMemoDate.getText();       
        String DebitMemoNo = jTextFieldDebitMemoNo.getText();
        String partyCode= txtPartyCode.getText().trim();
        String invNo= txtInvoiceNo.getText().trim();
        String invDate= txtInvoiceDate.getText().trim();
        String invFromDate=txtFromDate.getText().trim();
        String invToDate=txtToDate.getText().trim();
        
        if(!DebitMemoDate.equals("")) {
            stringFindQuery=stringFindQuery+" AND DEBITMEMO_DATE='"+EITLERPGLOBAL.formatDateDB(DebitMemoDate)+"' ";
        }
        
        if(!DebitMemoNo.equals("")) {
            stringFindQuery=stringFindQuery+" AND DEBITMEMO_NO ='"+ DebitMemoNo +"'";
        }
        
        if(!partyCode.equals("")) {
            stringFindQuery=stringFindQuery+" AND DB_PARTY_CODE ='"+ partyCode +"'";
        }
        if(!invNo.equals("")) {
            stringFindQuery=stringFindQuery+" AND INVOICE_NO ='"+ invNo +"'";
        }
        if(!invDate.equals("")) {
            stringFindQuery=stringFindQuery+" AND INVOICE_DATE ='"+ EITLERPGLOBAL.formatDateDB(invDate) +"'";
        }
        
        if(!invFromDate.equals("")) {
            stringFindQuery=stringFindQuery+" AND INVOICE_DATE >='"+ EITLERPGLOBAL.formatDateDB(invFromDate) +"'";
        }
        if(!invToDate.equals("")) {
            stringFindQuery=stringFindQuery+" AND INVOICE_DATE <='"+ EITLERPGLOBAL.formatDateDB(invToDate) +"'";
        }
        
        
        
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed

    private void txtPartyCodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPartyCodeKeyPressed
if (evt.getKeyCode() == 112) {
            try {
                LOV aList = new LOV();

                aList.SQL = "SELECT PARTY_CODE,PARTY_NAME FROM DINESHMILLS.D_SAL_PARTY_MASTER  WHERE MAIN_ACCOUNT_CODE='210010'";
                aList.ReturnCol = 1;
                aList.ShowReturnCol = true;
                aList.DefaultSearchOn = 2;
                aList.UseSpecifiedConn = true;
                aList.dbURL = EITLERPGLOBAL.DatabaseURL;

                if (aList.ShowLOV()) {
                    txtPartyCode.setText(aList.ReturnVal);
                    //lblPARTY.setText(clsSales_Party.getPartyName(EITLERPGLOBAL.gCompanyID, aList.ReturnVal));
                }
            }catch(Exception e)
            {
                e.printStackTrace();
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_txtPartyCodeKeyPressed
    
    
