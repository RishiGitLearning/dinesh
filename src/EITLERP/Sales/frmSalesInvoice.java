    private void Find() {
        Loader ObjLoader=new Loader(this,"EITLERP.Sales.frmInvoiceFind",true);
        frmInvoiceFind ObjReturn= (frmInvoiceFind) ObjLoader.getObj();
        
        if(ObjReturn.Cancelled==false) {
            if(!objInvoice.Filter(ObjReturn.strQuery,EITLERPGLOBAL.gCompanyID)) {
                JOptionPane.showMessageDialog(null,"No records found.");
            }
            MoveLast();
        }
    }
    
    
    public void FindByCompany(int pCompanyID,String pDocNo,String pDocDate) {
        String dbURL=clsFinYear.getDBURL(pCompanyID,EITLERPGLOBAL.FinYearFrom);
        objInvoice.Filter(" WHERE COMPANY_ID="+Integer.toString(pCompanyID)+" AND INVOICE_NO='"+pDocNo+"' AND INVOICE_DATE='"+ EITLERPGLOBAL.formatDateDB(pDocDate)+"' ",pCompanyID,dbURL);
        objInvoice.MoveLast();
        DisplayData();
    }
    
    
