    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String TRANSPORTER = TRANSPORTER_NAME.getText();       
        String DocNo = txtDocNo.getText();
        
        if(!TRANSPORTER.equals("")) {
            stringFindQuery=stringFindQuery+" AND TRANSPORTER_NAME='"+TRANSPORTER_NAME.getText()+"' ";
        }
        
        if(!DocNo.equals("")) {
            stringFindQuery=stringFindQuery+" AND TRANSPORTER_ID ='"+ DocNo +"'";
        }
        
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed

