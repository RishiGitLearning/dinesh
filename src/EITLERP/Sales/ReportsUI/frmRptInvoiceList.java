    private boolean Validate() {
        //Form level validations
        if(txtFromDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter from date");
            return false;
        } else if(!EITLERPGLOBAL.isDate(txtFromDate.getText())) {
            JOptionPane.showMessageDialog(null,"Invalid From Date in DD/MM/YYYY format.");
            return false;
        }
        
        if(txtToDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter To date");
            return false;
        } else if(!EITLERPGLOBAL.isDate(txtToDate.getText())) {
            JOptionPane.showMessageDialog(null,"Invalid To Date in DD/MM/YYYY format.");
            return false;
        }
        
        if (EITLERPGLOBAL.getCombostrCode(cmbInvoiceType).equals("0")){
            JOptionPane.showMessageDialog(null,"Invalid Invoice Type.");
        }
        return true;
    }
    
    private void PreviewReport() {
        try {
            int InvoiceType = Integer.parseInt(EITLERPGLOBAL.getCombostrCode(cmbInvoiceType));
            String MainCode="";
            if(InvoiceType==1) {
                MainCode="210027";
            } else if(InvoiceType==2) {
                MainCode="210010";
            } else if(InvoiceType==3) {
                MainCode="210072";
            }
            URL ReportFile=new URL("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/sales/rptSalesInvoiceDetail.jsp?dbURL="+EITLERPGLOBAL.DatabaseURL+"&CompanyID="+EITLERPGLOBAL.gCompanyID+"&InvoiceType="+EITLERPGLOBAL.getCombostrCode(cmbInvoiceType)+"&FromDate="+EITLERPGLOBAL.formatDateDB(txtFromDate.getText())+"&ToDate="+EITLERPGLOBAL.formatDateDB(txtToDate.getText())+"&MainCode="+MainCode);
            System.out.println("Report Path : " +ReportFile);
            EITLERPGLOBAL.loginContext.showDocument(ReportFile,"_blank");
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null,"File error "+e.getMessage());
        }
    }
    
    
