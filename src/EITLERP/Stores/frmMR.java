    private void Save() {
        
        
        //Check for New Item Code and Final Approval Action. In such case, do not allow new item code while final approving
        if(OpgFinal.isSelected()) {
            for(int i=0;i<Table.getRowCount();i++) {
                String ItemID=(String)Table.getValueAt(i, 1);
                
                if(ItemID.trim().equals("NEWITEM")) {
                    JOptionPane.showMessageDialog(null,"You cannot final approve the PR with NEWITEM item. Please replace these items with new item codes and then save");
                    return;
                }
                
            }
        }
        //Form level validations
        if(txtDocDate.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter document date");
            return;
        }
        
        if(cmbBuyer.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(null,"Please select the user");
            return;
        }
        
        if(cmbForDept.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(null,"Please select the department");
            return;
        }
        
        if(cmbHierarchy.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(null,"Please select the hierarchy.");
            return;
        }
        
        if((!OpgApprove.isSelected())&&(!OpgReject.isSelected())&&(!OpgFinal.isSelected())&&(!OpgHold.isSelected())) {
            JOptionPane.showMessageDialog(null,"Please select the Approval Action");
            return;
        }
        
        //Check the no. of items
        if(Table.getRowCount()<=0) {
            JOptionPane.showMessageDialog(null,"Please enter some items.");
            return;
        }
        
        //Check negative Qty.
        for(int i=0;i<=Table.getRowCount()-1;i++) {
            String strDate=(String)Table.getValueAt(i,5);
            if(!EITLERPGLOBAL.isDate(strDate)) {
                JOptionPane.showMessageDialog(null,"Invalid required date. Pleaes verify the input");
                return;
            }
            
            double theQty=Double.parseDouble((String)Table.getValueAt(i, 4));
            if(theQty<=0) {
                JOptionPane.showMessageDialog(null,"Please enter qty.");
                return;
            }
            
            String theDate=(String)Table.getValueAt(i,5);
            if(theDate.equals("")||!EITLERPGLOBAL.isDate(theDate)) {
                JOptionPane.showMessageDialog(null,"Please enter valid required date");
                return;
            }
            
            
            
            
            java.sql.Date DocDate=java.sql.Date.valueOf(EITLERPGLOBAL.formatDateDB(txtDocDate.getText()));
            if(!strDate.trim().equals("")) {
                java.sql.Date ReqDate=java.sql.Date.valueOf(EITLERPGLOBAL.formatDateDB(strDate));
                if(DocDate.after(ReqDate)) {
                    JOptionPane.showMessageDialog(null,"Required date must be after the document date.");
                    return;
                }
            }
            
            
            
            String strItemID=(String)Table.getValueAt(i,1);
            if(!clsItem.IsValidItemID(EITLERPGLOBAL.gCompanyID,strItemID)) {
                JOptionPane.showMessageDialog(null,"Invalid Item code. Please verify the input");
                return;
            }
            
            
            
            
        }
        
        if(!EITLERPGLOBAL.isDate(txtDocDate.getText())) {
            JOptionPane.showMessageDialog(null,"Invalid MR Date");
            return;
        }
        
        
        if(OpgReject.isSelected()&&txtToRemarks.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter the remarks for rejection");
            return;
        }
        
        if( (OpgApprove.isSelected()||OpgReject.isSelected())&&cmbSendTo.getItemCount()<=0) {
            JOptionPane.showMessageDialog(null,"Please select the user, to whom rejected document to be send");
            return;
        }
        
        SetData();
        
        if(EditMode==EITLERPGLOBAL.ADD) {
            if(ObjMR.Insert()) {
                MoveLast();
                DisplayData();
            }
            else {
                JOptionPane.showMessageDialog(null,"Error occured while saving. Error is "+ObjMR.LastError);
                return;
            }
        }
        
        if(EditMode==EITLERPGLOBAL.EDIT) {
            if(ObjMR.Update()) {
                DisplayData();
            }
            else {
                JOptionPane.showMessageDialog(null,"Error occured while saving. Error is "+ObjMR.LastError);
                return;
            }
        }
        
        EditMode=0;
        SetFields(false);
        EnableToolbar();
        SetMenuForRights();
        try {
            frmPA.RefreshView();
        }catch(Exception e){}
    }
    
