    //Didplay data on the Screen
    private void DisplayData() {
        
        //=========== Color Indication ===============//
        try {
            if(EditMode==0) {
                if(ObjMR.getAttribute("APPROVED").getInt()==1) {
                    lblTitle.setBackground(Color.BLUE);
                }
                
                if(ObjMR.getAttribute("APPROVED").getInt()!=1) {
                    lblTitle.setBackground(Color.GRAY);
                }
                
                if(ObjMR.getAttribute("CANCELED").getInt()==1) {
                    lblTitle.setBackground(Color.RED);
                }
                
                
            }
        }
        catch(Exception c) {
            
        }
        //============================================//
        
        
        //========= Authority Delegation Check =====================//
        if(EITLERPGLOBAL.gAuthorityUserID!=EITLERPGLOBAL.gUserID) {
            int ModuleID=2;
            
            if(clsAuthority.IsAuthorityGiven(EITLERPGLOBAL.gCompanyID,EITLERPGLOBAL.gUserID,EITLERPGLOBAL.gAuthorityUserID,ModuleID)) {
                EITLERPGLOBAL.gNewUserID=EITLERPGLOBAL.gAuthorityUserID;
            }
            else {
                EITLERPGLOBAL.gNewUserID=EITLERPGLOBAL.gUserID;
            }
        }
        //==========================================================//
        
        
        ClearFields();
        txtDocNo.setText((String)ObjMR.getAttribute("REQ_NO").getObj());
        lblTitle.setText("PURCHASE REQUISITION - "+txtDocNo.getText());
        lblRevNo.setText(Integer.toString((int)ObjMR.getAttribute("REVISION_NO").getVal()));
        txtDocDate.setText(EITLERPGLOBAL.formatDate((String)ObjMR.getAttribute("REQ_DATE").getObj()));
        EITLERPGLOBAL.setComboIndex(cmbBuyer,(int)ObjMR.getAttribute("BUYER").getVal());
        GenerateDeptCombo();
        EITLERPGLOBAL.setComboIndex(cmbForDept,(int)ObjMR.getAttribute("DEST_DEPT_ID").getVal());
        EITLERPGLOBAL.setComboIndex(cmbStatus,(String)ObjMR.getAttribute("STATUS").getObj());
        
        txtPurpose.setText((String)ObjMR.getAttribute("PURPOSE").getObj());
        txtRemarks.setText((String)ObjMR.getAttribute("REMARKS").getObj());
        chkCancelled.setSelected(ObjMR.getAttribute("CANCELED").getBool());
        chkTestingFlag.setSelected(ObjMR.getAttribute("TESTING_FLAG").getBool());
        txtTestingReason.setText((String)ObjMR.getAttribute("TESTING_REASON").getObj()) ;
        txtTestingDays.setText((String)ObjMR.getAttribute("TESTING_DAYS").getObj());
        EITLERPGLOBAL.setComboIndex(cmbHierarchy,(int)ObjMR.getAttribute("HIERARCHY_ID").getVal());
        
        
        chkAttachement.setSelected(ObjMR.getAttribute("ATTACHEMENT").getBool());
        txtFile.setText((String)ObjMR.getAttribute("ATTACHEMENT_PATH").getObj());
        OldFileName=(String)ObjMR.getAttribute("ATTACHEMENT_PATH").getObj();
        
        DoNotEvaluate=true;
        
        FormatGrid();
        //Now Generate Table
        for(int i=1;i<=ObjMR.colMRItems.size();i++) {
            clsMRItem ObjItem=(clsMRItem)ObjMR.colMRItems.get(Integer.toString(i));
            Object[] rowData=new Object[10];
            
            rowData[0]=Integer.toString(i);
            rowData[1]=(String)ObjItem.getAttribute("ITEM_CODE").getObj();
            rowData[2]=clsItem.getItemName(EITLERPGLOBAL.gCompanyID,(String)ObjItem.getAttribute("ITEM_CODE").getObj());
            rowData[3]=(String)ObjItem.getAttribute("ITEM_EXTRA_DESC").getObj();
            rowData[4]=Double.toString(EITLERPGLOBAL.round(ObjItem.getAttribute("REQ_QTY").getVal(),3));
            rowData[5]=EITLERPGLOBAL.formatDate((String)ObjItem.getAttribute("REQUIRED_DATE").getObj());
            rowData[6]=Integer.toString((int)ObjItem.getAttribute("UNIT").getVal());
            rowData[7]=clsParameter.getParaDescription(EITLERPGLOBAL.gCompanyID, "UNIT",(int)ObjItem.getAttribute("UNIT").getVal());
            rowData[8]=(String)ObjItem.getAttribute("REMARKS").getObj();
            rowData[9]=Double.toString(ObjItem.getAttribute("ALLOCATED_QTY").getVal());
            
            DataModel.addRow(rowData);
        }
        
        DoNotEvaluate=false;
        
        //======== Generating Grid for Document Approval Flow ========//
        FormatGridA();
        HashMap List=new HashMap();
        String DocNo=(String)ObjMR.getAttribute("REQ_NO").getObj();
        List=ApprovalFlow.getDocumentFlow(EITLERPGLOBAL.gCompanyID, 2, DocNo);
        for(int i=1;i<=List.size();i++) {
            clsDocFlow ObjFlow=(clsDocFlow)List.get(Integer.toString(i));
            Object[] rowData=new Object[7];
            
            rowData[0]=Integer.toString(i);
            rowData[1]=clsUser.getUserName(EITLERPGLOBAL.gCompanyID,(int)ObjFlow.getAttribute("USER_ID").getVal());
            rowData[2]=(String)ObjFlow.getAttribute("STATUS").getObj();
            rowData[3]=clsDepartment.getDeptName(EITLERPGLOBAL.gCompanyID, (int)ObjFlow.getAttribute("DEPT_ID").getVal());
            rowData[4]=EITLERPGLOBAL.formatDate((String)ObjFlow.getAttribute("RECEIVED_DATE").getObj());
            rowData[5]=EITLERPGLOBAL.formatDate((String)ObjFlow.getAttribute("ACTION_DATE").getObj());
            rowData[6]=(String)ObjFlow.getAttribute("REMARKS").getObj();
            
            DataModelA.addRow(rowData);
        }
        
        //Showing Audit Trial History
        FormatGridHS();
        HashMap History=clsMaterialRequisition.getHistoryList(EITLERPGLOBAL.gCompanyID, DocNo);
        for(int i=1;i<=History.size();i++) {
            clsMaterialRequisition ObjHistory=(clsMaterialRequisition)History.get(Integer.toString(i));
            Object[] rowData=new Object[5];
            
            rowData[0]=Integer.toString((int)ObjHistory.getAttribute("REVISION_NO").getVal());
            rowData[1]=clsUser.getUserName(EITLERPGLOBAL.gCompanyID,(long)ObjHistory.getAttribute("UPDATED_BY").getVal());
            rowData[2]=EITLERPGLOBAL.formatDate((String)ObjHistory.getAttribute("ENTRY_DATE").getObj());
            
            String ApprovalStatus="";
            
            if(((String)ObjHistory.getAttribute("APPROVAL_STATUS").getObj()).equals("A")) {
                ApprovalStatus="Approved";
            }
            
            if(((String)ObjHistory.getAttribute("APPROVAL_STATUS").getObj()).equals("F")) {
                ApprovalStatus="Final Approved";
            }
            
            if(((String)ObjHistory.getAttribute("APPROVAL_STATUS").getObj()).equals("W")) {
                ApprovalStatus="Waiting";
            }
            
            if(((String)ObjHistory.getAttribute("APPROVAL_STATUS").getObj()).equals("R")) {
                ApprovalStatus="Rejected";
            }
            
            if(((String)ObjHistory.getAttribute("APPROVAL_STATUS").getObj()).equals("P")) {
                ApprovalStatus="Pending";
            }
            
            if(((String)ObjHistory.getAttribute("APPROVAL_STATUS").getObj()).equals("C")) {
                ApprovalStatus="Skiped";
            }
            
            
            rowData[3]=ApprovalStatus;
            rowData[4]=(String)ObjHistory.getAttribute("APPROVER_REMARKS").getObj();
            
            DataModelHS.addRow(rowData);
        }
        
        DisplayIndicators();
        //============================================================//
    }
    
