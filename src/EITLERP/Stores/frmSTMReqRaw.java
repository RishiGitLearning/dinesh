    private boolean Validate() {
        //Form level validations
        if(txtDocDate.getText().equals("")) {
            JOptionPane.showMessageDialog(this,"Please enter document date");
            return false;
        }
        
        if(cmbTransferTo.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(this,"Please select the Transfer To");
            return false;
        }
        
        if(cmbDept.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(this,"Please select the Department");
            return false;
        }
        
        if(txtRemarks.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this,"Please enter the Remarks");
            return false;
        }
        if(cmbHierarchy.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(this,"Please select the hierarchy.");
            return false;
        }
        
        if((!OpgApprove.isSelected())&&(!OpgReject.isSelected())&&(!OpgFinal.isSelected())&&(!OpgHold.isSelected())) {
            JOptionPane.showMessageDialog(this,"Please select the Approval Action");
            return false;
        }
        
        //Check the no. of items
        if(Table.getRowCount()<=0) {
            JOptionPane.showMessageDialog(this,"Please enter some items.");
            return false;
        }
        
        if(EditMode!=EITLERPGLOBAL.AMEND) {
            if(cmbHierarchy.getSelectedIndex()==-1) {
                JOptionPane.showMessageDialog(this,"Please select the hierarchy.");
                return false;
            }
            
            if((!OpgApprove.isSelected())&&(!OpgReject.isSelected())&&(!OpgFinal.isSelected())&&(!OpgHold.isSelected())) {
                JOptionPane.showMessageDialog(this,"Please select the Approval Action");
                return false;
            }
            
            java.sql.Date docDate=java.sql.Date.valueOf(EITLERPGLOBAL.formatDateDB(txtDocDate.getText()));
            
            if(OpgReject.isSelected()&&txtToRemarks.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this,"Please enter the remarks for rejection");
                return false;
            }
            
            if((OpgApprove.isSelected()||OpgReject.isSelected())&&cmbSendTo.getItemCount()<=0) {
                JOptionPane.showMessageDialog(this,"Please select the user, to whom rejected document to be send");
                return false;
            }
        }
        
        //Check negative Qty.
        for(int i=0;i<=Table.getRowCount()-1;i++) {
            String strDate=Table.getValueAt(i,5).toString();
            if(!EITLERPGLOBAL.isDate(strDate)) {
                JOptionPane.showMessageDialog(this,"Invalid required date. Pleaes verify the input");
                return false;
            }
            
            double theQty=Double.parseDouble(Table.getValueAt(i, 4).toString());
            if(theQty<=0) {
                JOptionPane.showMessageDialog(this,"Please enter qty.");
                return false;
            }
            
            String theDate=Table.getValueAt(i,5).toString();
            if(theDate.equals("")||!EITLERPGLOBAL.isDate(theDate)) {
                JOptionPane.showMessageDialog(this,"Please enter valid required date");
                return false;
            }
            
            java.sql.Date DocDate=java.sql.Date.valueOf(EITLERPGLOBAL.formatDateDB(txtDocDate.getText()));
            if(!strDate.trim().equals("")) {
                java.sql.Date ReqDate=java.sql.Date.valueOf(EITLERPGLOBAL.formatDateDB(strDate));
                if(DocDate.after(ReqDate)) {
                    JOptionPane.showMessageDialog(this,"Required date must be after the document date.");
                    return false;
                }
            }
            
            String strItemID=(String)Table.getValueAt(i,1);
            if(!clsItem.IsValidItemID(EITLERPGLOBAL.gCompanyID,strItemID)) {
                JOptionPane.showMessageDialog(this,"Invalid Item code. Please verify the input");
                return false;
            }
            int SrNo = Integer.parseInt(Table.getValueAt(i,0).toString());
            double ExQty=data.getDoubleValueFromDB("SELECT SUM(QTY) AS SUMQTY FROM D_INV_STM_HEADER A,D_INV_STM_DETAIL B WHERE A.STM_NO=B.STM_NO AND A.STM_TYPE=B.STM_TYPE AND A.CANCELLED=0 AND STM_REQ_NO='"+txtDocNo.getText().trim()+"' AND STM_REQ_SR_NO="+SrNo);
            
            if(theQty<ExQty) {
                JOptionPane.showMessageDialog(this,"Qty cannot be decreased to "+theQty+". It can be short closed upto "+ExQty);
                return false;
            }
        }
        
        if(!EITLERPGLOBAL.isDate(txtDocDate.getText())) {
            JOptionPane.showMessageDialog(this,"Invalid Requisition Date");
            return false;
        }
        
        
        if(OpgReject.isSelected()&&txtToRemarks.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this,"Please enter the remarks for rejection");
            return false;
        }
        
        if( (OpgApprove.isSelected()||OpgReject.isSelected())&&cmbSendTo.getItemCount()<=0) {
            JOptionPane.showMessageDialog(this,"Please select the user, to whom rejected document to be send");
            return false;
        }
        
        return true;
    }
