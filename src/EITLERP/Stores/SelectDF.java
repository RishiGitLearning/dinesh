    private void FormatGrid() {
        DataModel=new EITLTableModel();
        
        Table.removeAll();
        
        Table.setModel(DataModel);
        Table.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        
        for(int i=1;i<=15;i++) {
            DataModel.SetReadOnly(i);
        }
        
        //Add Columns to it
        DataModel.addColumn("*"); //0 Selection
        DataModel.addColumn("Sr.");//1
        DataModel.addColumn("Item ID");//2
        DataModel.addColumn("Item Name");//3
        DataModel.addColumn("Description");//4
        DataModel.addColumn("Received Qty");//5
        DataModel.addColumn("Returned Qty.");//6
        DataModel.addColumn("Balance Qty.");//7
        DataModel.addColumn("Unit ID");//8
        DataModel.addColumn("Unit");//9
        
        Rend.setCustomComponent(0,"CheckBox");
        Table.getColumnModel().getColumn(0).setCellRenderer(Rend);
        Table.getColumnModel().getColumn(0).setCellEditor(new DefaultCellEditor(new JCheckBox()));
    }
    
    private void SetList() {
        int SrNo=0;
        HashMap List=new HashMap();
        
        colSelItems.clear();
        
        String DFNo=txtGRNNo.getText();
        
        clsDeclarationForm tmpObj=new clsDeclarationForm();
        ObjDF=(clsDeclarationForm)tmpObj.getObject(EITLERPGLOBAL.gCompanyID, DFNo);
        
        CopyHeader=chkCopyHeader.isSelected();
        
        List=clsDeclarationForm.getDFItemList(EITLERPGLOBAL.gCompanyID, SelDFNo,false);
        //Scan the Table to get selected items
        for(int i=1;i<=List.size();i++) {
            clsDeclarationFormItem ObjItem=(clsDeclarationFormItem)List.get(Integer.toString(i));
            SrNo=(int)ObjItem.getAttribute("SR_NO").getVal();
            
            //Search in the table for SrNo.
            for(int j=0;j<Table.getRowCount();j++) {
                if(Integer.parseInt(Table.getValueAt(j, 1).toString())==SrNo) {
                    if(Table.getValueAt(j,0).toString().equals("true")) {
                        //Selected Item
                        colSelItems.put(Integer.toString(colSelItems.size()+1),ObjItem);
                    }
                }
            }
        }
    }
    
    private void GenerateGrid() {
        HashMap List=new HashMap();
        String DFNo=txtGRNNo.getText();
        
        if(!DFNo.equals("")) {
            
            List=clsDeclarationForm.getDFItemList(EITLERPGLOBAL.gCompanyID, DFNo,false);
            
            for(int i=1;i<=List.size();i++) {
                clsDeclarationFormItem ObjItem=(clsDeclarationFormItem)List.get(Integer.toString(i));
                HashMap colLot=ObjItem.colItemLot;
                if(ObjItem.getAttribute("BAL_QTY").getDouble()<=0) {
                    continue;
                }
                Object[] rowData=new Object[10];
                
                rowData[0]=Boolean.valueOf(true); //By default not selected
                rowData[1]=Integer.toString((int)ObjItem.getAttribute("SR_NO").getVal());
                rowData[2]=(String)ObjItem.getAttribute("ITEM_CODE").getObj();
                rowData[3]=clsItem.getItemName(EITLERPGLOBAL.gCompanyID,(String)ObjItem.getAttribute("ITEM_CODE").getObj());
                rowData[4]=(String)ObjItem.getAttribute("DECLARATION_DESC").getObj();
                rowData[5]=Double.toString(ObjItem.getAttribute("RECD_QTY").getVal());
                rowData[6]=Double.toString(ObjItem.getAttribute("RETURNED_QTY").getVal());
                rowData[7]=Double.toString(ObjItem.getAttribute("BAL_QTY").getVal());
                rowData[8]=Integer.toString((int)ObjItem.getAttribute("UNIT").getVal());
                rowData[9]=clsParameter.getParaDescription(EITLERPGLOBAL.gCompanyID, "UNIT", (int)ObjItem.getAttribute("UNIT").getVal());
                DataModel.addRow(rowData);
                
                //Set the Collection
                DataModel.SetUserObject(Table.getRowCount()-1, colLot);
            }
        }
        
    }
    
    //Recurses through the hierarchy of classes
    //until it finds Frame
    private Frame findParentFrame(JApplet pApplet) {
        Container c = (Container) pApplet;
        while(c != null) {
            if (c instanceof Frame)
                return (Frame)c;
            
            c = c.getParent();
        }
        return (Frame)null;
    }
    
    public boolean ShowList() {
        try {
            FormatGrid();
            
            setSize(700,430);
            
            Frame f=findParentFrame(this);
            
            aDialog=new JDialog(f,"Select D.F. Items",true);
            
            aDialog.getContentPane().add("Center",this);
            Dimension appletSize = this.getSize();
            aDialog.setSize(appletSize);
            aDialog.setResizable(false);
            
            //Place it to center of the screen
            Dimension screenSize=Toolkit.getDefaultToolkit().getScreenSize();
            aDialog.setLocation((int)(screenSize.width-appletSize.getWidth())/2,(int)(screenSize.height-appletSize.getHeight())/2);
            
            aDialog.setDefaultCloseOperation(javax.swing.JDialog.DISPOSE_ON_CLOSE);
            aDialog.show();
        }
        catch(Exception e) {
        }
        return !Cancelled;
    }
    
    private void GenerateCombo() {
        HashMap List=new HashMap();
        String strCondition="";
        
        //----- Generate cmbType ------- //
        cmbDeptModel=new EITLComboModel();
        cmbDept.removeAllItems();
        cmbDept.setModel(cmbDeptModel);
        
        strCondition=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID)+" ";
        
        List=clsDepartment.getList(strCondition);
        for(int i=1;i<=List.size();i++) {
            clsDepartment ObjDept=(clsDepartment) List.get(Integer.toString(i));
            ComboData aData=new ComboData();
            aData.Code=(int) ObjDept.getAttribute("DEPT_ID").getVal();
            aData.Text=(String)ObjDept.getAttribute("DEPT_DESC").getObj();
            aData.strCode="";
            cmbDeptModel.addElement(aData);
        }
        //------------------------------ //
    }
    
    
