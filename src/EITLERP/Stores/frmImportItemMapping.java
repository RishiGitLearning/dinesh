    private void cmdAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAddActionPerformed
        // TODO add your handling code here:
        if(txtFirstItemCode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter import item code");
            return;
        }
        
        if(!clsItem.IsValidItemID(EITLERPGLOBAL.gCompanyID, txtFirstItemCode.getText())) {
            JOptionPane.showMessageDialog(null,"Import Item code you entered is not valid. Please verify");
            return;
        }
        
        if(txtSecondItemCode.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter second item code");
            return;
        }

        if(!clsItem.IsValidItemID(EITLERPGLOBAL.gCompanyID, txtSecondItemCode.getText())) {
            JOptionPane.showMessageDialog(null,"Substitite Item code you entered is not valid. Please verify");
            return;
        }
        
        String strSQL="";
        
        
        clsImportSubMapping ObjItem=new clsImportSubMapping();
        ObjItem.setAttribute("COMPANY_ID",EITLERPGLOBAL.gCompanyID);
        ObjItem.setAttribute("ITEM_ID",txtFirstItemCode.getText());
        ObjItem.setAttribute("SUB_ITEM_ID",txtSecondItemCode.getText().trim());
        
        if(clsImportSubMapping.Insert(ObjItem)) {
            GenerateGrid();
        }
        else {
            JOptionPane.showMessageDialog(null,"Error occured while saving");
        }
        
    }//GEN-LAST:event_cmdAddActionPerformed
    
