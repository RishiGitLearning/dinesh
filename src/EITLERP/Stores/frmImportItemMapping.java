    private void FormatGrid() {
        DataModel=new EITLTableModel();
        
        Table.removeAll();
        
        Table.setModel(DataModel);
        Table.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        
        DataModel.addColumn("Sr.");
        DataModel.addColumn("Import Item ID");
        DataModel.addColumn("Item Name");
        DataModel.addColumn("Substitute Item ID");
        DataModel.addColumn("Item Name");
        
        DataModel.SetNumeric(1,true);
        DataModel.TableReadOnly(true);
    }
    
    
    private void GenerateGrid() {
        try
        {
        HashMap List=new HashMap();
        
        FormatGrid();
        
        ResultSet rsTmp=data.getResult("SELECT * FROM D_COM_IMPORT_SUB_ITEMS");
        rsTmp.first();
        
        if(rsTmp.getRow()>0) {
            while(!rsTmp.isAfterLast()) {
                
                Object[] rowData=new Object[5];
                
                rowData[0]=Integer.toString(UtilFunctions.getInt(rsTmp,"SR_NO",0));
                rowData[1]=UtilFunctions.getString(rsTmp,"ITEM_ID","");
                rowData[2]=clsItem.getItemName(EITLERPGLOBAL.gCompanyID,UtilFunctions.getString(rsTmp,"ITEM_ID",""));
                rowData[3]=UtilFunctions.getString(rsTmp,"SUB_ITEM_ID","");
                rowData[4]=clsItem.getItemName(EITLERPGLOBAL.gCompanyID,UtilFunctions.getString(rsTmp,"SUB_ITEM_ID",""));
                DataModel.addRow(rowData);
                
                rsTmp.next();
            }
        }
        
        }
        catch(Exception e)
        {
            
        }
    }

    private Frame findParentFrame(JApplet pApplet) {
        Container c = (Container) pApplet;
        while(c != null) {
            if (c instanceof Frame)
                return (Frame)c;
            
            c = c.getParent();
        }
        return (Frame)null;
    }
    
    public void ShowList() {
        try {
            
            setSize(499,504);
            
            Frame f=findParentFrame(this);
            aDialog=new JDialog(f,"Import Substitute Items",true);
            
            aDialog.getContentPane().add("Center",this);
            Dimension appletSize = this.getSize();
            aDialog.setSize(appletSize);
            aDialog.setResizable(false);
            
            //Place it to center of the screen
            Dimension screenSize=Toolkit.getDefaultToolkit().getScreenSize();
            aDialog.setLocation((int)(screenSize.width-appletSize.getWidth())/2,(int)(screenSize.height-appletSize.getHeight())/2);
            
            aDialog.setDefaultCloseOperation(javax.swing.JDialog.DISPOSE_ON_CLOSE);
            aDialog.show();
        }
        catch(Exception e) {
        }
        
    }
    
