    private void cmdPreviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdPreviewActionPerformed
        // TODO add your handling code here:
        
        if(!txtFromDate.getText().trim().equals("")) {
            if(!EITLERPGLOBAL.isDate(txtFromDate.getText().trim())) {
                JOptionPane.showMessageDialog(null,"Please enter valid From Date");
                return;
            }
        }
        
        if(!txtToDate.getText().trim().equals("")) {
            if(!EITLERPGLOBAL.isDate(txtToDate.getText().trim())) {
                JOptionPane.showMessageDialog(null,"Please enter valid To Date");
                return;
            }
        }
        
        if(opgRGPReturn.isSelected()) {
            if(txtFromDate.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null,"Please enter from date");
                return;
            }
            
            
            if(txtToDate.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null,"Please enter to date");
                return;
            }
            
            if(chkRGP.isSelected()) {
                if(txtFromRGP.getText().trim().equals("")||txtToRGP.getText().trim().equals("")) {
                    JOptionPane.showMessageDialog(null,"Please specify RGP Document range");
                    return;
                }
            }
        }
        
        String Condition="";
        
        String FromDate=EITLERPGLOBAL.formatDateDB(txtFromDate.getText());
        String ToDate=EITLERPGLOBAL.formatDateDB(txtToDate.getText());
        
        if(opgRGP.isSelected()) {
            
            if(chkRGP.isSelected()) {
                Condition=" AND A.GATEPASS_NO>='"+txtFromRGP.getText().trim()+"' AND A.GATEPASS_NO<='"+txtToRGP.getText().trim()+"' ";
            }
            
            if((!txtFromDate.getText().trim().equals(""))&&(!txtToDate.getText().trim().equals(""))) {
                Condition=Condition+" AND A.GATEPASS_DATE>='"+EITLERPGLOBAL.formatDateDB(txtFromDate.getText().trim())+"' AND A.GATEPASS_DATE<='"+EITLERPGLOBAL.formatDateDB(txtToDate.getText().trim())+"' ";
            }
            
            try {
                URL ReportFile=new URL("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/rptPendingRGP.jsp?dbURL="+EITLERPGLOBAL.DatabaseURL+"&CompanyID="+EITLERPGLOBAL.gCompanyID+"&Condition="+Condition);
                EITLERPGLOBAL.loginContext.showDocument(ReportFile,"_blank");
            }
            catch(Exception e) {
                JOptionPane.showMessageDialog(null,"Error Previwing "+e.getMessage());
            }
            
        }

        
        if(opgRGPDept.isSelected()) {
            
            if(chkRGP.isSelected()) {
                Condition=" AND A.GATEPASS_NO>='"+txtFromRGP.getText().trim()+"' AND A.GATEPASS_NO<='"+txtToRGP.getText().trim()+"' ";
            }
            
            if((!txtFromDate.getText().trim().equals(""))&&(!txtToDate.getText().trim().equals(""))) {
                Condition=Condition+" AND A.GATEPASS_DATE>='"+EITLERPGLOBAL.formatDateDB(txtFromDate.getText().trim())+"' AND A.GATEPASS_DATE<='"+EITLERPGLOBAL.formatDateDB(txtToDate.getText().trim())+"' ";
            }
            
            try {
                URL ReportFile=new URL("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/rptPendingRGPDept.jsp?dbURL="+EITLERPGLOBAL.DatabaseURL+"&CompanyID="+EITLERPGLOBAL.gCompanyID+"&Condition="+Condition);
                EITLERPGLOBAL.loginContext.showDocument(ReportFile,"_blank");
            }
            catch(Exception e) {
                JOptionPane.showMessageDialog(null,"Error Previwing "+e.getMessage());
            }
            
        }

        
        if(opgRGPReturn.isSelected()) {
            
            if(chkRGP.isSelected()) {
                Condition=" AND A.GATEPASS_NO>='"+txtFromRGP.getText().trim()+"' AND A.GATEPASS_NO<='"+txtToRGP.getText().trim()+"' ";
            }
            
            if((!txtFromDate.getText().trim().equals(""))&&(!txtToDate.getText().trim().equals(""))) {
                Condition=Condition+" AND A.GATEPASS_DATE>='"+EITLERPGLOBAL.formatDateDB(txtFromDate.getText().trim())+"' AND A.GATEPASS_DATE<='"+EITLERPGLOBAL.formatDateDB(txtToDate.getText().trim())+"' ";
            }
            
            
            try {
                URL ReportFile=new URL("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/rptRGPReturn.jsp?dbURL="+EITLERPGLOBAL.DatabaseURL+"&CompanyID="+EITLERPGLOBAL.gCompanyID+"&FromDate="+FromDate+"&ToDate="+ToDate+"&Condition="+Condition);
                EITLERPGLOBAL.loginContext.showDocument(ReportFile,"_blank");
            }
            catch(Exception e) {
                JOptionPane.showMessageDialog(null,"Error Previwing "+e.getMessage());
            }
        }
        
        if(opgRGPList.isSelected()) {
            
            if(chkRGP.isSelected()) {
                Condition=" AND A.GATEPASS_NO>='"+txtFromRGP.getText().trim()+"' AND A.GATEPASS_NO<='"+txtToRGP.getText().trim()+"' ";
            }
            
            if((!txtFromDate.getText().trim().equals(""))&&(!txtToDate.getText().trim().equals(""))) {
                Condition=Condition+" AND A.GATEPASS_DATE>='"+EITLERPGLOBAL.formatDateDB(txtFromDate.getText().trim())+"' AND A.GATEPASS_DATE<='"+EITLERPGLOBAL.formatDateDB(txtToDate.getText().trim())+"' ";
            }
            
            try {
                URL ReportFile=new URL("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/rptRGPList.jsp?dbURL="+EITLERPGLOBAL.DatabaseURL+"&CompanyID="+EITLERPGLOBAL.gCompanyID+"&FromDate="+FromDate+"&ToDate="+ToDate+"&Condition="+Condition);
                EITLERPGLOBAL.loginContext.showDocument(ReportFile,"_blank");
            }
            catch(Exception e) {
                JOptionPane.showMessageDialog(null,"Error Previwing "+e.getMessage());
            }
        }
        
    }//GEN-LAST:event_cmdPreviewActionPerformed
    
    
