    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        // TODO add your handling code here:
       Cancelled=false;
      
       String subQuery="";
       
       //strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID)+" AND RETURN_DATE>='"+EITLERPGLOBAL.FinFromDateDB+"' AND RETURN_DATE<='"+EITLERPGLOBAL.FinToDateDB+"' ";
       strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID)+" ";
              
       if(!txtDocNo.getText().equals(""))
       {
         strQuery=strQuery+" AND RETURN_NO='"+txtDocNo.getText()+"' ";
       }
       
       if(!txtDateFrom.getText().equals(""))
       {
         strQuery=strQuery+" AND RETURN_DATE>='"+EITLERPGLOBAL.formatDateDB(txtDateFrom.getText())+"' ";
       }

       if(!txtDateTo.getText().equals(""))
       {
         strQuery=strQuery+" AND RETURN_DATE<='"+EITLERPGLOBAL.formatDateDB(txtDateTo.getText())+"' ";
       }
       
       if(!txtRGPNo.getText().equals(""))
       {
         strQuery=strQuery+" AND RGP_NO='"+txtRGPNo.getText().trim()+"' ";  
       }
       
       
       //====== Sub Query =======//
       if(!txtItemID.getText().trim().equals(""))
       {
        subQuery=subQuery+" AND ITEM_ID='"+txtItemID.getText()+"' ";
       }
       
       if(!txtDesc.getText().trim().equals(""))
       {
        subQuery=subQuery+" AND ITEM_DESC LIKE '%"+txtDesc.getText()+"%' ";   
       }
       
       
       if(!subQuery.trim().equals(""))
       {
        subQuery=" AND RETURN_NO IN (SELECT RETURN_NO FROM D_INV_RGP_RETURN_DETAIL WHERE COMPANY_ID="+EITLERPGLOBAL.gCompanyID+" "+subQuery+")";
       }
       
       
       strQuery=strQuery+subQuery;
       //========== End Sub Query ============//
       
       
       getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
