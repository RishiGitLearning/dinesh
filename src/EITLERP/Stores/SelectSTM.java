private void SetList()
{
    int SrNo=0;
    HashMap List=new HashMap();
    colSelItems.clear();

    if(STMType==2)
    {
       clsSTMGen tmpObj=new clsSTMGen();
       ObjSTMGen=(clsSTMGen)tmpObj.getObject(EITLERPGLOBAL.gCompanyID, txtSTMNo.getText());
    }
    else
    {
       clsSTMRaw tmpObj=new clsSTMRaw();
       ObjSTMRaw=(clsSTMRaw)tmpObj.getObject(EITLERPGLOBAL.gCompanyID,txtSTMNo.getText());
    }
    
    CopyHeader=chkCopyHeader.isSelected();
    
    List=clsSTMRaw.getSTMItemList(EITLERPGLOBAL.gCompanyID, SelSTMNo,false,STMType);    
    //Scan the Table to get selected items
    for(int i=1;i<=List.size();i++)
    {
       clsSTMRawItem ObjItem=(clsSTMRawItem)List.get(Integer.toString(i));
       SrNo=(int)ObjItem.getAttribute("SR_NO").getVal();
       
        //Search in the table for SrNo.
       for(int j=0;j<Table.getRowCount();j++)
       {
           if(Integer.parseInt(Table.getValueAt(j, 1).toString())==SrNo) 
           {
             if(Table.getValueAt(j,0).toString().equals("true"))  
             {
               //Selected Item  
               colSelItems.put(Integer.toString(colSelItems.size()+1),ObjItem);
             }
           }
       }
    }
}

private void GenerateGrid()
{
    HashMap List=new HashMap();
    String STMNo=txtSTMNo.getText();
    
    if(!STMNo.equals(""))
    {
       List=clsSTMRaw.getSTMItemList(EITLERPGLOBAL.gCompanyID, STMNo,false,STMType);
       
       for(int i=1;i<=List.size();i++)
       {
           clsSTMRawItem ObjItem=(clsSTMRawItem)List.get(Integer.toString(i));
           HashMap colLot=ObjItem.colSTMLot;
           Object[] rowData=new Object[28];
           
           rowData[0]=Boolean.valueOf(true); //By default not selected
           rowData[1]=Integer.toString((int)ObjItem.getAttribute("SR_NO").getVal());
           rowData[2]=(String)ObjItem.getAttribute("ITEM_ID").getObj();
           rowData[3]=clsItem.getItemName(EITLERPGLOBAL.gCompanyID,(String)ObjItem.getAttribute("ITEM_ID").getObj());
           rowData[4]=(String)ObjItem.getAttribute("BOE_NO").getObj();
           rowData[5]=(String)ObjItem.getAttribute("BOE_SR_NO").getObj();
           rowData[6]=EITLERPGLOBAL.formatDate((String)ObjItem.getAttribute("BOE_DATE").getObj());
           rowData[7]=Double.toString(ObjItem.getAttribute("QTY").getVal());
           rowData[8]=clsParameter.getParaDescription(EITLERPGLOBAL.gCompanyID, "UNIT", (int)ObjItem.getAttribute("UNIT").getVal());
           rowData[9]=Double.toString(ObjItem.getAttribute("RATE").getVal());
           rowData[10]=Double.toString(ObjItem.getAttribute("TOTAL_AMOUNT").getVal());
           rowData[11]=Double.toString(ObjItem.getAttribute("NET_AMOUNT").getVal());
           rowData[12]="";
           rowData[13]="";
           rowData[14]="";
           rowData[15]=(String)ObjItem.getAttribute("STM_DESC").getObj();
           rowData[16]="0";
           rowData[17]="";
           rowData[18]="";
           rowData[19]="";
           rowData[20]="N";    
           rowData[21]=(String)ObjItem.getAttribute("INDENT_NO").getObj();
           rowData[22]=Integer.toString((int)ObjItem.getAttribute("INDENT_SR_NO").getVal());
           rowData[23]=(String)ObjItem.getAttribute("CENTER_CODE_NO").getObj();
           rowData[24]="";
           rowData[25]=(String)ObjItem.getAttribute("REMARKS").getObj();
           rowData[26]=(String)ObjItem.getAttribute("WAREHOUSE_ID").getObj();
           rowData[27]=(String)ObjItem.getAttribute("LOCATION_ID").getObj();
           
           DataModel.addRow(rowData);
           
           //Set the Collection
           DataModel.SetUserObject(Table.getRowCount()-1, colLot);
       }
    }
    
}

    //Recurses through the hierarchy of classes
    //until it finds Frame
    private Frame findParentFrame(JApplet pApplet) {
        Container c = (Container) pApplet;
        while(c != null) {
            if (c instanceof Frame)
                return (Frame)c;
            
            c = c.getParent();
        }
        return (Frame)null;
    }

public boolean ShowList() {
        try {
            FormatGrid();

            setSize(700,410);
            
            Frame f=findParentFrame(this);
            
            aDialog=new JDialog(f,"Select STM Items",true);
            
            aDialog.getContentPane().add("Center",this);
            Dimension appletSize = this.getSize();
            aDialog.setSize(appletSize);
            aDialog.setResizable(false);
            
            //Place it to center of the screen
            Dimension screenSize=Toolkit.getDefaultToolkit().getScreenSize();
            aDialog.setLocation((int)(screenSize.width-appletSize.getWidth())/2,(int)(screenSize.height -appletSize.getHeight())/2);
            
            aDialog.setDefaultCloseOperation(javax.swing.JDialog.DISPOSE_ON_CLOSE);
            aDialog.show();
        }
        catch(Exception e) {
        }
        return !Cancelled;
    }    

