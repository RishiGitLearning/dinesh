    private boolean Validate() {
        //Form level validations
        if(txtOutwardDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter Outward date");
            return false;
        } else if(!EITLERPGLOBAL.isDate(txtOutwardDate.getText())) {
            JOptionPane.showMessageDialog(null,"Invalid Outward Date in DD/MM/YYYY format.");
            return false;
        }
        
        if (txtGatepassNo.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter Gatepass No.");
            return false;
        }
        else {
            if(EditMode==EITLERPGLOBAL.ADD) {
                String str = "";
                if (SelPrefix.trim().equals("RO")) {
                    str = "SELECT * FROM D_INV_GATE_OUTWARD_HEADER  WHERE GATEPASS_NO='"+ txtGatepassNo.getText().trim() +"' AND RGP_NRGP='R'";
                }
                else if (SelPrefix.trim().equals("NO")) {
                    str = "SELECT * FROM D_INV_GATE_OUTWARD_HEADER  WHERE GATEPASS_NO='"+ txtGatepassNo.getText().trim() +"' AND RGP_NRGP='N'";
                }
                boolean chk = data.IsRecordExist(str);
                if (chk == true) {
                    JOptionPane.showMessageDialog(null,"Material outward entry is already done with "+ txtGatepassNo.getText().trim() );
                    return false;
                }
            }
        }
        
        if(txtGateOutwardDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter Gatepass Outward date");
            return false;
        } else if(!EITLERPGLOBAL.isDate(txtGateOutwardDate.getText())) {
            JOptionPane.showMessageDialog(null,"Invalid Gatepass Outward Date in DD/MM/YYYY format.");
            return false;
        }
        
        if(cmbHierarchy.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(null,"Please select the hierarchy.");
            return false;
        }
        
        if((!OpgApprove.isSelected())&&(!OpgReject.isSelected())&&(!OpgFinal.isSelected())&&(!OpgHold.isSelected())) {
            JOptionPane.showMessageDialog(null,"Please select the Approval Action");
            return false;
        }
        
        if(OpgReject.isSelected()&&txtToRemarks.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter the remarks for rejection");
            return false;
        }
        
        if( (OpgApprove.isSelected()||OpgReject.isSelected())&&cmbSendTo.getItemCount()<=0) {
            JOptionPane.showMessageDialog(null,"Please select the user, to whom rejected document to be send");
            return false;
        }
        
        return true;
    }
    
