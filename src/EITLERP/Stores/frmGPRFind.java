    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        // TODO add your handling code here:
       Cancelled=false;
       String subQuery="";
      
       //strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID)+" AND GATEPASS_REQ_DATE>='"+EITLERPGLOBAL.FinFromDateDB+"' AND GATEPASS_REQ_DATE<='"+EITLERPGLOBAL.FinToDateDB+"' ";
       strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID);
       
       if(!txtDocNo.getText().equals(""))
       {
         strQuery=strQuery+" AND GATEPASS_REQ_NO='"+txtDocNo.getText()+"' ";
       }
       
       if(!txtDateFrom.getText().equals(""))
       {
         strQuery=strQuery+" AND GATEPASS_REQ_DATE>='"+EITLERPGLOBAL.formatDateDB(txtDateFrom.getText())+"' ";
       }

       if(!txtDateTo.getText().equals(""))
       {
         strQuery=strQuery+" AND GATEPASS_REQ_DATE<='"+EITLERPGLOBAL.formatDateDB(txtDateTo.getText())+"' ";
       }
       
       if(cmbType.getSelectedIndex()!=-1)
       {
         strQuery=strQuery+" AND GATEPASS_TYPE='"+EITLERPGLOBAL.getCombostrCode(cmbType)+"' ";
       }
       
       if(cmbForDept.getSelectedIndex()!=-1)
       {
         strQuery=strQuery+" AND DEST_DEPT_ID="+EITLERPGLOBAL.getComboCode(cmbForDept)+" ";
       }
       
       if(!txtRequiredOn.getText().equals(""))
       {
         strQuery=strQuery+" AND REQUIRED_DATE="+EITLERPGLOBAL.formatDateDB(txtRequiredOn.getText())+" ";
       }

       
       //====== Sub Query =======//
       if(!txtItemID.getText().trim().equals(""))
       {
        subQuery=subQuery+" AND ITEM_CODE='"+txtItemID.getText()+"' ";
       }
       
       if(!txtDesc.getText().trim().equals(""))
       {
        subQuery=subQuery+" AND ITEM_EXTRA_DESC LIKE '%"+txtDesc.getText()+"%' ";   
       }
       
       if(!subQuery.trim().equals(""))
       {
        subQuery=" AND GATEPASS_REQ_NO IN (SELECT GATEPASS_REQ_NO FROM D_INV_GATEPASS_REQ_DETAIL WHERE COMPANY_ID="+EITLERPGLOBAL.gCompanyID+" "+subQuery+")";
       }
       
       strQuery=strQuery+subQuery;
       //========== End Sub Query ============//
       
       
       getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
