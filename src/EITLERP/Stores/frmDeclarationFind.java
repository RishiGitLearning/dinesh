    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        // TODO add your handling code here:
       Cancelled=false;
      
       strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID); 
       
       if(!txtDecID.getText().equals(""))
       {
         strQuery=strQuery+" AND DECLARATION_ID='"+txtDecID.getText()+"' ";
       }
       
       if(!txtDateFrom.getText().equals(""))
       {
         strQuery=strQuery+" AND DECLARATION_DATE>='"+EITLERPGLOBAL.formatDateDB(txtDateFrom.getText())+"' ";
       }

       if(!txtDateTo.getText().equals(""))
       {
         strQuery=strQuery+" AND DECLARATION_DATE<='"+EITLERPGLOBAL.formatDateDB(txtDateTo.getText())+"' ";
       }
       
        if(!txtContractor.getText().trim().equals(""))
       {
        strQuery=strQuery+" AND CONTRACTOR_NAME='"+txtContractor.getText()+"' ";
       }
       
       if(!txtDeptID.getText().trim().equals(""))
       {
        strQuery=strQuery+" AND FOR_DEPT_ID='"+txtDeptID.getText()+"' ";
       }
       
       getParent().getParent().getParent().getParent().show(false);
   
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
