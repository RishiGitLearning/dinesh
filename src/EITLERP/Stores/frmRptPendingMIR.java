    private void cmdPreviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdPreviewActionPerformed
        // TODO add your handling code here:
        String Condition="";
        int Level=EITLERPGLOBAL.getComboCode(cmbLevel);
        int Document=EITLERPGLOBAL.getComboCode(cmbDocument);
        if(txtFromDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter from date");
            return;
        }
        if(txtToDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter to date");
            return;
        }
        
        String FromDate=EITLERPGLOBAL.formatDateDB(txtFromDate.getText());
        String ToDate=EITLERPGLOBAL.formatDateDB(txtToDate.getText());
        
        if(Level==2) {
            Condition=" AND E.SR_NO=1 ";
        }
        
        if(Level==3) {
            Condition=" AND E.SR_NO>1 AND UD.DEPT_ID<>2 ";
        }
        
        if(Level==4) {
            Condition=" AND E.SR_NO>1 AND UD.DEPT_ID=2 ";
        }
        
        
        if(opgSimple.isSelected()) {
            if(Document==1) {
                try {
                    URL ReportFile=new URL("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/rptPendingMIR.jsp?dbURL="+EITLERPGLOBAL.DatabaseURL+"&CompanyID="+EITLERPGLOBAL.gCompanyID+"&FromDate="+FromDate+"&ToDate="+ToDate+"&Condition="+Condition);
                    EITLERPGLOBAL.loginContext.showDocument(ReportFile,"_blank");
                }
                catch(Exception e) {
                    JOptionPane.showMessageDialog(null,"Error Previwing "+e.getMessage());
                }
            }
            if(Document==2) {
                JOptionPane.showMessageDialog(null,"There is no simple Report for Job work.");
            }
        }
        
        
        if(opgLevelwise.isSelected()) {
            
            if(Document==1) {
                try {
                    URL ReportFile=new URL("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/rptPendingMIRLevel.jsp?dbURL="+EITLERPGLOBAL.DatabaseURL+"&CompanyID="+EITLERPGLOBAL.gCompanyID+"&FromDate="+FromDate+"&ToDate="+ToDate+"&Condition="+Condition);
                    EITLERPGLOBAL.loginContext.showDocument(ReportFile,"_blank");
                }
                catch(Exception e) {
                    JOptionPane.showMessageDialog(null,"Error Previwing "+e.getMessage());
                }
            }
            if(Document==2) {
                try {
                    URL ReportFile=new URL("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/rptPendingJobLevel.jsp?dbURL="+EITLERPGLOBAL.DatabaseURL+"&CompanyID="+EITLERPGLOBAL.gCompanyID+"&FromDate="+FromDate+"&ToDate="+ToDate+"&Condition="+Condition);
                    EITLERPGLOBAL.loginContext.showDocument(ReportFile,"_blank");
                }
                catch(Exception e) {
                    JOptionPane.showMessageDialog(null,"Error Previwing "+e.getMessage());
                }
                
            }
        }
        
    }//GEN-LAST:event_cmdPreviewActionPerformed
    
