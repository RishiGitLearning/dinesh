    //Recurses through the hierarchy of classes
    //until it finds Frame
    private Frame findParentFrame(JApplet pApplet) {
        Container c = (Container) pApplet;
        while(c != null) {
            if (c instanceof Frame)
                return (Frame)c;
            
            c = c.getParent();
        }
        return (Frame)null;
    }
    
    private void GenerateCombo() {
        cmbMethodModel=new EITLComboModel();
        cmbMethod.removeAllItems();
        cmbMethod.setModel(cmbMethodModel);
        
        ComboData aData=new ComboData();
        aData.Code=1;
        aData.Text="FIFO";
        cmbMethodModel.addElement(aData);
        
        aData=new ComboData();
        aData.Code=2;
        aData.Text="LIFO";
        cmbMethodModel.addElement(aData);
        
    }
    
