    private void txtCodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodeKeyPressed
        // Add your handling code here:
        
        try {
            if(evt.getKeyCode()==10) //Enter key pressed
            {
                if(txtCode.getText().length()<14) {
                    Cancelled=true;
                }
                else {
                    Cancelled=false;
                    ReturnVal=(String)txtCode.getText().trim();
                }
                aDialog.dispose();
                return;
            }
            
            if(evt.getKeyCode()==27) //Escape key pressed
            {
                Cancelled=true;
                ReturnVal="";
                aDialog.dispose();
                return;
            }
            
            if(evt.getKeyCode()==112) //F1 Key pressed
            {
                if(txtCode.getText().trim().equals(""))
                {
                   JOptionPane.showMessageDialog(null,"Please enter item code first");
                   return;
                }
                
                
                LOV aList=new LOV();

                if(EITLERPGLOBAL.getComboCode(cmbMethod)==1)
                {
                aList.SQL="SELECT A.MIR_NO,DATE_FORMAT(A.MIR_DATE,'%d/%m/%Y') ,B.QTY FROM D_INV_MIR_HEADER A,D_INV_MIR_DETAIL B WHERE A.COMPANY_ID=B.COMPANY_ID AND A.MIR_NO=B.MIR_NO AND A.MIR_TYPE=B.MIR_TYPE AND A.APPROVED=1 AND A.CANCELLED=0 AND B.ISSUED_QTY<B.QTY AND B.ITEM_ID='"+txtCode.getText()+"' ORDER BY A.MIR_DATE DESC";
                }
                else
                {
                aList.SQL="SELECT A.MIR_NO,DATE_FORMAT(A.MIR_DATE,'%d/%m/%Y') ,B.QTY FROM D_INV_MIR_HEADER A,D_INV_MIR_DETAIL B WHERE A.COMPANY_ID=B.COMPANY_ID AND A.MIR_NO=B.MIR_NO AND A.MIR_TYPE=B.MIR_TYPE AND A.APPROVED=1 AND A.CANCELLED=0 AND B.ISSUED_QTY<B.QTY AND B.ITEM_ID='"+txtCode.getText()+"' ORDER BY A.MIR_DATE";
                }
               
                aList.ReturnCol=1;
                aList.ShowReturnCol=true;
                aList.DefaultSearchOn=1;
                
                if(aList.ShowLOV()) {
                    txtCode.setText(txtCode.getText()+aList.ReturnVal);
                }
                
            }
            
        }
        catch(Exception e)
        {}
    }//GEN-LAST:event_txtCodeKeyPressed
    
