    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        // TODO add your handling code here:
       Cancelled=false;

       String subQuery="";
       
       //strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID)+" AND GATEPASS_DATE>='"+EITLERPGLOBAL.FinFromDateDB+"' AND GATEPASS_DATE<='"+EITLERPGLOBAL.FinToDateDB+"' ";
       strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID)+" ";
       
       if(!txtDocNo.getText().equals(""))
       {
         strQuery=strQuery+" AND GATEPASS_NO='"+txtDocNo.getText()+"' ";
       }
       
       if(!txtDateFrom.getText().equals(""))
       {
         strQuery=strQuery+" AND GATEPASS_DATE>='"+EITLERPGLOBAL.formatDateDB(txtDateFrom.getText())+"' ";
       }

       if(!txtDateTo.getText().equals(""))
       {
         strQuery=strQuery+" AND GATEPASS_DATE<='"+EITLERPGLOBAL.formatDateDB(txtDateTo.getText())+"' ";
       }
       
       
       if(cmbForDept.getSelectedIndex()>=0)
       {
           strQuery=strQuery+" AND FOR_DEPT="+EITLERPGLOBAL.getComboCode(cmbForDept)+" ";  
       }
       
       if(cmbBuyer.getSelectedIndex()>=0)
       {
          strQuery=strQuery+" AND USER_ID="+EITLERPGLOBAL.getComboCode(cmbBuyer)+" ";   
       }
       
       if(!txtParty.getText().trim().equals(""))
       {
          strQuery=strQuery+" AND PARTY_NAME LIKE '%"+txtParty.getText().trim()+"%' ";    
       }
       
       if(!txtCity.getText().trim().equals(""))
       {
         strQuery=strQuery+" AND CITY LIKE '%"+txtCity.getText().trim()+"%' ";      
       }
       
       //====== Sub Query =======//
       if(!txtItemID.getText().trim().equals(""))
       {
        subQuery=subQuery+" AND ITEM_CODE='"+txtItemID.getText()+"' ";
       }
       
       if(!txtItemDesc.getText().trim().equals(""))
       {
        subQuery=subQuery+" AND RGP_DESC LIKE '%"+txtItemDesc.getText()+"%' ";   
       }
       
       
       if(!txtGatepassReqNo.getText().trim().equals(""))
       {
        subQuery=subQuery+" AND GATEPASSREQ_NO='"+txtGatepassReqNo.getText()+"' ";   
       }
       

       if(!txtDecID.getText().trim().equals(""))
       {
        subQuery=subQuery+" AND DECLARATION_ID='"+txtDecID.getText()+"' ";   
       }
       
       if(!subQuery.trim().equals(""))
       {
        subQuery=" AND GATEPASS_NO IN (SELECT GATEPASS_NO FROM D_INV_RGP_DETAIL WHERE COMPANY_ID="+EITLERPGLOBAL.gCompanyID+" "+subQuery+")";
       }
       
       
       strQuery=strQuery+subQuery;
       //========== End Sub Query ============//
       
       
       getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
