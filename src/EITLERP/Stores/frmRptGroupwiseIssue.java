    private void GenerateCombo() {
        try {
            
            cmbItemTypeModel=new EITLComboModel();
            cmbItemType.removeAll();
            cmbItemType.setModel(cmbItemTypeModel);
            
            ComboData objData=new ComboData();
            objData.Code=1;
            objData.Text="General";
            
            cmbItemTypeModel.addElement(objData);
            
            objData=new ComboData();
            objData.Code=2;
            objData.Text="Raw Material";
            
            cmbItemTypeModel.addElement(objData);
            
            objData=new ComboData();
            objData.Code=3;
            objData.Text="Raw Material STM";
            
            cmbItemTypeModel.addElement(objData);
            
        }
        catch(Exception e) {
            
        }
    }
    
    
    private void cmdPreviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdPreviewActionPerformed
        // TODO add your handling code here:
        
        if(txtFromDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter from date");
            return;
        }
        
        if(txtToDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter to date");
            return;
        }
        
        String FromDate=EITLERPGLOBAL.formatDateDB(txtFromDate.getText());
        String ToDate=EITLERPGLOBAL.formatDateDB(txtToDate.getText());
        
        int ItemType=EITLERPGLOBAL.getComboCode(cmbItemType);
        
        try{
            DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            Date fromDate = df.parse(txtFromDate.getText());
            Date RestrictDate = df.parse("01/10/2009");
            
            if(ItemType!=1 && fromDate.before(RestrictDate)) {
                JOptionPane.showMessageDialog(null,"From Date must be equals or after 01/10/2009");
                return;
            }
        }catch(Exception e) {
            
        }
        
        try {
            DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            Date fromDate = df.parse(txtFromDate.getText());
            Date RestrictDate = df.parse("01/10/2009");
            
            
            if(ItemType!=1 && fromDate.before(RestrictDate)) {
                JOptionPane.showMessageDialog(null,"From Date must be equals or after 01/10/2009");
                return;
            }
        }
        catch(Exception e) {
            
        }
        
        
        if(ItemType==1) {
            try {
                URL ReportFile=new URL("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/rptGroupwiseIssue.jsp?dbURL="+EITLERPGLOBAL.DatabaseURL+"&CompanyID="+EITLERPGLOBAL.gCompanyID+"&FromDate="+FromDate+"&ToDate="+ToDate);
                EITLERPGLOBAL.loginContext.showDocument(ReportFile,"_blank");
            }
            catch(Exception e) {
                JOptionPane.showMessageDialog(null,"Error Previwing "+e.getMessage());
            }
        }
        if(ItemType==2) {
            try {
                URL ReportFile=new URL("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/rptGroupwiseIssueRM.jsp?dbURL="+EITLERPGLOBAL.DatabaseURL+"&CompanyID="+EITLERPGLOBAL.gCompanyID+"&FromDate="+FromDate+"&ToDate="+ToDate);
                EITLERPGLOBAL.loginContext.showDocument(ReportFile,"_blank");
            }
            catch(Exception e) {
                JOptionPane.showMessageDialog(null,"Error Previwing "+e.getMessage());
            }
        }
        if(ItemType==3) {
            try {
                URL ReportFile=new URL("http://"+EITLERPGLOBAL.HostIP+"/EITLERP/Reports/rptGroupwiseIssueRMSTM.jsp?dbURL="+EITLERPGLOBAL.DatabaseURL+"&CompanyID="+EITLERPGLOBAL.gCompanyID+"&FromDate="+FromDate+"&ToDate="+ToDate);
                EITLERPGLOBAL.loginContext.showDocument(ReportFile,"_blank");
            }
            catch(Exception e) {
                JOptionPane.showMessageDialog(null,"Error Previwing "+e.getMessage());
            }
        }
    }//GEN-LAST:event_cmdPreviewActionPerformed
    
    
