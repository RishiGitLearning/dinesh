    private void FormatGrid() {
        try {
            
            DataModel=new EITLTableModel();
            Table.removeAll();
            
            
            Table.setModel(DataModel);
            Table.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            
            DataModel.addColumn("Sr."); //0 - Read Only
            DataModel.addColumn("Job Name"); //1
            
            DataModel.SetVariable(0, "SR_NO");
            DataModel.SetVariable(1,"JOB_NAME");
            
            DataModel.TableReadOnly(true);
        }
        catch(Exception e) {
            
        }
    }
    
    
    private void GenerateGrid() {
        try {
            
            
            FormatGrid();
            
            ResultSet rsTmp=data.getResult("SELECT * FROM D_COM_SCHEDULE_JOBS WHERE COMPANY_ID="+EITLERPGLOBAL.gCompanyID);
            rsTmp.first();
            
            if(rsTmp.getRow()>0) {
                while(!rsTmp.isAfterLast()) {
                    Object[] rowData=new Object[2];
                    rowData[0]=Integer.toString(UtilFunctions.getInt(rsTmp,"SR_NO", 0));
                    rowData[1]=UtilFunctions.getString(rsTmp,"JOB_NAME","");
                    
                    DataModel.addRow(rowData);
                    
                    rsTmp.next();
                }
            }
            
            
        }
        catch(Exception e) {
            
        }
    }
    
