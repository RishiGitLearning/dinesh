 private void Save() 
   {
      if(txtAdd1.getText().trim().equals(""))
      {
       JOptionPane.showMessageDialog(null,"Please enter address");
       return;
      }
      
      if(txtCity.getText().trim().equals(""))
      {
       JOptionPane.showMessageDialog(null,"Please enter city");
       return;
      }
       SetData();
      
      if(EditMode==EITLERPGLOBAL.ADD)
      {
          if(ObjShipping.Insert())
          {
          }
          else
          {
            JOptionPane.showMessageDialog(null,"Error occured while saving. "+ObjShipping.LastError);
            return;
          }
      }
      
      if(EditMode==EITLERPGLOBAL.EDIT)
      {
          if(ObjShipping.Update())
          {
          }
          else
          {
            JOptionPane.showMessageDialog(null,"Error occured while saving. "+ObjShipping.LastError);
            return;
          }
      }
      
      EditMode=0;
      DisplayData();
      SetFields(false);
      EnableToolbar();
      ShowMessage("Ready ...........");
 }
