 private void Delete()
    {
       if(JOptionPane.showConfirmDialog(null,"Are you sure you want to delete ?","SDML ERP",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION)
       {
           if(ObjShipping.Delete())
           {
             ObjShipping.MoveLast();
             DisplayData();
           }
           else
           {
            JOptionPane.showMessageDialog(null,"Error while deleting record. Error is"+ObjShipping.LastError);
           }
       }
    }
