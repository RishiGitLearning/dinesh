    private void DisplayData() {
       txtShipID.setText(Integer.toString((int)ObjShipping.getAttribute("SHIP_ID").getVal()));
       txtAdd1.setText((String)ObjShipping.getAttribute("ADD1").getObj());
       txtAdd2.setText((String)ObjShipping.getAttribute("ADD2").getObj());
       txtAdd3.setText((String)ObjShipping.getAttribute("ADD3").getObj());
       txtCity.setText((String)ObjShipping.getAttribute("CITY").getObj());
       txtPincode.setText((String)ObjShipping.getAttribute("PINCODE").getObj());
       txtState.setText((String)ObjShipping.getAttribute("STATE").getObj());
       txtCountry.setText((String)ObjShipping.getAttribute("COUNTRY").getObj());
    }
    
     //Sets data to the Class Object
    private void SetData() {
        ObjShipping.setAttribute("COMPANY_ID",EITLERPGLOBAL.gCompanyID);        
        ObjShipping.setAttribute("ADD1",txtAdd1.getText());
        ObjShipping.setAttribute("ADD2",txtAdd2.getText());
        ObjShipping.setAttribute("ADD3",txtAdd3.getText());
        ObjShipping.setAttribute("CITY",txtCity.getText());
        ObjShipping.setAttribute("PINCODE",txtPincode.getText());
        ObjShipping.setAttribute("STATE",txtState.getText());
        ObjShipping.setAttribute("COUNTRY",txtCountry.getText());
        if(EditMode==EITLERPGLOBAL.ADD)
        {
          ObjShipping.setAttribute("CREATED_BY",EITLERPGLOBAL.gUserID);
          ObjShipping.setAttribute("CREATED_DATE",EITLERPGLOBAL.getCurrentDateDB());
        }
        else
        {
          ObjShipping.setAttribute("MODIFIED_BY",EITLERPGLOBAL.gUserID);
          ObjShipping.setAttribute("MODIFIED_DATE",EITLERPGLOBAL.getCurrentDateDB());
        }
    }

