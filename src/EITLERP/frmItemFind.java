    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        // TODO add your handling code here:
        Cancelled=false;
        
        strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID);
        
        if(!txtItemID.getText().equals("")) {
            strQuery=strQuery+" AND ITEM_ID LIKE '"+txtItemID.getText()+"%' ";
        }
        
        if(!txtName.getText().equals("")) {
            strQuery=strQuery+" AND ITEM_DESCRIPTION LIKE '%"+txtName.getText()+"%' ";
        }
        
        if(cmbCategory.getSelectedIndex()!=-1) {
            strQuery=strQuery+" AND CATEGORY_ID="+EITLERPGLOBAL.getComboCode(cmbCategory) +" ";
        }
        
        if(cmbGroupCode.getSelectedIndex()!=-1) {
            strQuery=strQuery+" AND GROUP_CODE="+EITLERPGLOBAL.getComboCode(cmbGroupCode) +" ";
        }
        
        if(cmbWareHouse.getSelectedIndex()!=-1) {
            strQuery=strQuery+" AND WAREHOUSE_ID='"+EITLERPGLOBAL.getCombostrCode(cmbWareHouse) +"' ";
        }
        
        if(cmbLocation.getSelectedIndex()!=-1) {
            strQuery=strQuery+" AND LOCATION_ID='"+EITLERPGLOBAL.getCombostrCode(cmbLocation) +"' ";
        }

        if(cmbUnit.getSelectedIndex()!=-1) {
            strQuery=strQuery+" AND UNIT="+EITLERPGLOBAL.getComboCode(cmbUnit) +" ";
        }
        
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
