    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        // TODO add your handling code here:
       Cancelled=false;
      
       strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID);
       
       if(cmbModule.getSelectedIndex()!=-1 )
       {
         strQuery=strQuery+" AND MODULE_ID= "+Integer.toString(EITLERPGLOBAL.getComboCode(cmbModule))+" ";
       }
       
       if(!txtCaption.getText().equals(""))
       {
           strQuery=strQuery+" AND  CAPTION LIKE '"+txtCaption.getText()+"%' ";
       }
       
       if(cmbOperation.getSelectedIndex()!=-1)
       {
          strQuery=strQuery+" AND  OPERATION = '"+EITLERPGLOBAL.getCombostrCode(cmbOperation)+"' ";  
       }
       
       if(cmbInput.getSelectedIndex()!=-1)
       {
         strQuery=strQuery+" AND INPUT = "+Integer.toString(EITLERPGLOBAL.getComboCode(cmbInput))+"' ";    
       }
       
       if(chkCalculate.isSelected())
       {
         strQuery=strQuery+" AND NO_CALCULATION=1";
       }
       
       if(chkUsePercent.isSelected())
       {
         strQuery=strQuery+" AND USE_PERCENT=1";  
       }
       
       getParent().getParent().getParent().getParent().show(false);
        
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
