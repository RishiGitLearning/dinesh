    private void FormatGrid() {
        DataModel=new EITLTableModel();
        
        Table.removeAll();
        
        Table.setModel(DataModel);
        Table.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        
        DataModel.addColumn("Sr.");
        DataModel.addColumn("PO No.");
        DataModel.addColumn("Date");
        DataModel.addColumn("Supplier Code");
        DataModel.addColumn("Supplier Name");
        DataModel.addColumn("Net Amount");
        DataModel.addColumn("Excise Amount");
        DataModel.addColumn("Excise Term");
        DataModel.addColumn("PO Type");
        
        DataModel.SetNumeric(1,true);
        
        
        DataModel.TableReadOnly(true);
        
    }
    
    
    private void GenerateGrid() {
        try {
            if(txtFromDate.getText().equals("")) {
                JOptionPane.showMessageDialog(null,"Please enter from date");
                return;
            }
            
            if(txtToDate.getText().equals("")) {
                JOptionPane.showMessageDialog(null,"Please enter to date");
                return;
            }
            
            if(!EITLERPGLOBAL.isDate(txtFromDate.getText())) {
                JOptionPane.showMessageDialog(null,"Invalid from date");
                return;
            }
            
            if(!EITLERPGLOBAL.isDate(txtToDate.getText())) {
                JOptionPane.showMessageDialog(null,"Invalid To date");
                return;
            }
            
            FormatGrid();
            
            String strSQL="";
            
            strSQL="SELECT A.PO_NO,DATE_FORMAT(A.PO_DATE,'%d/%m/%Y') AS PO_DATE,IF(SUM(B.COLUMN_3_AMT) IS NULL,0,SUM(B.COLUMN_3_AMT)) AS D_EXCISE,IF(SUM(A.COLUMN_3_AMT) IS NULL,0,SUM(A.COLUMN_3_AMT)) AS H_EXCISE,A.EXCISE_TERM,A.SUPP_ID,A.NET_AMOUNT, ";
            strSQL=strSQL+" (IF(SUM(B.COLUMN_3_AMT) IS NULL,0,SUM(B.COLUMN_3_AMT))+IF(SUM(A.COLUMN_3_AMT) IS NULL,0,SUM(A.COLUMN_3_AMT))) AS EXCISE_AMOUNT,A.PO_TYPE ";
            strSQL=strSQL+" FROM ";
            strSQL=strSQL+" D_PUR_PO_HEADER A, ";
            strSQL=strSQL+" D_PUR_PO_DETAIL B ";
            strSQL=strSQL+" WHERE ";
            strSQL=strSQL+" A.COMPANY_ID=B.COMPANY_ID AND ";
            strSQL=strSQL+" A.PO_NO=B.PO_NO AND ";
            strSQL=strSQL+" A.PO_TYPE=B.PO_TYPE AND ";
            strSQL=strSQL+" A.APPROVED=1 AND A.CANCELLED=0 AND ";
            strSQL=strSQL+" A.PO_DATE>='"+EITLERPGLOBAL.formatDateDB(txtFromDate.getText())+"' AND A.PO_DATE<='"+EITLERPGLOBAL.formatDateDB(txtToDate.getText())+"' ";
            strSQL=strSQL+" GROUP BY A.PO_NO ";
            strSQL=strSQL+" HAVING (D_EXCISE+H_EXCISE)>0 OR (A.EXCISE_TERM<>'N/A' AND A.EXCISE_TERM IS NOT NULL AND TRIM(A.EXCISE_TERM)<>'') ";
            
            ResultSet rsTmp=data.getResult(strSQL);
            rsTmp.first();
            
            if(rsTmp.getRow()>0) {
                while(!rsTmp.isAfterLast()) {
                    
                    Object[] rowData=new Object[9];
                    rowData[0]=Integer.toString(rsTmp.getRow());
                    rowData[1]=rsTmp.getString("PO_NO");
                    rowData[2]=rsTmp.getString("PO_DATE");
                    rowData[3]=rsTmp.getString("SUPP_ID");
                    rowData[4]=clsSupplier.getSupplierName(EITLERPGLOBAL.gCompanyID,rsTmp.getString("SUPP_ID"));
                    rowData[5]=Double.toString(rsTmp.getDouble("NET_AMOUNT"));
                    rowData[6]=Double.toString(rsTmp.getDouble("EXCISE_AMOUNT"));
                    rowData[7]=rsTmp.getString("EXCISE_TERM");
                    rowData[8]=Integer.toString(rsTmp.getInt("PO_TYPE"));
                    
                    DataModel.addRow(rowData);
                    
                    rsTmp.next();
                }
                
            }
            
            
        }
        catch(Exception e) {
            
        }
    }
