    //Didplay data on the Screen
    private void DisplayData() {
        
        //=========== Color Indication ===============//
        try {
            if(EditMode==0) {
                if(ObjInquiry.getAttribute("APPROVED").getInt()==1) {
                    lblTitle.setBackground(Color.BLUE);
                }
                
                if(ObjInquiry.getAttribute("APPROVED").getInt()!=1) {
                    lblTitle.setBackground(Color.GRAY);
                }
                
                if(ObjInquiry.getAttribute("CANCELLED").getInt()==1) {
                    lblTitle.setBackground(Color.RED);
                }
                
                
            }
        }
        catch(Exception c) {
            
        }
        //============================================//
        
        
        //========= Authority Delegation Check =====================//
        if(EITLERPGLOBAL.gAuthorityUserID!=EITLERPGLOBAL.gUserID) {
            int ModuleID=18;
            
            if(clsAuthority.IsAuthorityGiven(EITLERPGLOBAL.gCompanyID,EITLERPGLOBAL.gUserID,EITLERPGLOBAL.gAuthorityUserID,ModuleID)) {
                EITLERPGLOBAL.gNewUserID=EITLERPGLOBAL.gAuthorityUserID;
            }
            else {
                EITLERPGLOBAL.gNewUserID=EITLERPGLOBAL.gUserID;
            }
        }
        //==========================================================//
        
        
        try{
            ClearFields();
            
            txtInquiryNo.setText((String)ObjInquiry.getAttribute("INQUIRY_NO").getObj());
            lblTitle.setText("INQUIRY "+txtInquiryNo.getText());
            lblRevNo.setText(Integer.toString((int)ObjInquiry.getAttribute("REVISION_NO").getVal()));
            txtInquiryDate.setText(EITLERPGLOBAL.formatDate((String)ObjInquiry.getAttribute("INQUIRY_DATE").getObj()));
            EITLERPGLOBAL.setComboIndex(cmbInquiryType,(String)ObjInquiry.getAttribute("INQUIRY_TYPE").getObj());
            txtRemarks.setText((String)ObjInquiry.getAttribute("REMARKS").getObj());
            txtPurpose.setText((String) ObjInquiry.getAttribute("PURPOSE").getObj());
            txtProject.setText((String) ObjInquiry.getAttribute("PROJECT").getObj());
            EITLERPGLOBAL.setComboIndex(cmbBuyer,(int) ObjInquiry.getAttribute("BUYER").getVal());
            EITLERPGLOBAL.setComboIndex(cmbStatus,(String)ObjInquiry.getAttribute("STATUS").getObj());
            
            EITLERPGLOBAL.setComboIndex(cmbHierarchy,(int)ObjInquiry.getAttribute("HIERARCHY_ID").getVal());
            
            txtLastQuotDate.setText(EITLERPGLOBAL.formatDate((String)ObjInquiry.getAttribute("LAST_QUOT_DATE").getObj()));
            
            //-------------- Show Suppliers -----------//
            FormatGridS();
            for(int i=1;i<=ObjInquiry.colSupp.size();i++) {
                clsInquirySupp ObjSupp=(clsInquirySupp)ObjInquiry.colSupp.get(Integer.toString(i));
                Object[] rowData=new Object[3];
                
                int PartyID=Integer.parseInt((String)ObjSupp.getAttribute("SUPP_ID").getObj());
                
                rowData[0]=Integer.toString(i);
                rowData[1]=Integer.toString(PartyID);
                rowData[2]=clsParty.getPartyNameByPartyID(EITLERPGLOBAL.gCompanyID, PartyID);
                DataModelS.addRow(rowData);
            }
            
            //======== Generating Grid for Document Approval Flow ========//
            FormatGridA();
            HashMap List=new HashMap();
            String DocNo=(String)ObjInquiry.getAttribute("INQUIRY_NO").getObj();
            List=ApprovalFlow.getDocumentFlow(EITLERPGLOBAL.gCompanyID, 18, DocNo);
            for(int i=1;i<=List.size();i++) {
                clsDocFlow ObjFlow=(clsDocFlow)List.get(Integer.toString(i));
                Object[] rowData=new Object[7];
                
                rowData[0]=Integer.toString(i);
                rowData[1]=clsUser.getUserName(EITLERPGLOBAL.gCompanyID,(int)ObjFlow.getAttribute("USER_ID").getVal());
                rowData[2]=(String)ObjFlow.getAttribute("STATUS").getObj();
                rowData[3]=clsDepartment.getDeptName(EITLERPGLOBAL.gCompanyID, (int)ObjFlow.getAttribute("DEPT_ID").getVal());
                rowData[4]=EITLERPGLOBAL.formatDate((String)ObjFlow.getAttribute("RECEIVED_DATE").getObj());
                rowData[5]=EITLERPGLOBAL.formatDate((String)ObjFlow.getAttribute("ACTION_DATE").getObj());
                rowData[6]=(String)ObjFlow.getAttribute("REMARKS").getObj();
                
                DataModelA.addRow(rowData);
            }
            
            
            
            //Showing Audit Trial History
            FormatGridHS();
            HashMap History=clsInquiry.getHistoryList(EITLERPGLOBAL.gCompanyID, DocNo);
            for(int i=1;i<=History.size();i++) {
                clsInquiry ObjHistory=(clsInquiry)History.get(Integer.toString(i));
                Object[] rowData=new Object[5];
                
                rowData[0]=Integer.toString((int)ObjHistory.getAttribute("REVISION_NO").getVal());
                rowData[1]=clsUser.getUserName(EITLERPGLOBAL.gCompanyID,(long)ObjHistory.getAttribute("UPDATED_BY").getVal());
                rowData[2]=EITLERPGLOBAL.formatDate((String)ObjHistory.getAttribute("ENTRY_DATE").getObj());
                
                String ApprovalStatus="";
                
                if(((String)ObjHistory.getAttribute("APPROVAL_STATUS").getObj()).equals("A")) {
                    ApprovalStatus="Approved";
                }
                
                if(((String)ObjHistory.getAttribute("APPROVAL_STATUS").getObj()).equals("F")) {
                    ApprovalStatus="Final Approved";
                }
                
                if(((String)ObjHistory.getAttribute("APPROVAL_STATUS").getObj()).equals("W")) {
                    ApprovalStatus="Waiting";
                }
                
                if(((String)ObjHistory.getAttribute("APPROVAL_STATUS").getObj()).equals("R")) {
                    ApprovalStatus="Rejected";
                }
                
                if(((String)ObjHistory.getAttribute("APPROVAL_STATUS").getObj()).equals("P")) {
                    ApprovalStatus="Pending";
                }
                
                if(((String)ObjHistory.getAttribute("APPROVAL_STATUS").getObj()).equals("C")) {
                    ApprovalStatus="Skiped";
                }
                
                
                rowData[3]=ApprovalStatus;
                rowData[4]=(String)ObjHistory.getAttribute("APPROVER_REMARKS").getObj();
                
                DataModelHS.addRow(rowData);
            }
            //============================================================//
            
            
            //============================================================//
            
            
            //========= Display Line Items =============//
            FormatGrid();
            
            for(int i=1;i<=ObjInquiry.colLineItems.size();i++) {
                //Insert New Row
                Object[] rowData=new Object[1];
                DataModelL.addRow(rowData);
                int NewRow=TableL.getRowCount()-1;
                
                clsInquiryItem ObjItem=(clsInquiryItem) ObjInquiry.colLineItems.get(Integer.toString(i));
                
                DataModelL.setValueByVariable("SR_NO",Integer.toString((int)ObjItem.getAttribute("SR_NO").getVal()),NewRow);
                DataModelL.setValueByVariable("ITEM_CODE",(String)ObjItem.getAttribute("ITEM_CODE").getObj(),NewRow);
                DataModelL.setValueByVariable("ITEM_EXTRA_DESC",(String)ObjItem.getAttribute("ITEM_EXTRA_DESC").getObj(),NewRow);
                DataModelL.setValueByVariable("MAKE",(String)ObjItem.getAttribute("MAKE").getObj(),NewRow);
                String ItemName=clsItem.getItemName(EITLERPGLOBAL.gCompanyID, (String)ObjItem.getAttribute("ITEM_CODE").getObj());
                DataModelL.setValueByVariable("ITEM_NAME",ItemName,NewRow);
                DataModelL.setValueByVariable("QTY",Double.toString(ObjItem.getAttribute("QTY").getVal()),NewRow);
                String UnitName=clsParameter.getParaDescription(EITLERPGLOBAL.gCompanyID,"UNIT",(int)ObjItem.getAttribute("UNIT").getVal());
                DataModelL.setValueByVariable("UNIT_NAME",UnitName,NewRow);
                DataModelL.setValueByVariable("REMARKS",(String)ObjItem.getAttribute("REMARKS").getObj(),NewRow);
                DataModelL.setValueByVariable("INDENT_NO",(String) ObjItem.getAttribute("INDENT_NO").getObj(),NewRow);
                DataModelL.setValueByVariable("DELIVERY_DATE",EITLERPGLOBAL.formatDate((String)ObjItem.getAttribute("DELIVERY_DATE").getObj()),NewRow);
                
                if (ObjItem.getAttribute("INDENT_SRNO").getVal() > 0) {
                    DataModelL.setValueByVariable("INDENT_SRNO",Integer.toString((int)ObjItem.getAttribute("INDENT_SRNO").getVal()),NewRow);
                }
                else  DataModelL.setValueByVariable("INDENT_SRNO","0",NewRow);
                DataModelL.setValueByVariable("UNIT_ID",Integer.toString((int) ObjItem.getAttribute("UNIT").getVal()),NewRow);
            }
            
            
            
            
            if(EditMode==0) {
                DataModelL.TableReadOnly(true);
            }
        }
        catch(Exception e){}
    }
    
