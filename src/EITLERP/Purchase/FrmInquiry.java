    private boolean Validate() {
        int ValidEntryCount=0;
        
        
        //Validates Item Entries
        if(TableL.getRowCount()<=0) {
            JOptionPane.showMessageDialog(null,"Please enter at least one item");
            return false;
        }
        
        
        if(TableS.getRowCount()<=0) {
            JOptionPane.showMessageDialog(null,"Please specify the suppliers");
            return false;
        }
        
        //Search in Table
        int ItemCol=DataModelL.getColFromVariable("ITEM_CODE");
        int QtyCol=DataModelL.getColFromVariable("QTY");
        
        for(int i=0;i<TableL.getRowCount();i++) {
            String ItemID="";
            double Rate=0,Qty=0;
            
            if(TableL.getValueAt(i, ItemCol)!=null && TableL.getValueAt(i, QtyCol)!=null) {
                ItemID=(String)TableL.getValueAt(i, ItemCol);
                Qty=Double.parseDouble((String)TableL.getValueAt(i,QtyCol));
                
                if(!clsItem.IsValidItemID(EITLERPGLOBAL.gCompanyID, ItemID)) {
                    JOptionPane.showMessageDialog(null,"Item ID "+ItemID+" is not valid item code");
                    return false;
                }
                
                if(clsItem.IsValidItemID(EITLERPGLOBAL.gCompanyID, ItemID) && Qty>0) {
                    ValidEntryCount++;
                }
                else {
                    JOptionPane.showMessageDialog(null,"Item entry is not valid. Please be sure to enter following information. \nValid Item ID, Quantity");
                    TableL.changeSelection(i, 1, false,false);
                    return false;
                }
            }
        }
        
        if(ValidEntryCount==0) {
            JOptionPane.showMessageDialog(null,"Item entry is not valid. Please verify");
            return false;
        }
        
        //Now Header level validations
        if(txtInquiryDate.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter Inquiry Date");
            return false;
        }
        
        if(cmbHierarchy.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(null,"Please select the hierarchy.");
            return false;
        }
        
        if((!OpgApprove.isSelected())&&(!OpgReject.isSelected())&&(!OpgFinal.isSelected())&&(!OpgHold.isSelected())) {
            JOptionPane.showMessageDialog(null,"Please select the Approval Action");
            return false;
        }
        
        if(!EITLERPGLOBAL.isDate(txtInquiryDate.getText())) {
            JOptionPane.showMessageDialog(null,"Invalid document date");
            return false;
        }
        
        if(!EITLERPGLOBAL.isDate(txtLastQuotDate.getText())) {
            JOptionPane.showMessageDialog(null,"Invalid last quotation date");
            return false;
        }
        
        
        java.sql.Date docDate=java.sql.Date.valueOf(EITLERPGLOBAL.formatDateDB(txtInquiryDate.getText()));
        
        if(!txtLastQuotDate.getText().trim().equals("")) {
            java.sql.Date quotDate=java.sql.Date.valueOf(EITLERPGLOBAL.formatDateDB(txtLastQuotDate.getText()));
            if(docDate.after(quotDate)) {
                JOptionPane.showMessageDialog(null,"Quotation due date must be greater than document date");
                return false;
            }
        }
        
        
        //New Check for Approval : When Rejecting remarks is must
        
        if(OpgReject.isSelected()&&txtToRemarks.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Please enter the remarks for rejection");
            return false;
        }
        
        //        if( (OpgApprove.isSelected()||OpgReject.isSelected())&&cmbSendTo.getItemCount()<=0)
        //        {
        //          JOptionPane.showMessageDialog(null,"Please select the user, to whom rejected document to be send");
        //          return false;
        //        }
        
        
        return true;
    }
    
