    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        // TODO add your handling code here:
       Cancelled=false;
      
       strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID)+" AND CIF_DATE>='"+EITLERPGLOBAL.FinFromDateDB+"' AND CIF_DATE<='"+EITLERPGLOBAL.FinToDateDB+"' ";
       
       if(!txtDocNo.getText().trim().equals(""))
       {
         strQuery=strQuery+" AND CIF_NO='"+txtDocNo.getText()+"' ";
       }
       
       if(!txtDateFrom.getText().equals(""))
       {
         strQuery=strQuery+" AND CIF_DATE>='"+EITLERPGLOBAL.formatDateDB(txtDateFrom.getText())+"' ";
       }

       if(!txtDateTo.getText().equals(""))
       {
         strQuery=strQuery+" AND CIF_DATE<='"+EITLERPGLOBAL.formatDateDB(txtDateTo.getText())+"' ";
       }
       
       if(!txtPONo.getText().trim().equals(""))
       {
         strQuery=strQuery+" AND PO_NO='"+txtPONo.getText()+"' ";
       }
       
       if(!txtPODate.getText().equals(""))
       {
         strQuery=strQuery+" AND PO_DATE='"+EITLERPGLOBAL.formatDateDB(txtPODate.getText())+"' ";
       }

       if(!txtInvoiceNo.getText().trim().equals(""))
       {
         strQuery=strQuery+" AND INVOICE_NO='"+txtInvoiceNo.getText()+"' ";
       }
       
       if(!txtInvoiceDate.getText().equals(""))
       {
         strQuery=strQuery+" AND INVOICE_DATE='"+EITLERPGLOBAL.formatDateDB(txtInvoiceDate.getText())+"' ";
       }
       
       if(cmbPriority.getSelectedIndex()!=-1)
       {
         strQuery=strQuery+" AND PRIORITY="+EITLERPGLOBAL.getComboCode(cmbPriority);
       }

       if(chkCancelled.isSelected())
       {
        strQuery=strQuery+" AND CANCELLED=1 ";
       }
       getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
