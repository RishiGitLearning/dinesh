    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        // TODO add your handling code here:
       Cancelled=false;
       
       String subQuery="";
      
       strQuery=" WHERE COMPANY_ID="+Long.toString(EITLERPGLOBAL.gCompanyID);  
       
       if(!txtDocNo.getText().equals(""))
       {
         strQuery=strQuery+" AND INQUIRY_NO='"+txtDocNo.getText()+"' ";
       }
       
       if(!txtDateFrom.getText().equals(""))
       {
         strQuery=strQuery+" AND INQUIRY_DATE>='"+EITLERPGLOBAL.formatDateDB(txtDateFrom.getText())+"' ";
       }

       if(!txtDateTo.getText().equals(""))
       {
         strQuery=strQuery+" AND INQUIRY_DATE<='"+EITLERPGLOBAL.formatDateDB(txtDateTo.getText())+"' ";
       }
      
       if(!txtSuppCode.getText().trim().equals(""))
       {
        strQuery=strQuery+" AND INQUIRY_NO IN (SELECT INQUIRY_NO FROM D_PUR_INQUIRY_SUPP WHERE COMPANY_ID="+EITLERPGLOBAL.gCompanyID+" AND SUPP_ID='"+txtSuppCode.getText()+"') ";
       }
       
       
       //====== Sub Query =======//
       if(!txtItemID.getText().trim().equals(""))
       {
        subQuery=subQuery+" AND ITEM_CODE='"+txtItemID.getText()+"' ";
       }
       
       if(!txtIndentNo.getText().trim().equals(""))
       {
        subQuery=subQuery+" AND INDENT_NO='"+txtIndentNo.getText()+"' ";   
       }
       
       if(!subQuery.trim().equals(""))
       {
        subQuery=" AND INQUIRY_NO IN (SELECT INQUIRY_NO FROM D_PUR_INQUIRY_DETAIL WHERE COMPANY_ID="+EITLERPGLOBAL.gCompanyID+" "+subQuery+")";
       }
       
       strQuery=strQuery+subQuery;
       //========== End Sub Query ============//

       
       getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
