    private void Save() {

        if (cmbHierarchy.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Please select the hierarchy.");
            return;
        }

        if ((!OpgApprove.isSelected()) && (!OpgReject.isSelected()) && (!OpgFinal.isSelected()) && (!OpgHold.isSelected())) {
            JOptionPane.showMessageDialog(null, "Please select the Approval Action");
            return;
        }

        if (OpgReject.isSelected() && txtToRemarks.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter the remarks for rejection");
            return;
        }

        if ((OpgApprove.isSelected() || OpgReject.isSelected()) && cmbSendTo.getItemCount() <= 0) {
            JOptionPane.showMessageDialog(null, "Please select the user, to whom rejected document to be send");
            return;
        }
        if(!EITLERPGLOBAL.formatDateDB(txtdocdt.getText()).equals(EITLERPGLOBAL.getCurrentDateDB())){
            JOptionPane.showMessageDialog(null, "You Can not Saving this Document... Document Date has been Expired...");
            return;
        }
        if (txtpckno.getText().trim().length() < 1) {
            JOptionPane.showMessageDialog(null, "Please Enter Packing Note No...It Should not be Blank...");
            return;
        }
        if (txtgrsweight.getText().trim().length() < 1) {
            JOptionPane.showMessageDialog(null, "Please Enter Gross Weight...It Should not be Blank...");
            return;
        }
        if (txttrns.getText().trim().length() < 1 && txttrns.isVisible()) {
            JOptionPane.showMessageDialog(null, "Please Select Transporter..It Should not be Blank...");
            return;
        }
        if (txtcheckpost.getText().trim().length() < 1 && txtcheckpost.isVisible()) {
            JOptionPane.showMessageDialog(null, "Please Select Check Post...It Should not be Blank...");
            return;
        }

        SetData();
        if (EditMode == EITLERPGLOBAL.ADD) {

            if (ObjDoc.Insert()) {
                // MoveLast();
                DisplayData();
                SetFields(true);
            } else {
                JOptionPane.showMessageDialog(null, "Error occured while saving. \nError is " + ObjDoc.LastError);
                return;
            }
        }

        if (EditMode == EITLERPGLOBAL.EDIT) {
            if (ObjDoc.Update()) {
                DisplayData();
                SetFields(true);
            } else {
                JOptionPane.showMessageDialog(null, "Error occured while saving. \nError is " + ObjDoc.LastError);
                return;
            }
        }

        EditMode = 0;
        SetFields(false);
        EnableToolbar();
        SetMenuForRights();
        try {
            frmPA.RefreshView();
        } catch (Exception e) {
        }
    }

