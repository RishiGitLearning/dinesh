    private void txtpcknoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpcknoKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == 112) //F1 Key pressed
        {
            LOV aList = new LOV();

            aList.SQL = "SELECT PACKING_NOTE_NO FROM FILTERFABRIC.FF_TRD_PACKING_HEADER "
                    + " WHERE PACKING_NOTE_NO NOT IN (SELECT PACKING_NOTE_NO FROM FILTERFABRIC.FF_TRD_INVOICE)"
                    + " AND PACKING_NOTE_NO NOT IN (SELECT PACKING_NOTE_NO FROM FILTERFABRIC.FF_TRD_INVOICE_PARAMETER WHERE PROCESSING_DATE='" + EITLERPGLOBAL.getCurrentDateDB() + "') ORDER BY PACKING_NOTE_NO";
            aList.ReturnCol = 1;
            aList.ShowReturnCol = true;
            aList.DefaultSearchOn = 1;

            if (aList.ShowLOV()) {
                txtpckno.setText(aList.ReturnVal);
                CheckPackingNote();
            }
        }
    }//GEN-LAST:event_txtpcknoKeyPressed

