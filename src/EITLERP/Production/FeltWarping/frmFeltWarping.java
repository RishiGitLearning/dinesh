    private void Save() {
        String productionDate, productionDocumentNo;
        float weight=0, length=0;
        int loomNo=0, i=0;
        String pieceNo="", formNo="", loomNoString="", remarks="";
        productionDate = txtFeltProductionDate.getText();
        formNo = txtFormNo.getText();
        
        //Form level validations
        if(productionDate.equals("") || !EITLERPGLOBAL.isDate(productionDate)) {
            JOptionPane.showMessageDialog(this,"Enter Valid Production Date.","Wrong Production Date",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // check Production Date is Within Financial Year?
        java.sql.Date FinFromDate=java.sql.Date.valueOf(EITLERPGLOBAL.FinFromDateDB);
        java.sql.Date FinToDate=java.sql.Date.valueOf(EITLERPGLOBAL.FinToDateDB);
        java.sql.Date ProductionDate=java.sql.Date.valueOf(EITLERPGLOBAL.formatDateDB(productionDate));
        if((ProductionDate.after(FinFromDate)||ProductionDate.compareTo(FinFromDate)==0)&&(ProductionDate.before(FinToDate)||ProductionDate.compareTo(FinToDate)==0)) {
            //Within the year
        }else {
            JOptionPane.showMessageDialog(this,"Production Date is Not Within Financial Year.","FINANCIAL YEAR ERROR",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(ProductionDate.compareTo(java.sql.Date.valueOf(EITLERPGLOBAL.getCurrentDateDB()))>0) {
            JOptionPane.showMessageDialog(this,"Production Date Must be Before or the Same Date as Today.","Wrong Production Date",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // creating document no
        productionDocumentNo = "FWP"+ productionDate.substring(6,10)+ productionDate.substring(3,5)+ productionDate.substring(0,2);
        
        if(productionDate.equals("") && !EITLERPGLOBAL.isDate(productionDate)) {
            JOptionPane.showMessageDialog(this,"Enter Valid Production Date");
            return;
        }
        
        if(EditMode==EITLERPGLOBAL.ADD) {
            if(ObjFeltWarping.checkProductionDateInDB(EITLERPGLOBAL.formatDateDB(productionDate))) {
                JOptionPane.showMessageDialog(frmFeltWarping.this,"Production Date "+productionDate+" is Already Exists","Production Date Already Exists",JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        
        if(formNo.equals("") || formNo.equals(null)){
            JOptionPane.showMessageDialog(this,"Enter Form No.");
            return;
        }
        
        if(productionDocumentNo.equals("")) {
            JOptionPane.showMessageDialog(this,"Enter Document No");
            return;
        }
        
        //Check the no. of items
        if(Table.getRowCount()<=0) {
            JOptionPane.showMessageDialog(this,"Enter Warping Details Before Saving.");
            return;
        }
        
        try{
            // check duplicate piece no in table
            for(int k=0;k<=Table.getRowCount()-1;k++) {
                for(int l=k;l<=Table.getRowCount()-1;l++){
                    if(l!=k && ((String)Table.getValueAt(k, 1)).trim().equals(((String)Table.getValueAt(l, 1)).trim()) ){
                        JOptionPane.showMessageDialog(this, "Same Piece No at Row "+(k+1)+" and "+(l+1),"ERROR",JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }
            }
            
            ObjFeltWarping.hmFeltWarpingDetails.clear();
            //Check the entered details in Table.
            for(i=0;i<=Table.getRowCount()-1;i++) {
                pieceNo = (String)Table.getValueAt(i, 1);
                if(pieceNo.equals("") || pieceNo.equals(null)){
                    JOptionPane.showMessageDialog(this,"Enter Piece No.");
                    return;
                }
                
                // check piece no is already warped?
                if(EditMode==EITLERPGLOBAL.ADD) {
                    if(ObjFeltWarping.checkPieceNoInDB(pieceNo.substring(0,5))) {
                        JOptionPane.showMessageDialog(frmFeltWarping.this,"Piece No."+pieceNo+" Is Already Warped"," Piece No Already Exists",JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }
                
                // check piece no is already warped?
                if(EditMode==EITLERPGLOBAL.EDIT) {
                    if(ObjFeltWarping.checkPieceNoInDB(pieceNo,productionDate)) {
                        JOptionPane.showMessageDialog(frmFeltWarping.this,"Piece No."+pieceNo+" Is Already Warped."," Piece No Already Exists",JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }
                
                loomNoString = (String)Table.getValueAt(i, 2);
                if(loomNoString.equals("") || loomNoString.equals(null)){
                    JOptionPane.showMessageDialog(this,"Enter Machine No.");
                    return;
                }else loomNo=Integer.parseInt(loomNoString);
                
                remarks = (String)Table.getValueAt(i, 3);
                
                clsFeltWarpingDetails ObjFeltWarpingDetails = new clsFeltWarpingDetails();
                
                ObjFeltWarpingDetails.setAttribute("PRODUCTION_PIECE_NO",pieceNo);
                ObjFeltWarpingDetails.setAttribute("LOOM_NO",loomNo);
                ObjFeltWarpingDetails.setAttribute("REMARKS",remarks);
                
                ObjFeltWarping.hmFeltWarpingDetails.put(Integer.toString(ObjFeltWarping.hmFeltWarpingDetails.size()+1), ObjFeltWarpingDetails);
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(this,"Enter Correct Details at Row "+(i+1));
            nfe.printStackTrace();
            return;
        }
        
        if(OpgReject.isSelected() && txtToRemarks.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this,"Enter the remarks for rejection");
            return;
        }
        
        if( (OpgApprove.isSelected()||OpgReject.isSelected()) && cmbSendTo.getItemCount()<=0) {
            JOptionPane.showMessageDialog(this,"Select the user, to whom rejected document to be send");
            return;
        }
        
        //set data for insert/update
        ObjFeltWarping.setAttribute("PRODUCTION_DATE", productionDate);
        ObjFeltWarping.setAttribute("PRODUCTION_DOCUMENT_NO",productionDocumentNo);
        ObjFeltWarping.setAttribute("PRODUCTION_FORM_NO",formNo);
        SetData();
        
        if(EditMode==EITLERPGLOBAL.ADD) {
            if(ObjFeltWarping.Insert()) {
                DisplayData();
            }
            else {
                JOptionPane.showMessageDialog(this,"Error occured while saving. Error is "+ObjFeltWarping.LastError);
                return;
            }
        }
        
        if(EditMode==EITLERPGLOBAL.EDIT) {
            if(ObjFeltWarping.Update()) {
                DisplayData();
            }
            else {
                JOptionPane.showMessageDialog(this,"Error occured while saving editing. Error is "+ObjFeltWarping.LastError);
                return;
            }
        }
        
        lblTitle.setBackground(Color.GRAY);
        EditMode=0;
        SetFields(false);
        EnableToolbar();
        SetMenuForRights();
        try {
            if(PENDING_DOCUMENT){
                frmPA.RefreshView();
                PENDING_DOCUMENT=false;
            }
        }catch(Exception e){e.printStackTrace();}
    }
    
