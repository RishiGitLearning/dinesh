    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String rejectionDate = txtRejectionDate.getText().trim();       
        String pieceNo = txtPieceNo.getText().trim();
        String formNo = txtFormNo.getText().trim();
        
        if(!rejectionDate.equals("") && EITLERPGLOBAL.isDate(rejectionDate)) {
            stringFindQuery=stringFindQuery+" AND REJ_DATE='"+EITLERPGLOBAL.formatDateDB(rejectionDate)+"' ";
        }
        
        if(!pieceNo.equals("")) {
            stringFindQuery=stringFindQuery+" AND REJ_PIECE_NO ='"+ pieceNo +"'";
        }
        
        if(!formNo.equals("")) {
            stringFindQuery=stringFindQuery+" AND REJ_FORM_NO ='"+ formNo +"'";
        }
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
        
