    private void Find() {
        Loader ObjLoader = new Loader(this, "EITLERP.Production.LoomWiseBeamFall.frmFindLoomWiseBeamFall", true);
        frmFindLoomWiseBeamFall ObjFindobjDM = (frmFindLoomWiseBeamFall) ObjLoader.getObj();

        if (ObjFindobjDM.Cancelled == false) {
            if (!objDM.Filter(ObjFindobjDM.stringFindQuery)) {
                JOptionPane.showMessageDialog(this, "No records found.", "Find Felt Design Master", JOptionPane.YES_OPTION);
            }
            MoveLast();
        }
    }

