    private void TableKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TableKeyReleased
        // TODO add your handling code here:
        int row = Table.getSelectedRow();
        int column = Table.getSelectedColumn();

        //=========== Cell Update Prevention Check ===========//
        String curValue = ((String) Table.getValueAt(row, column - 1)).trim();
        if (curValue.equals("")) {
            return;
        }
        //====================================================//
        if (DoNotEvaluate) {
            return;
        }

        try {
            double dayWeight = 0, totalWeight = 0, previousWeight = 0, dayTotalWeight = 0, monthWeight = 0;
            dayTotalWeight = Double.parseDouble(txtDayWeight.getText());
            monthWeight = Double.parseDouble(txtMonthWeight.getText());

            for (int i = 0; i < Table.getRowCount(); i++) {
                if (Table.getValueAt(i, 9) != null && !Table.getValueAt(i, 9).toString().equals("")) {
                    dayWeight = dayWeight + Double.parseDouble(((String) Table.getValueAt(i, 9)).trim());
                    totalWeight = monthWeight + dayTotalWeight;
                }
            }

            txtDayWeight.setText(Double.toString(EITLERPGLOBAL.round(dayWeight, 2)));
            txtTotalWeight.setText(Double.toString(EITLERPGLOBAL.round(totalWeight, 2)));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_TableKeyReleased

