    private void cmdFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFindActionPerformed
        Cancelled=false;
        
        String proformaDate = txtProformaDate.getText();       
        String proformaNo = txtProformaNo.getText();        
        
        if(!proformaDate.equals("")) {
            stringFindQuery+=" PROFORMA_DATE='"+EITLERPGLOBAL.formatDateDB(proformaDate)+"' ";
        }
        
        if(!proformaNo.equals("")) {
            stringFindQuery+=" PROFORMA_NO ='"+ proformaNo +"'";
        }  
        
        if(!proformaNo.equals("") && !proformaDate.equals("")) {
            stringFindQuery=" PROFORMA_NO ='"+ proformaNo +"' AND PROFORMA_DATE='"+EITLERPGLOBAL.formatDateDB(proformaDate)+"'";
        }
        System.out.println(stringFindQuery);
        getParent().getParent().getParent().getParent().show(false);
    }//GEN-LAST:event_cmdFindActionPerformed
    
    
